# Generate the analyst-facing standalone scripts from package-native modules.
# Files under inst/scripts named in `econimap_standalone_spec()` are build
# artifacts. Edit their R/ modules, then run this script.

econimap_generator_root <- function() {
  file_arg <- grep("^--file=", commandArgs(FALSE), value = TRUE)
  if (length(file_arg)) {
    script <- normalizePath(sub("^--file=", "", file_arg[1]), mustWork = TRUE)
    return(dirname(dirname(script)))
  }
  candidates <- c(getwd(), file.path(getwd(), "econimap"), file.path(getwd(), ".."))
  hit <- candidates[file.exists(file.path(candidates, "DESCRIPTION")) & dir.exists(file.path(candidates, "R"))]
  if (!length(hit)) stop("Could not locate the econimap package root.", call. = FALSE)
  normalizePath(hit[1], mustWork = TRUE)
}

