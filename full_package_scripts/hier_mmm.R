normalize_curve_type_hier_mmm <- function(x) {
  out <- tolower(trimws(as.character(x %||% "weibull")))
  out[is.na(out) | !nzchar(out)] <- "weibull"
  out[out %in% c("weibull_cdf", "exponential", "exp")] <- "weibull"
  out[out %in% c("hill_function", "hill_curve", "logistic_saturation")] <- "hill"
  bad <- setdiff(unique(out), c("weibull", "hill"))
  if (length(bad)) stop("Unsupported curve_type values: ", paste(bad, collapse = ", "), ". Use 'weibull' or 'hill'.")
  out
}

saturate_media_hier_mmm <- function(x, cvalue, dvalue = 1, curve_type = "weibull") {
  z <- pmax(suppressWarnings(as.numeric(x)), 1e-12) * suppressWarnings(as.numeric(cvalue)[1])
  dvalue <- suppressWarnings(as.numeric(dvalue)[1])
  if (!is.finite(dvalue) || dvalue <= 0) dvalue <- 1
  curve_type <- normalize_curve_type_hier_mmm(curve_type)[1]
  pow_z <- z ^ dvalue
  if (identical(curve_type, "hill")) {
    pow_z / (1 + pow_z)
  } else {
    1 - exp(-pow_z)
  }
}

curve_rate_from_anchor_hier_mmm <- function(x,
                                            rrate,
                                            dvalue = 1,
                                            anchor_saturation = 0.50,
                                            curve_type = "weibull",
                                            train_mask = NULL,
                                            normalize_curve_x = TRUE) {
  x_ref <- pmax(suppressWarnings(as.numeric(x)), 0)
  x_ref[!is.finite(x_ref)] <- 0
  n <- length(x_ref)
  if (is.null(train_mask)) train_mask <- rep(TRUE, n)
  train_mask <- rep(as.logical(train_mask), length.out = n)
  train_mask[is.na(train_mask)] <- FALSE
  if (!any(train_mask)) train_mask <- rep(TRUE, n)
  rrate <- min(max(suppressWarnings(as.numeric(rrate)[1]), 0), 0.999)
  dvalue <- suppressWarnings(as.numeric(dvalue)[1])
  if (!is.finite(dvalue) || dvalue <= 0) dvalue <- 1
  anchor_saturation <- clip_numeric(suppressWarnings(as.numeric(anchor_saturation)[1]), 0.01, 0.99)
  if (!is.finite(anchor_saturation)) return(NA_real_)

  carry <- numeric(n)
  carry_state <- 0
  for (ii in seq_along(x_ref)) {
    carry_state <- x_ref[ii] + rrate * carry_state
    carry[ii] <- carry_state
  }
  carry_scale <- 1
  if (isTRUE(normalize_curve_x)) {
    carry_scale <- mean(carry[train_mask], na.rm = TRUE)
    if (!is.finite(carry_scale) || is.na(carry_scale) || abs(carry_scale) <= 1e-8) carry_scale <- 1
  }
  z <- carry / carry_scale
  active <- train_mask & is.finite(z) & z > 0 & is.finite(x_ref) & x_ref > 0
  anchor_x <- stats::median(z[active], na.rm = TRUE)
  if (!is.finite(anchor_x) || anchor_x <= 0) return(NA_real_)

  curve_type <- normalize_curve_type_hier_mmm(curve_type)[1]
  if (identical(curve_type, "hill")) {
    (anchor_saturation / (1 - anchor_saturation)) ^ (1 / dvalue) / anchor_x
  } else {
    (-log(1 - anchor_saturation)) ^ (1 / dvalue) / anchor_x
  }
}

