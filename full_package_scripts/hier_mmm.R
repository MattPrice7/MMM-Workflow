media_transform_hier_mmm <- function(x,
                                     rrate,
                                     cvalue,
                                     dvalue = 1,
                                     curve_type = "weibull",
                                     train_mask = NULL,
                                     normalize_curve_x = TRUE,
                                     multiplier = 1) {
  x_ref <- pmax(suppressWarnings(as.numeric(x)), 0)
  x_ref[!is.finite(x_ref)] <- 0
  x_eval <- pmax(x_ref * as.numeric(multiplier)[1], 0)
  n <- length(x_ref)
  if (is.null(train_mask)) train_mask <- rep(TRUE, n)
  train_mask <- rep(as.logical(train_mask), length.out = n)
  train_mask[is.na(train_mask)] <- FALSE
  if (!any(train_mask)) train_mask <- rep(TRUE, n)
  rrate <- min(max(suppressWarnings(as.numeric(rrate)[1]), 0), 0.999)
  cvalue <- suppressWarnings(as.numeric(cvalue)[1])
  dvalue <- suppressWarnings(as.numeric(dvalue)[1])
  if (!is.finite(cvalue)) cvalue <- 0
  if (!is.finite(dvalue) || dvalue <= 0) dvalue <- 1

  adstock_one <- function(z) {
    carry <- 0
    out <- numeric(length(z))
    for (ii in seq_along(z)) {
      carry <- z[ii] + rrate * carry
      out[ii] <- carry
    }
    out
  }

  carry_ref <- adstock_one(x_ref)
  carry_eval <- adstock_one(x_eval)
  carry_scale <- 1
  if (isTRUE(normalize_curve_x)) {
    carry_scale <- mean(carry_ref[train_mask], na.rm = TRUE)
    if (!is.finite(carry_scale) || is.na(carry_scale) || abs(carry_scale) <= 1e-8) carry_scale <- 1
  }
  sat_ref <- saturate_media_hier_mmm(carry_ref / carry_scale, cvalue = cvalue, dvalue = dvalue, curve_type = curve_type)
  sat_eval <- saturate_media_hier_mmm(carry_eval / carry_scale, cvalue = cvalue, dvalue = dvalue, curve_type = curve_type)
  trans_mean <- mean(sat_ref[train_mask], na.rm = TRUE)
  if (!is.finite(trans_mean) || is.na(trans_mean) || abs(trans_mean) <= 1e-8) trans_mean <- if (isTRUE(normalize_curve_x)) 1 else 1e-8
  transformed <- if (isTRUE(normalize_curve_x)) sat_eval / trans_mean else sat_eval
  list(
    transformed = transformed,
    carry_scale = carry_scale,
    transformed_mean = trans_mean,
    center_value = if (isTRUE(normalize_curve_x)) 1 else trans_mean,
    adstock = carry_eval,
    saturation = sat_eval
  )
}

variable_contribution_rows_hier_mmm <- function(fit_obj, variable, multiplier = 1, posterior_params = NULL) {
  var_name <- as.character(variable)[1]
  dt <- copy(as.data.table(fit_obj$data))
  vl <- copy(as.data.table(fit_obj$variable_lookup))[order(variable_idx)]
  if (!"curve_type" %in% names(vl)) vl[, curve_type := "weibull"]
  if (!(var_name %in% vl$variable)) stop("Unknown modeled variable: ", var_name)
  pm <- posterior_params %||% extract_pm_from_fit_obj_hier_mmm(fit_obj)
  j <- vl$variable_idx[match(var_name, vl$variable)]
  has_curve <- vl[variable_idx == j, has_curve][1] == 1L
  out <- numeric(nrow(dt))
  scale <- dt$rescale_factor__
  if (is.null(scale)) scale <- rep(1, nrow(dt))

  if (!has_curve) {
    beta_vec <- pm$beta[dt$group_idx, j]
    out <- beta_vec * (dt[[var_name]] * multiplier) * scale
    return(out)
  }

  curve_pos <- match(j, fit_obj$stan_data$curve_idx)
  if (is.na(curve_pos)) stop("Internal error: curve variable not found in curve_idx: ", var_name)
  rr <- pm$rrate[j]
  cv <- pm$cvalue[j]
  dv <- pm$dvalue[j]
  curve_type <- vl[variable_idx == j, curve_type][1] %||% "weibull"

  for (g in sort(unique(dt$group_idx))) {
    idx <- which(dt$group_idx == g)
    tr <- media_transform_hier_mmm(
      x = dt[[var_name]][idx],
      rrate = rr,
      cvalue = cv,
      dvalue = dv,
      curve_type = curve_type,
      train_mask = !dt$is_holdout__[idx],
      normalize_curve_x = fit_obj$normalize_curve_x,
      multiplier = multiplier
    )
    trans <- tr$transformed
    out[idx] <- pm$beta[g, j] * trans * scale[idx]
  }
  out
}

variable_contribution_sum_hier_mmm <- function(fit_obj, variable, multiplier = 1, posterior_params = NULL) {
  out <- variable_contribution_rows_hier_mmm(fit_obj, variable, multiplier = multiplier, posterior_params = posterior_params)
  sum(out, na.rm = TRUE)
}

build_response_curves_hier_mmm <- function(fit_obj,
                                           spend_map = NULL,
                                           raw_data = NULL,
                                           variables = NULL,
                                           multiplier_grid = seq(0, 3, by = 0.05),
                                           step_pct = 0.01,
                                           response_curve_scope = c("auto", "total", "group", "both"),
                                           spend_suffix = "_spend",
                                           value_per_kpi = NA_real_) {
  response_curve_scope <- match.arg(response_curve_scope)
  dt <- copy(as.data.table(fit_obj$data))
  group_col <- fit_obj$group_col %||% "group"
  if (!(group_col %in% names(dt))) dt[, (group_col) := "ALL"]
  vars <- if (!is.null(variables)) {
    unique(as.character(variables))
  } else if (!is.null(fit_obj$variable_lookup) && "variable" %in% names(as.data.table(fit_obj$variable_lookup))) {
    unique(as.character(as.data.table(fit_obj$variable_lookup)$variable))
  } else {
    character()
  }

  sm <- normalize_spend_map_hier_mmm(
    fit_obj,
    spend_map = spend_map,
    raw_data = raw_data,
    spend_suffix = spend_suffix,
    variables = vars
  )
  if (nrow(sm)) vars <- intersect(vars, sm$variable)
  vars <- vars[vars %in% names(dt)]
  if (!length(vars)) return(data.table())

  multiplier_grid <- sort(unique(pmax(suppressWarnings(as.numeric(multiplier_grid)), 0)))
  multiplier_grid <- multiplier_grid[is.finite(multiplier_grid)]
  if (!length(multiplier_grid)) stop("multiplier_grid must contain finite non-negative values.")
  step_pct <- suppressWarnings(as.numeric(step_pct)[1])
  if (!is.finite(step_pct) || step_pct <= 0) step_pct <- 0.01
  value_per_kpi <- suppressWarnings(as.numeric(value_per_kpi)[1])

  raw_aligned <- get_aligned_raw_data_hier_mmm(fit_obj, raw_data = raw_data)
  if (!(group_col %in% names(raw_aligned))) raw_aligned[, (group_col) := dt[[group_col]]]
  group_values <- sort(unique(as.character(dt[[group_col]])))
  has_meaningful_groups <- length(group_values) > 1L
  include_group <- response_curve_scope %in% c("group", "both") ||
    (identical(response_curve_scope, "auto") && has_meaningful_groups)
  include_total <- response_curve_scope %in% c("total", "both", "auto")

  spend_by_group <- data.table(variable = character(), group = character(), current_spend = numeric())
  if (nrow(sm)) {
    spend_by_group <- rbindlist(lapply(seq_len(nrow(sm)), function(i) {
      v <- sm$variable[i]
      sc <- sm$spend_col[i]
      if (!(sc %in% names(raw_aligned))) {
        return(data.table(variable = v, group = group_values, current_spend = NA_real_))
      }
      tmp <- copy(raw_aligned[, c(group_col, sc), with = FALSE])
      setnames(tmp, c(group_col, sc), c("group", "spend"))
      tmp[, `:=`(group = as.character(group), spend = suppressWarnings(as.numeric(spend)))]
      tmp[, .(current_spend = sum(spend, na.rm = TRUE)), by = group][, variable := v]
    }), fill = TRUE)
  }

  support_by_group <- rbindlist(lapply(vars, function(v) {
    tmp <- dt[, .(current_support = sum(pmax(suppressWarnings(as.numeric(get(v))), 0), na.rm = TRUE)), by = group_col]
    setnames(tmp, group_col, "group")
    tmp[, `:=`(group = as.character(group), variable = v)]
    tmp
  }), fill = TRUE)

  make_rows_one <- function(v, m) {
    contrib <- variable_contribution_rows_hier_mmm(fit_obj, v, multiplier = m)
    base <- variable_contribution_rows_hier_mmm(fit_obj, v, multiplier = 1)
    up <- variable_contribution_rows_hier_mmm(fit_obj, v, multiplier = m + step_pct)
    row_dt <- data.table(
      group = as.character(dt[[group_col]]),
      contribution = contrib,
      current_contribution_row = base,
      up_contribution = up
    )
    by_group <- row_dt[, .(
      contribution = sum(contribution, na.rm = TRUE),
      current_contribution = sum(current_contribution_row, na.rm = TRUE),
      up_contribution = sum(up_contribution, na.rm = TRUE)
    ), by = group]
    by_group[, `:=`(
      scope = "group",
      variable = v,
      spend_multiplier = m
    )]
    by_group_all <- copy(by_group)
    if (!isTRUE(include_group)) by_group <- by_group[0]
    if (isTRUE(include_total)) {
      total <- by_group_all[, .(
        group = "ALL",
        contribution = sum(contribution, na.rm = TRUE),
        current_contribution = sum(current_contribution, na.rm = TRUE),
        up_contribution = sum(up_contribution, na.rm = TRUE),
        scope = "total",
        variable = v,
        spend_multiplier = m
      )]
      by_group <- rbindlist(list(by_group, total), fill = TRUE)
    }
    by_group
  }

  out <- rbindlist(lapply(vars, function(v) {
    rbindlist(lapply(multiplier_grid, function(m) make_rows_one(v, m)), fill = TRUE)
  }), fill = TRUE)

  if (nrow(spend_by_group)) {
    out[spend_by_group, current_spend := i.current_spend, on = .(variable, group)]
    total_spend <- spend_by_group[, .(current_spend = sum(current_spend, na.rm = TRUE)), by = variable][, group := "ALL"]
    out[is.na(current_spend) | !is.finite(current_spend), current_spend := NA_real_]
    out[total_spend, current_spend := i.current_spend, on = .(variable, group)]
  } else {
    out[, current_spend := NA_real_]
  }
  out[support_by_group, current_support := i.current_support, on = .(variable, group)]
  total_support <- support_by_group[, .(current_support = sum(current_support, na.rm = TRUE)), by = variable][, group := "ALL"]
  out[total_support, current_support := i.current_support, on = .(variable, group)]
  out[!is.finite(current_support), current_support := NA_real_]

  out[, `:=`(
    spend = current_spend * spend_multiplier,
    support = current_support * spend_multiplier,
    contribution_vs_current = contribution - current_contribution,
    incremental_contribution_for_mroi = up_contribution - contribution,
    incremental_spend_for_mroi = current_spend * step_pct
  )]
  out[, `:=`(
    roi = fifelse(is.finite(spend) & abs(spend) > 1e-8, contribution / spend, NA_real_),
    mroi = fifelse(is.finite(incremental_spend_for_mroi) & abs(incremental_spend_for_mroi) > 1e-8,
                   incremental_contribution_for_mroi / incremental_spend_for_mroi, NA_real_),
    cost_per_kpi = fifelse(is.finite(contribution) & contribution > 1e-8, spend / contribution, NA_real_),
    value_per_cost = fifelse(is.finite(value_per_kpi) & is.finite(spend) & abs(spend) > 1e-8,
                             contribution * value_per_kpi / spend, NA_real_)
  )]
  out[, `:=`(
    response_curve_basis = "stan_posterior_mean_point_estimate",
    uncertainty_note = "Response curves are generated from posterior means. Posterior draw intervals are not included in this table.",
    flighting_assumption = "Each channel curve scales the historical support path within the selected model data.",
    cost_assumption = "Spend scales with support using the historical spend/support relationship implied by spend_map."
  )]
  setcolorder(out, intersect(c(
    "scope", "group", "variable", "spend_multiplier", "spend", "current_spend",
    "support", "current_support", "contribution", "current_contribution",
    "contribution_vs_current", "roi", "mroi", "cost_per_kpi", "value_per_cost",
    "incremental_spend_for_mroi", "incremental_contribution_for_mroi",
    "response_curve_basis", "uncertainty_note", "flighting_assumption", "cost_assumption"
  ), names(out)))
  out[order(variable, scope, group, spend_multiplier)]
}

extract_posterior_draw_params_hier_mmm <- function(fit_obj,
                                                   max_draws = 200L,
                                                   seed = 123L) {
  if (!is.null(fit_obj$posterior_draw_params)) return(fit_obj$posterior_draw_params)
  if (is.null(fit_obj$fit) || !is.function(fit_obj$fit$draws)) {
    stop("fit_obj does not contain a CmdStan fit with posterior draws.", call. = FALSE)
  }
  require_hier_mmm_pkg("posterior", "extracting posterior draws for response-curve uncertainty")
  pm_mean <- extract_pm_from_fit_obj_hier_mmm(fit_obj)
  draw_mat <- as.matrix(fit_obj$fit$draws(variables = c("beta", "rrate", "cvalue", "dvalue"), format = "matrix"))
  if (!nrow(draw_mat)) stop("No posterior draws were available for beta/curve parameters.", call. = FALSE)
  max_draws <- suppressWarnings(as.integer(max_draws)[1])
  if (!is.finite(max_draws) || max_draws <= 0L) max_draws <- nrow(draw_mat)
  if (nrow(draw_mat) > max_draws) {
    set.seed(as.integer(seed)[1] %||% 123L)
    keep <- sort(sample(seq_len(nrow(draw_mat)), max_draws))
    draw_mat <- draw_mat[keep, , drop = FALSE]
  }
  cn <- colnames(draw_mat)
  get_draw_value <- function(row_i, nm, default) {
    pos <- match(nm, cn)
    if (is.na(pos)) return(default)
    val <- suppressWarnings(as.numeric(draw_mat[row_i, pos]))
    if (is.finite(val)) val else default
  }
  G <- fit_obj$stan_data$G %||% nrow(pm_mean$beta)
  J <- fit_obj$stan_data$J %||% ncol(pm_mean$beta)
  curve_idx <- fit_obj$stan_data$curve_idx %||% integer()
  params <- vector("list", nrow(draw_mat))
  draw_ids <- rownames(draw_mat)
  if (is.null(draw_ids) || any(!nzchar(draw_ids))) draw_ids <- as.character(seq_len(nrow(draw_mat)))
  for (row_i in seq_len(nrow(draw_mat))) {
    beta <- pm_mean$beta
    for (g in seq_len(G)) {
      for (j in seq_len(J)) {
        beta[g, j] <- get_draw_value(row_i, sprintf("beta[%d,%d]", g, j), beta[g, j])
      }
    }
    rrate <- pm_mean$rrate
    cvalue <- pm_mean$cvalue
    dvalue <- pm_mean$dvalue
    if (length(curve_idx)) {
      for (k in seq_along(curve_idx)) {
        j <- curve_idx[k]
        rrate[j] <- get_draw_value(row_i, sprintf("rrate[%d]", k), rrate[j])
        cvalue[j] <- get_draw_value(row_i, sprintf("cvalue[%d]", k), cvalue[j])
        dvalue[j] <- get_draw_value(row_i, sprintf("dvalue[%d]", k), dvalue[j])
      }
    }
    params[[row_i]] <- list(
      draw_id = as.character(draw_ids[row_i]),
      beta = beta,
      rrate = rrate,
      cvalue = cvalue,
      dvalue = dvalue,
      alpha_flat = pm_mean$alpha_flat,
      gamma = pm_mean$gamma,
      level_component = pm_mean$level_component,
      cycle_component = pm_mean$cycle_component,
      season_component = pm_mean$season_component,
      level_state = pm_mean$level_state,
      sigma_y = pm_mean$sigma_y
    )
  }
  list(draw_ids = as.character(draw_ids), params = params)
}

build_response_curves_draws_hier_mmm <- function(fit_obj,
                                                 spend_map = NULL,
                                                 raw_data = NULL,
                                                 variables = NULL,
                                                 multiplier_grid = seq(0, 3, by = 0.05),
                                                 step_pct = 0.01,
                                                 response_curve_scope = c("auto", "total", "group", "both"),
                                                 spend_suffix = "_spend",
                                                 value_per_kpi = NA_real_,
                                                 max_draws = 200L,
                                                 seed = 123L) {
  response_curve_scope <- match.arg(response_curve_scope)
  draw_params <- extract_posterior_draw_params_hier_mmm(fit_obj, max_draws = max_draws, seed = seed)
  if (is.null(draw_params$params) || !length(draw_params$params)) return(data.table())
  out <- rbindlist(lapply(draw_params$params, function(pm_draw) {
    draw_id <- as.character(pm_draw$draw_id %||% "")
    pm_draw$draw_id <- NULL
    tmp_fit <- fit_obj
    tmp_fit$posterior_means <- pm_draw
    rc <- build_response_curves_hier_mmm(
      fit_obj = tmp_fit,
      spend_map = spend_map,
      raw_data = raw_data,
      variables = variables,
      multiplier_grid = multiplier_grid,
      step_pct = step_pct,
      response_curve_scope = response_curve_scope,
      spend_suffix = spend_suffix,
      value_per_kpi = value_per_kpi
    )
    if (nrow(rc)) {
      rc[, `:=`(
        .draw = draw_id,
        response_curve_basis = "stan_posterior_draw",
        uncertainty_note = "Response curve generated from one posterior draw of beta and curve parameters."
      )]
      setcolorder(rc, c(".draw", setdiff(names(rc), ".draw")))
    }
    rc
  }), fill = TRUE)
  out[order(variable, .draw, scope, group, spend_multiplier)]
}

