business_prior_metric_alias_hier_mmm <- function(x) {
  x <- tolower(trimws(as.character(x %||% "")))
  x <- gsub("[^a-z0-9]+", "_", x)
  x <- gsub("^_|_$", "", x)
  out <- x
  out[x %in% c("coefficient", "beta", "model_coef", "coef_prior")] <- "coef"
  out[x %in% c("incremental_kpi_per_cost", "incremental_outcome_per_cost", "incremental_kpi_per_dollar", "ikpc", "kpi_per_cost", "kpi_per_dollar", "outcome_per_cost", "outcome_per_dollar")] <- "ikpc"
  out[x %in% c("cost_per_kpi", "cost_per_outcome", "cost_per_conversion", "cost_per_subscriber", "cost_per_lead", "cpa", "cpkpi")] <- "cpkpi"
  out[x %in% c("roi", "return_on_investment")] <- "roi"
  out[x %in% c("mroi", "marginal_roi", "marginal_return_on_investment")] <- "mroi"
  out
}

business_prior_metric_candidates_hier_mmm <- function(metric) {
  switch(
    metric,
    coef = c("prior_mean", "coef", "coefficient", "beta", "model_coef", "coef_prior"),
    ikpc = c("prior_mean", "ikpc", "incremental_kpi_per_cost", "incremental_outcome_per_cost", "kpi_per_cost", "kpi_per_dollar", "outcome_per_cost"),
    cpkpi = c("prior_mean", "cpkpi", "cost_per_kpi", "cost_per_outcome", "cost_per_subscriber", "cost_per_lead", "cpa"),
    roi = c("prior_mean", "roi"),
    mroi = c("prior_mean", "mroi", "marginal_roi"),
    c("prior_mean", metric)
  )
}

business_prior_row_num_hier_mmm <- function(bp, i, candidates) {
  candidates <- candidates[candidates %in% names(bp)]
  for (cc in candidates) {
    val <- suppressWarnings(as.numeric(bp[[cc]][i]))
    if (is.finite(val)) return(val)
  }
  NA_real_
}

business_prior_detect_metric_hier_mmm <- function(bp, i) {
  if ("prior_metric" %in% names(bp)) {
    pm <- business_prior_metric_alias_hier_mmm(bp$prior_metric[i])
    if (nzchar(pm) && !is.na(pm)) return(pm)
  }
  if ("metric" %in% names(bp)) {
    pm <- business_prior_metric_alias_hier_mmm(bp$metric[i])
    if (nzchar(pm) && !is.na(pm)) return(pm)
  }
  for (metric in c("coef", "ikpc", "cpkpi", "roi", "mroi")) {
    if (is.finite(business_prior_row_num_hier_mmm(bp, i, business_prior_metric_candidates_hier_mmm(metric)))) {
      return(metric)
    }
  }
  NA_character_
}

business_prior_sd_precision_hier_mmm <- function(bp, i, metric) {
  base <- business_prior_metric_candidates_hier_mmm(metric)
  sd_cols <- unique(c("prior_sd", paste0(base, "_sd"), paste0(base, "_se")))
  precision_cols <- unique(c("prior_precision", paste0(base, "_precision")))
  sd <- business_prior_row_num_hier_mmm(bp, i, sd_cols)
  precision <- business_prior_row_num_hier_mmm(bp, i, precision_cols)
  if ((!is.finite(sd) || sd <= 0) && is.finite(precision) && precision > 0) {
    sd <- 1 / sqrt(precision)
  }
  list(sd = sd, precision = precision)
}

kpi_value_for_business_prior_hier_mmm <- function(kpi_value_per_outcome, bp, i, variable) {
  if ("kpi_value_per_outcome" %in% names(bp)) {
    val <- suppressWarnings(as.numeric(bp$kpi_value_per_outcome[i]))
    if (is.finite(val) && val > 0) return(val)
  }
  if (length(kpi_value_per_outcome) > 1L && !is.null(names(kpi_value_per_outcome))) {
    val <- suppressWarnings(as.numeric(kpi_value_per_outcome[[as.character(variable)]]))
    if (is.finite(val) && val > 0) return(val)
  }
  val <- suppressWarnings(as.numeric(kpi_value_per_outcome)[1])
  if (!is.finite(val) || val <= 0) val <- 1
  val
}

normalize_business_spend_map_hier_mmm <- function(metadata_input, spend_map = NULL) {
  if (!is.null(spend_map)) {
    sm <- as.data.table(copy(spend_map))
    if (!"variable" %in% names(sm)) stop("spend_map must include variable.", call. = FALSE)
    if (!"spend_col" %in% names(sm)) {
      alt <- intersect(c("cost_col", "spend", "cost"), names(sm))
      if (!length(alt)) stop("spend_map must include spend_col or cost_col.", call. = FALSE)
      sm[, spend_col := as.character(get(alt[1]))]
    }
    return(sm[, .(variable = as.character(variable), spend_col = as.character(spend_col))])
  }
  md <- read_input_table(metadata_input)
  if (!"variable" %in% names(md)) return(data.table(variable = character(), spend_col = character()))
  scol <- pick_first_existing_col(md, c("spend_col", "cost_col"))
  if (is.na(scol)) return(data.table(variable = character(), spend_col = character()))
  md[, .(variable = as.character(variable), spend_col = as.character(get(scol)))]
}

business_design_sum_hier_mmm <- function(prep, variable) {
  sd <- prep$stan_data
  vl <- prep$variable_lookup
  vv <- as.character(variable)[1]
  row <- vl[variable == vv][1]
  if (!nrow(row)) return(NA_real_)
  j <- row$variable_idx[1]
  if (row$has_curve[1] == 1L) {
    k <- match(j, sd$curve_idx)
    if (is.na(k)) return(NA_real_)
    x <- sd$X_curve_fixed[, k]
  } else {
    x <- sd$X[, j]
  }
  train <- sd$is_train == 1L
  sum(as.numeric(x)[train], na.rm = TRUE)
}

apply_business_priors_to_metadata_hier_mmm <- function(data,
                                                       metadata_input,
                                                       business_priors,
                                                       dep_var_col,
                                                       group_col,
                                                       time_col,
                                                       entity_col,
                                                       spend_map = NULL,
                                                       kpi_value_per_outcome = 1,
                                                       default_relative_sd = 0.50,
                                                       min_coef_sd = 1e-6,
                                                       max_precision = Inf,
                                                       prep_args = list(),
                                                       ...) {
  bp <- as.data.table(copy(business_priors))
  if (!nrow(bp)) return(list(metadata = read_input_table(metadata_input), business_prior_audit = data.table()))
  if (!"variable" %in% names(bp)) stop("business_priors must include variable.", call. = FALSE)
  bp[, variable := as.character(variable)]
  if (anyDuplicated(bp$variable)) {
    stop("business_priors contains duplicate variable rows: ", paste(unique(bp$variable[duplicated(bp$variable)]), collapse = ", "), call. = FALSE)
  }
  default_relative_sd <- suppressWarnings(as.numeric(default_relative_sd)[1])
  if (!is.finite(default_relative_sd) || default_relative_sd <= 0) default_relative_sd <- 0.50
  min_coef_sd <- suppressWarnings(as.numeric(min_coef_sd)[1])
  if (!is.finite(min_coef_sd) || min_coef_sd <= 0) min_coef_sd <- 1e-6
  max_precision <- suppressWarnings(as.numeric(max_precision)[1])
  if (!is.finite(max_precision) || max_precision <= 0) max_precision <- Inf

  prep_formals <- names(formals(prepare_stan_data_hier_mmm))
  extra_prep_args <- list(...)
  if (length(extra_prep_args)) prep_args <- modifyList(prep_args, extra_prep_args)
  prep_call <- prep_args[names(prep_args) %in% prep_formals]
  prep_call <- modifyList(prep_call, list(
    data = data,
    metadata_input = metadata_input,
    dep_var_col = dep_var_col,
    group_col = group_col,
    time_col = time_col,
    entity_col = entity_col
  ))
  prep <- do.call(prepare_stan_data_hier_mmm, prep_call)
  raw <- read_input_table(data)
  join_keys <- c(group_col, time_col, entity_col)
  train_info <- prep$data[, c(join_keys, "is_holdout__", "rescale_factor__"), with = FALSE]
  raw[train_info, `:=`(
    business_prior_is_holdout__ = i.is_holdout__,
    business_prior_rescale_factor__ = i.rescale_factor__
  ), on = join_keys]
  raw_train <- raw[business_prior_is_holdout__ == FALSE]
  sm <- normalize_business_spend_map_hier_mmm(metadata_input, spend_map = spend_map)
  if (nrow(sm)) sm[, variable := as.character(variable)]

  metadata_out <- read_input_table(metadata_input)
  if (!"variable" %in% names(metadata_out)) stop("metadata_input must include variable.", call. = FALSE)
  metadata_out[, variable := as.character(variable)]

  rows <- lapply(seq_len(nrow(bp)), function(i) {
    v <- bp$variable[i]
    metric <- business_prior_detect_metric_hier_mmm(bp, i)
    input_mean <- business_prior_row_num_hier_mmm(bp, i, business_prior_metric_candidates_hier_mmm(metric))
    sp <- business_prior_sd_precision_hier_mmm(bp, i, metric)
    input_sd <- sp$sd
    input_precision <- sp$precision
    dist <- if ("prior_distribution" %in% names(bp)) as.character(bp$prior_distribution[i]) else "normal"
    if (!nzchar(dist %||% "") || is.na(dist)) dist <- "normal"
    evidence_source <- if ("evidence_source" %in% names(bp)) as.character(bp$evidence_source[i]) else NA_character_
    evidence_notes <- if ("evidence_notes" %in% names(bp)) as.character(bp$evidence_notes[i]) else NA_character_
    if (!is.finite(input_mean) || is.na(metric)) {
      return(data.table(variable = v, warning = "invalid_business_prior_metric_or_mean"))
    }
    if (identical(metric, "coef")) {
      coef <- input_mean
      coef_sd <- input_sd
      if (!is.finite(coef_sd) || coef_sd <= 0) coef_sd <- max(abs(coef) * default_relative_sd, min_coef_sd)
      coef_sd <- max(coef_sd, min_coef_sd)
      precision_uncapped <- 1 / coef_sd^2
      precision <- min(precision_uncapped, max_precision)
      return(data.table(
        variable = v, coef = coef, coef_sd = coef_sd, coef_precision = precision,
        coef_precision_uncapped = precision_uncapped,
        coef_precision_was_capped = is.finite(max_precision) && precision_uncapped > max_precision,
        prior_metric = metric, input_prior_mean = input_mean, input_prior_sd = coef_sd,
        input_prior_precision = if (is.finite(input_precision)) input_precision else precision_uncapped,
        input_precision_preserved = is.finite(input_precision) && input_precision > 0,
        prior_distribution = dist, stan_prior_distribution = "normal_on_coefficient",
        kpi_value_per_outcome = NA_real_, spend_col = NA_character_,
        spend_total_train = NA_real_, model_design_sum_train = NA_real_,
        economic_prior_basis = "direct_coefficient",
        evidence_source = evidence_source, evidence_notes = evidence_notes,
        warning = NA_character_
      ))
    }
    sc <- sm[variable == v, spend_col][1]
    if (is.na(sc) || !nzchar(sc) || !(sc %in% names(raw_train))) {
      return(data.table(variable = v, warning = "missing_spend_col_for_business_prior_conversion"))
    }
    design_sum <- business_design_sum_hier_mmm(prep, v)
    spend <- pmax(suppressWarnings(as.numeric(raw_train[[sc]])), 0)
    rescale <- suppressWarnings(as.numeric(raw_train$business_prior_rescale_factor__))
    rescale[!is.finite(rescale) | abs(rescale) <= 1e-8] <- 1
    spend_total <- sum(spend, na.rm = TRUE)
    if (!is.finite(spend_total) || spend_total <= 0 || !is.finite(design_sum) || design_sum <= 0) {
      return(data.table(variable = v, warning = "invalid_spend_or_model_design_for_business_prior_conversion"))
    }
    kpi_value <- kpi_value_for_business_prior_hier_mmm(kpi_value_per_outcome, bp, i, v)
    outcome_per_cost <- NA_real_
    outcome_per_cost_sd <- NA_real_
    cost_per_outcome <- NA_real_
    cost_per_outcome_sd <- NA_real_
    if (metric == "ikpc") {
      outcome_per_cost <- input_mean
      outcome_per_cost_sd <- input_sd
    } else if (metric == "cpkpi") {
      cost_per_outcome <- input_mean
      cost_per_outcome_sd <- input_sd
      outcome_per_cost <- 1 / cost_per_outcome
      if (is.finite(cost_per_outcome_sd) && cost_per_outcome_sd > 0) {
        outcome_per_cost_sd <- cost_per_outcome_sd / (cost_per_outcome^2)
      }
    } else if (metric %in% c("roi", "mroi")) {
      outcome_per_cost <- input_mean / kpi_value
      if (is.finite(input_sd) && input_sd > 0) outcome_per_cost_sd <- input_sd / kpi_value
    }
    if (!is.finite(outcome_per_cost) || outcome_per_cost <= 0) {
      return(data.table(variable = v, warning = "invalid_business_prior_economics"))
    }
    if (!is.finite(outcome_per_cost_sd) || outcome_per_cost_sd <= 0) {
      outcome_per_cost_sd <- max(abs(outcome_per_cost) * default_relative_sd, 1e-8)
    }
    exposure_to_model_outcome <- sum(spend / rescale, na.rm = TRUE)
    coef <- (outcome_per_cost * exposure_to_model_outcome) / design_sum
    coef_sd <- max((outcome_per_cost_sd * exposure_to_model_outcome) / design_sum, min_coef_sd)
    precision_uncapped <- 1 / coef_sd^2
    precision <- min(precision_uncapped, max_precision)
    data.table(
      variable = v, coef = coef, coef_sd = coef_sd, coef_precision = precision,
      coef_precision_uncapped = precision_uncapped,
      coef_precision_was_capped = is.finite(max_precision) && precision_uncapped > max_precision,
      prior_metric = metric, input_prior_mean = input_mean, input_prior_sd = input_sd,
      input_prior_precision = input_precision, input_precision_preserved = FALSE,
      prior_distribution = dist, stan_prior_distribution = "normal_delta_method_on_coefficient",
      kpi_value_per_outcome = kpi_value, outcome_per_cost = outcome_per_cost,
      outcome_per_cost_sd = outcome_per_cost_sd,
      cost_per_outcome = if (is.finite(cost_per_outcome)) cost_per_outcome else 1 / outcome_per_cost,
      cost_per_outcome_sd = cost_per_outcome_sd,
      spend_col = sc, spend_total_train = spend_total, model_design_sum_train = design_sum,
      economic_prior_basis = if (metric == "mroi") "marginal_metric_average_scale_fallback" else "average_metric_delta_method",
      evidence_source = evidence_source, evidence_notes = evidence_notes,
      warning = if (metric == "mroi") "mroi_converted_with_average_scale_fallback_pending_true_marginal_conversion" else NA_character_
    )
  })
  audit <- rbindlist(rows, fill = TRUE)
  usable <- audit[is.na(warning) | !nzchar(warning)]
  if (nrow(usable)) {
    metadata_out[usable, on = "variable", `:=`(
      coef = i.coef,
      coef_precision = i.coef_precision,
      business_prior_metric = i.prior_metric,
      business_prior_input_mean = i.input_prior_mean,
      business_prior_input_sd = i.input_prior_sd,
      business_prior_input_precision = i.input_prior_precision,
      business_prior_coef_sd = i.coef_sd,
      business_prior_coef_precision_uncapped = i.coef_precision_uncapped,
      business_prior_precision_was_capped = i.coef_precision_was_capped,
      business_prior_distribution = i.prior_distribution,
      business_prior_basis = i.economic_prior_basis,
      business_prior_evidence_source = i.evidence_source,
      business_prior_evidence_notes = i.evidence_notes
    )]
  }
  list(metadata = metadata_out[], business_prior_audit = audit[])
}

build_roi_mroi_hier_mmm <- function(fit_obj,
                                    spend_map = NULL,
                                    raw_data = NULL,
                                    variables = NULL,
                                    step_pct = 0.01,
                                    spend_suffix = "_spend") {
  sm <- normalize_spend_map_hier_mmm(fit_obj, spend_map = spend_map, raw_data = raw_data,
                                     spend_suffix = spend_suffix, variables = variables)
  if (!nrow(sm)) stop("No spend mapping found. Pass spend_map or add cost_col/spend_col to metadata.")
  raw_aligned <- get_aligned_raw_data_hier_mmm(fit_obj, raw_data = raw_data)
  long <- copy(as.data.table(fit_obj$long_decomp))
  if (!is.null(variables)) sm <- sm[variable %in% variables]

  rows <- lapply(seq_len(nrow(sm)), function(i) {
    v <- sm$variable[i]
    sc <- sm$spend_col[i]
    spend <- sum(as.numeric(raw_aligned[[sc]]), na.rm = TRUE)
    contribution <- long[variable == v, sum(contribution, na.rm = TRUE)]
    base_contribution <- variable_contribution_sum_hier_mmm(fit_obj, v, multiplier = 1)
    up_contribution <- variable_contribution_sum_hier_mmm(fit_obj, v, multiplier = 1 + step_pct)
    inc_contribution <- up_contribution - base_contribution
    inc_spend <- spend * step_pct
    data.table(
      variable = v,
      spend_col = sc,
      spend = spend,
      contribution = contribution,
      roi = fifelse(abs(spend) > 1e-8, contribution / spend, NA_real_),
      mroi_step_pct = step_pct,
      incremental_spend = inc_spend,
      incremental_contribution = inc_contribution,
      mroi = fifelse(abs(inc_spend) > 1e-8, inc_contribution / inc_spend, NA_real_),
      decisioning_basis = "posterior_mean_point_estimate",
      uncertainty_note = "ROI/mROI are computed from posterior means only; use as decision support, not posterior uncertainty intervals."
    )
  })
  rbindlist(rows, fill = TRUE)[order(-mroi)]
}

calibrate_lift_tests_hier_mmm <- function(fit_obj,
                                          calibration_input,
                                          id_col = "calibration_id",
                                          variable_col = "variable",
                                          observed_lift_col = "observed_lift",
                                          observed_sd_col = "observed_lift_sd",
                                          observed_precision_col = "observed_lift_precision",
                                          start_col = "start_period",
                                          end_col = "end_period") {
  cal <- as_hier_mmm_data_table(calibration_input, label = "calibration_input")
  if (is.null(cal) || !nrow(cal)) return(data.table())
  if (!(variable_col %in% names(cal))) stop("calibration_input must include ", variable_col)
  if (!(observed_lift_col %in% names(cal))) stop("calibration_input must include ", observed_lift_col)
  if (!(id_col %in% names(cal))) cal[, (id_col) := paste0("calib_", .I)]

  long <- copy(as.data.table(fit_obj$long_decomp))
  group_col <- fit_obj$group_col
  time_col <- fit_obj$time_col

  rows <- lapply(seq_len(nrow(cal)), function(i) {
    row <- cal[i]
    v <- as.character(row[[variable_col]][1])
    obs <- suppressWarnings(as.numeric(row[[observed_lift_col]][1]))
    sd <- NA_real_
    if (observed_sd_col %in% names(row)) sd <- suppressWarnings(as.numeric(row[[observed_sd_col]][1]))
    if ((!is.finite(sd) || is.na(sd) || sd <= 0) && observed_precision_col %in% names(row)) {
      pr <- suppressWarnings(as.numeric(row[[observed_precision_col]][1]))
      if (is.finite(pr) && pr > 0) sd <- 1 / sqrt(pr)
    }
    tmp <- long[variable == v]
    if (group_col %in% names(cal) && group_col %in% names(tmp) && !is.na(row[[group_col]][1]) && nzchar(as.character(row[[group_col]][1]))) {
      tmp <- tmp[get(group_col) == row[[group_col]][1]]
    }
    if (start_col %in% names(cal) && time_col %in% names(tmp) && !is.na(row[[start_col]][1]) && nzchar(as.character(row[[start_col]][1]))) {
      tmp <- tmp[get(time_col) >= row[[start_col]][1]]
    }
    if (end_col %in% names(cal) && time_col %in% names(tmp) && !is.na(row[[end_col]][1]) && nzchar(as.character(row[[end_col]][1]))) {
      tmp <- tmp[get(time_col) <= row[[end_col]][1]]
    }
    model_lift <- tmp[, sum(contribution, na.rm = TRUE)]
    diff <- obs - model_lift
    ratio <- if (is.finite(model_lift) && abs(model_lift) > 1e-8) obs / model_lift else NA_real_
    z <- if (is.finite(sd) && sd > 0) diff / sd else NA_real_
    data.table(
      calibration_id = as.character(row[[id_col]][1]),
      variable = v,
      observed_lift = obs,
      model_lift = model_lift,
      lift_difference = diff,
      observed_to_model_ratio = ratio,
      observed_lift_sd = sd,
      calibration_z = z,
      recommended_coef_multiplier = ifelse(is.finite(ratio), pmin(pmax(ratio, 0.25), 4), NA_real_),
      calibration_flag = fifelse(is.finite(z) & abs(z) > 2, "outside_2sd", fifelse(is.finite(ratio) & (ratio < 0.70 | ratio > 1.30), "ratio_check", "ok"))
    )
  })
  rbindlist(rows, fill = TRUE)
}

apply_calibration_to_metadata <- function(metadata_input,
                                          calibration_diagnostics,
                                          output_path = NULL,
                                          multiplier_col = "recommended_coef_multiplier") {
  md <- read_input_table(metadata_input)
  cd <- as.data.table(copy(calibration_diagnostics))
  if (!nrow(cd)) return(md)
  adj <- cd[is.finite(get(multiplier_col)), .(calibration_multiplier = median(get(multiplier_col), na.rm = TRUE)), by = variable]
  if (!nrow(adj)) return(md)
  md[adj, coef := as.numeric(coef) * i.calibration_multiplier, on = "variable"]
  md[adj, calibration_multiplier := i.calibration_multiplier, on = "variable"]
  if (!is.null(output_path) && nzchar(output_path)) fwrite(md, output_path)
  md[]
}

optimize_budget_hier_mmm <- function(fit_obj,
                                     spend_map = NULL,
                                     raw_data = NULL,
                                     total_budget = NULL,
                                     variables = NULL,
                                     budget_step_frac = 0.01,
                                     min_multiplier = 0,
                                     max_multiplier = 3,
                                     spend_suffix = "_spend",
                                     max_iter = 1000) {
  sm <- normalize_spend_map_hier_mmm(fit_obj, spend_map = spend_map, raw_data = raw_data,
                                     spend_suffix = spend_suffix, variables = variables)
  if (!nrow(sm)) stop("No spend mapping found for optimizer.")
  raw_aligned <- get_aligned_raw_data_hier_mmm(fit_obj, raw_data = raw_data)
  sm[, current_spend := vapply(spend_col, function(cc) sum(as.numeric(raw_aligned[[cc]]), na.rm = TRUE), numeric(1))]
  sm <- sm[current_spend > 0]
  if (!nrow(sm)) stop("All mapped spend columns sum to zero.")
  if (is.null(total_budget)) total_budget <- sum(sm$current_spend)
  if (!is.finite(total_budget) || total_budget <= 0) stop("total_budget must be positive.")
  if (length(min_multiplier) == 1L) min_multiplier <- rep(min_multiplier, nrow(sm))
  if (length(max_multiplier) == 1L) max_multiplier <- rep(max_multiplier, nrow(sm))
  sm[, `:=`(min_multiplier = min_multiplier, max_multiplier = max_multiplier)]

  multipliers <- sm$min_multiplier
  names(multipliers) <- sm$variable
  spend_now <- sm$current_spend * multipliers
  remaining <- total_budget - sum(spend_now)
  chunk <- max(total_budget * budget_step_frac, total_budget / max_iter)
  iter <- 0L

  while (remaining > 1e-8 && iter < max_iter) {
    iter <- iter + 1L
    candidates <- lapply(seq_len(nrow(sm)), function(i) {
      v <- sm$variable[i]
      cur_mult <- multipliers[v]
      if (cur_mult >= sm$max_multiplier[i] - 1e-8) return(NULL)
      alloc <- min(chunk, remaining, sm$current_spend[i] * (sm$max_multiplier[i] - cur_mult))
      if (alloc <= 1e-8) return(NULL)
      delta_mult <- alloc / sm$current_spend[i]
      base <- variable_contribution_sum_hier_mmm(fit_obj, v, multiplier = cur_mult)
      up <- variable_contribution_sum_hier_mmm(fit_obj, v, multiplier = cur_mult + delta_mult)
      data.table(variable = v, add_spend = alloc, delta_mult = delta_mult,
                 incremental_contribution = up - base,
                 marginal_roi = (up - base) / alloc)
    })
    cand <- rbindlist(candidates, fill = TRUE)
    if (!nrow(cand)) break
    cand <- cand[order(-marginal_roi)]
    best <- cand[1]
    multipliers[best$variable] <- multipliers[best$variable] + best$delta_mult
    remaining <- remaining - best$add_spend
  }

  plan <- copy(sm)
  plan[, recommended_multiplier := as.numeric(multipliers[variable])]
  plan[, recommended_spend := current_spend * recommended_multiplier]
  plan[, expected_contribution := vapply(variable, function(v) variable_contribution_sum_hier_mmm(fit_obj, v, multiplier = multipliers[v]), numeric(1))]
  plan[, expected_roi := fifelse(abs(recommended_spend) > 1e-8, expected_contribution / recommended_spend, NA_real_)]
  plan[, `:=`(
    total_budget = total_budget,
    optimizer_iterations = iter,
    unallocated_budget = max(remaining, 0),
    optimizer_basis = "posterior_mean_point_estimate_prototype",
    uncertainty_note = "Budget optimization uses posterior mean response only; it does not yet optimize over posterior uncertainty."
  )]
  plan[order(-recommended_spend)]
}

add_reach_frequency_signal <- function(data,
                                       reach_col,
                                       frequency_col,
                                       output_col,
                                       method = c("effective_reach", "reach_x_frequency"),
                                       frequency_cap = 10) {
  method <- match.arg(method)
  dt <- copy(as.data.table(data))
  if (!all(c(reach_col, frequency_col) %in% names(dt))) stop("reach_col and frequency_col must exist in data.")
  r <- pmax(as.numeric(dt[[reach_col]]), 0)
  f <- pmax(pmin(as.numeric(dt[[frequency_col]]), frequency_cap), 0)
  if (method == "effective_reach") {
    dt[, (output_col) := r * (1 - exp(-f))]
  } else {
    dt[, (output_col) := r * f]
  }
  dt[]
}

write_decision_outputs_hier_mmm <- function(decision_outputs, output_dir, prefix = "") {
  if (is.null(output_dir) || !nzchar(output_dir)) return(invisible(NULL))
  dir.create(output_dir, recursive = TRUE, showWarnings = FALSE)
  pfx <- if (nzchar(prefix)) paste0(prefix, "_") else ""
  for (nm in names(decision_outputs)) {
    obj <- decision_outputs[[nm]]
    if (inherits(obj, "data.table") || is.data.frame(obj)) {
      fwrite(as.data.table(obj), file.path(output_dir, paste0(pfx, nm, ".csv")))
    }
  }
  invisible(output_dir)
}

run_hier_mmm_decisioning <- function(fit_obj,
                                     spend_map = NULL,
                                     raw_data = NULL,
                                     calibration_input = NULL,
                                     budget_optimizer_config = NULL,
                                     output_dir = NULL,
                                     output_prefix = "") {
  out <- list()
  if (!is.null(spend_map)) {
    out$roi_mroi <- build_roi_mroi_hier_mmm(fit_obj, spend_map = spend_map, raw_data = raw_data)
  }
  if (!is.null(calibration_input)) {
    out$calibration <- calibrate_lift_tests_hier_mmm(fit_obj, calibration_input = calibration_input)
  }
  if (!is.null(budget_optimizer_config)) {
    cfg <- budget_optimizer_config
    if (is.null(cfg$spend_map)) cfg$spend_map <- spend_map
    if (is.null(cfg$raw_data)) cfg$raw_data <- raw_data
    out$budget_optimizer <- do.call(optimize_budget_hier_mmm, c(list(fit_obj = fit_obj), cfg))
  }
  out$decisioning_notes <- data.table(
    component = c("roi_mroi", "budget_optimizer"),
    basis = c("posterior_mean_point_estimate", "posterior_mean_point_estimate_prototype"),
    note = c(
      "ROI/mROI are computed from posterior means only; posterior draw intervals are not included in this table.",
      "Budget optimization uses posterior mean response only and should be treated as prototype decision support."
    )
  )
  write_decision_outputs_hier_mmm(out, output_dir = output_dir, prefix = output_prefix)
  out
}

run_hier_mmm_sensitivity <- function(data,
                                     metadata_input,
                                     dep_var_col,
                                     group_col,
                                     time_col,
                                     entity_col,
                                     variants = NULL,
                                     output_dir = file.path("hier_mmm_outputs", "sensitivity"),
                                     chains = 2,
                                     parallel_chains = 2,
                                     iter_warmup = 500,
                                     iter_sampling = 500,
                                     continue_on_error = TRUE,
                                     ...) {
  if (is.null(variants)) {
    variants <- data.table(
      variant = c("base_fourier_student_t_fixed_curve", "fourier_centered_x", "flat_student_t_fixed_curve", "ucm_student_t_fixed_curve", "ucm_normal_fixed_curve", "ucm_student_t_est_curve"),
