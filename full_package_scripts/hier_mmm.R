# Infer the deployment directory when this file is sourced.
# This lets fit_hier_mmm() find hier_mmm.stan without requiring the user to pass stan_file.
get_hier_mmm_script_dir <- function() {
  frames <- sys.frames()
  ofiles <- vapply(frames, function(f) {
    if (!is.null(f$ofile)) as.character(f$ofile)[1] else NA_character_
  }, character(1))
  ofiles <- ofiles[!is.na(ofiles) & nzchar(ofiles)]
  if (length(ofiles)) return(dirname(normalizePath(ofiles[length(ofiles)], mustWork = FALSE)))
  getwd()
}

.hier_mmm_deploy_dir <- get_hier_mmm_script_dir()

resolve_hier_mmm_stan_file <- function(stan_file = NULL) {
  if (!is.null(stan_file) && nzchar(stan_file)) {
    stan_file <- path.expand(stan_file)
    if (!file.exists(stan_file)) stop("Stan file does not exist: ", stan_file)
    return(normalizePath(stan_file, mustWork = TRUE))
  }

  candidates <- unique(c(
    file.path(.hier_mmm_deploy_dir, "hier_mmm.stan"),
    file.path(.hier_mmm_deploy_dir, "hier_mmm_v17_fourier_default.stan"),
    file.path(getwd(), "hier_mmm.stan"),
    file.path(getwd(), "hier_mmm_v17_fourier_default.stan")
  ))
  hit <- candidates[file.exists(candidates)]
  if (length(hit)) return(normalizePath(hit[1], mustWork = TRUE))

  stop(
    "Could not find a matching Stan file. Put hier_mmm.R and hier_mmm.stan in the same folder, ",
    "or pass stan_file explicitly. Checked: ", paste(candidates, collapse = "; ")
  )
}


read_input_table <- function(x) {
  if (is.null(x)) stop("Input table is NULL.")
  if (inherits(x, "data.table")) return(copy(x))
  if (is.data.frame(x)) return(as.data.table(copy(x)))

  if (is.character(x) && length(x) == 1L) {
    path <- path.expand(x)
    if (!file.exists(path)) stop("Input file does not exist: ", path)
    ext <- tolower(tools::file_ext(path))
    if (ext %in% c("csv", "txt", "tsv")) return(fread(path))
    if (ext == "rds") return(as.data.table(readRDS(path)))
    if (ext %in% c("xlsx", "xls")) {
      if (!requireNamespace("readxl", quietly = TRUE)) {
        stop("Reading Excel inputs requires the readxl package. Use CSV or install readxl.")
      }
      return(as.data.table(readxl::read_excel(path)))
    }
    stop("Unsupported input file type: .", ext)
  }

  stop("Input must be a file path, data.table, or data.frame.")
}

finite_or_stop <- function(x, label) {
  bad <- is.na(x) | !is.finite(x)
  if (any(bad)) stop(label, " contains NA/NaN/Inf values. Bad rows: ", paste(which(bad)[seq_len(min(sum(bad), 10L))], collapse = ", "))
  invisible(TRUE)
}

log_hier_mmm_checkpoint <- function(stage,
                                    status = "OK",
                                    detail = "",
                                    checkpoint_file = NULL,
                                    verbose = TRUE) {
  status <- toupper(trimws(as.character(status %||% "OK")))
  stage <- trimws(as.character(stage %||% ""))
  detail <- trimws(as.character(detail %||% ""))
  ts <- format(Sys.time(), "%Y-%m-%d %H:%M:%S")
  msg <- if (nzchar(detail)) {
    sprintf("[%s] %-7s | %s | %s", ts, status, stage, detail)
  } else {
    sprintf("[%s] %-7s | %s", ts, status, stage)
  }
  if (isTRUE(verbose)) message(msg)
  if (!is.null(checkpoint_file) && nzchar(checkpoint_file)) {
    dir.create(dirname(checkpoint_file), recursive = TRUE, showWarnings = FALSE)
    cat(paste0(msg, "\n"), file = checkpoint_file, append = TRUE)
  }
  invisible(msg)
}

validate_numeric_input_cols <- function(dt,
                                        cols,
                                        label = "model input",
                                        stop_on_zero_variance = TRUE) {
  cols <- unique(cols[cols %in% names(dt)])
  if (!length(cols)) return(invisible(TRUE))

  for (cc in cols) {
    suppressWarnings(dt[, (cc) := as.numeric(get(cc))])
    finite_or_stop(dt[[cc]], paste0(label, " column '", cc, "'"))
    s <- safe_sd(dt[[cc]])
    if (isTRUE(stop_on_zero_variance) && s <= 1e-8) {
      stop(label, " column '", cc, "' has zero/nearly-zero variance across all rows.")
    }
  }
  invisible(TRUE)
}

smooth_vector <- function(x, window = 7L) {
  x <- as.numeric(x)
  n <- length(x)
  window <- as.integer(window %||% 7L)
  if (n <= 2L || window <= 1L) return(x)
  window <- min(window, n)
  if (window %% 2L == 0L) window <- max(1L, window - 1L)
  if (window <= 1L) return(x)

  y <- as.numeric(stats::filter(x, rep(1 / window, window), sides = 2))
  good <- which(is.finite(y))
  if (!length(good)) return(rep(mean(x, na.rm = TRUE), n))
  if (length(good) < n) {
    y <- stats::approx(x = good, y = y[good], xout = seq_len(n), rule = 2)$y
  }
  y
}

clip_numeric <- function(x, lower, upper) pmin(pmax(x, lower), upper)


safe_sd <- function(x, floor = 1e-8) {
  s <- stats::sd(x, na.rm = TRUE)
  if (!is.finite(s) || is.na(s) || s < floor) floor else s
}

