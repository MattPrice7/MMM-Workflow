      estimate_dvalue = c(FALSE, FALSE, FALSE, FALSE, FALSE, TRUE),
      center_predictors_for_sampling = c(FALSE, TRUE, FALSE, FALSE, FALSE, FALSE),
      sample_curve_parameters = c("never", "never", "never", "never", "never", "always")
    )
  } else {
    variants <- as.data.table(variants)
  }
  if (!"variant" %in% names(variants)) variants[, variant := paste0("variant_", .I)]
  variant_arg_list <- function(vrow) {
    keep <- setdiff(names(vrow), "variant")
    out <- as.list(vrow[, ..keep])
    out <- lapply(out, function(x) if (length(x) == 1L) x[[1]] else x)
    out[!vapply(out, function(x) length(x) == 0L || (length(x) == 1L && is.na(x)), logical(1))]
  }
  fits <- list()
  summary_rows <- list()
  dots <- list(...)
  for (i in seq_len(nrow(variants))) {
    vname <- variants$variant[i]
    base_args <- modifyList(list(
      data = data, metadata_input = metadata_input, dep_var_col = dep_var_col,
      group_col = group_col, time_col = time_col, entity_col = entity_col,
