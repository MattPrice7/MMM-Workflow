normalize_spend_map_hier_mmm <- function(fit_obj,
                                         spend_map = NULL,
                                         raw_data = NULL,
                                         spend_suffix = "_spend",
                                         variables = NULL) {
  vl <- copy(as.data.table(fit_obj$variable_lookup))
  md <- copy(as.data.table(fit_obj$metadata))
  data_cols <- names(fit_obj$data)
  raw_dt <- as_hier_mmm_data_table(raw_data)
  raw_cols <- if (is.null(raw_dt)) character() else names(raw_dt)

  media_roles <- c("media", "reach_frequency")
  candidate_vars <- vl[role %in% media_roles, variable]
  if (!length(candidate_vars)) candidate_vars <- vl$variable
  if (!is.null(variables)) candidate_vars <- intersect(candidate_vars, variables)

  if (!is.null(spend_map)) {
    if (is.character(spend_map) && !is.null(names(spend_map))) {
      sm <- data.table(variable = names(spend_map), spend_col = as.character(spend_map))
    } else {
      sm <- as.data.table(copy(spend_map))
      if (!("variable" %in% names(sm))) stop("spend_map must include a variable column.")
      if (!("spend_col" %in% names(sm))) {
        alt <- intersect(c("cost_col", "spend", "spend_column", "cost_column"), names(sm))
        if (!length(alt)) stop("spend_map must include spend_col or cost_col.")
        setnames(sm, alt[1], "spend_col")
      }
      sm <- sm[, .(variable = as.character(variable), spend_col = as.character(spend_col))]
    }
  } else if ("cost_col" %in% names(md)) {
    sm <- md[variable %in% candidate_vars & !is.na(cost_col) & nzchar(trimws(as.character(cost_col))),
             .(variable, spend_col = as.character(cost_col))]
  } else if ("spend_col" %in% names(md)) {
    sm <- md[variable %in% candidate_vars & !is.na(spend_col) & nzchar(trimws(as.character(spend_col))),
             .(variable, spend_col = as.character(spend_col))]
  } else {
    inferred <- paste0(candidate_vars, spend_suffix)
    exists <- inferred %in% c(data_cols, raw_cols)
    sm <- data.table(variable = candidate_vars[exists], spend_col = inferred[exists])
  }

  if (!nrow(sm)) return(data.table(variable = character(), spend_col = character()))
  sm <- unique(sm[variable %in% candidate_vars], by = "variable")
  missing_cols <- sm[!(spend_col %in% c(data_cols, raw_cols)), spend_col]
  if (length(missing_cols)) {
    stop("Spend columns not found in fit_obj$data or raw_data: ", paste(unique(missing_cols), collapse = ", "))
  }
  sm[]
}
attach_spend_to_hier_mmm_outputs <- function(fit_obj,
                                             spend_map = NULL,
                                             raw_data = NULL,
                                             spend_suffix = "_spend") {
  sm <- normalize_spend_map_hier_mmm(fit_obj, spend_map = spend_map, raw_data = raw_data, spend_suffix = spend_suffix)
  diag <- data.table(
    variable = sm$variable %||% character(),
    spend_col = sm$spend_col %||% character(),
    matched_long_rows = integer(),
    matched_wide_rows = integer()
  )
  fit_obj$spend_attachment_diagnostics <- diag
  if (!nrow(sm)) return(fit_obj)

  raw_dt <- as_hier_mmm_data_table(raw_data)
  if (is.null(raw_dt)) raw_dt <- copy(as.data.table(fit_obj$data))
  long <- copy(as.data.table(fit_obj$long_decomp))
  wide <- copy(as.data.table(fit_obj$wide_decomp))

  key_candidates <- unique(c(fit_obj$time_col, fit_obj$group_col, fit_obj$entity_col))
  join_keys <- key_candidates[key_candidates %in% names(raw_dt) & key_candidates %in% names(long)]
  if (!length(join_keys)) {
    warning("Could not attach spend to Stan outputs: no shared time/group/entity keys between raw_data and long_decomp.")
    fit_obj$spend_attachment_diagnostics[, `:=`(matched_long_rows = 0L, matched_wide_rows = 0L)]
    return(fit_obj)
  }

  support_map <- unique(as.data.table(fit_obj$variable_lookup)[variable %in% sm$variable, .(variable, support_col = variable)], by = "variable")
  support_map <- support_map[support_col %in% names(raw_dt)]

  spend_long <- rbindlist(lapply(seq_len(nrow(sm)), function(i) {
    v <- sm$variable[i]
    sc <- sm$spend_col[i]
    out <- raw_dt[, c(join_keys, sc), with = FALSE]
    setnames(out, sc, "spend")
    out[, variable := v]
    out[, spend := suppressWarnings(as.numeric(spend))]
    out[]
  }), use.names = TRUE, fill = TRUE)
  spend_long <- spend_long[, .(spend = sum(spend, na.rm = TRUE)), by = c(join_keys, "variable")]

  support_long <- data.table()
  if (nrow(support_map)) {
    support_long <- rbindlist(lapply(seq_len(nrow(support_map)), function(i) {
      v <- support_map$variable[i]
      sc <- support_map$support_col[i]
      out <- raw_dt[, c(join_keys, sc), with = FALSE]
      setnames(out, sc, "support")
      out[, variable := v]
      out[, support := suppressWarnings(as.numeric(support))]
      out[]
    }), use.names = TRUE, fill = TRUE)
    support_long <- support_long[, .(support = sum(support, na.rm = TRUE)), by = c(join_keys, "variable")]
  }

  long[spend_long, spend := i.spend, on = c(setNames(join_keys, join_keys), "variable")]
  if (nrow(support_long)) long[support_long, support := i.support, on = c(setNames(join_keys, join_keys), "variable")]
  long[variable %in% sm$variable & !is.finite(spend), spend := NA_real_]
  if ("support" %in% names(long)) long[variable %in% support_map$variable & !is.finite(support), support := NA_real_]

  wide_spend <- dcast(spend_long, paste(paste(join_keys, collapse = " + "), "~ variable"), value.var = "spend", fill = 0)
  spend_vars <- setdiff(names(wide_spend), join_keys)
  if (length(spend_vars)) {
    setnames(wide_spend, spend_vars, paste0("spend_", spend_vars))
    wide_spend[, total_spend := rowSums(.SD, na.rm = TRUE), .SDcols = paste0("spend_", spend_vars)]
  } else {
    wide_spend[, total_spend := 0]
  }
  wide[wide_spend, on = join_keys, `:=`(
    total_spend = i.total_spend
  )]
  for (cc in setdiff(names(wide_spend), c(join_keys, "total_spend"))) {
    wide[wide_spend, on = join_keys, (cc) := get(paste0("i.", cc))]
  }
  if (nrow(support_long)) {
    wide_support <- dcast(support_long, paste(paste(join_keys, collapse = " + "), "~ variable"), value.var = "support", fill = 0)
    support_vars <- setdiff(names(wide_support), join_keys)
    if (length(support_vars)) {
      setnames(wide_support, support_vars, paste0("support_", support_vars))
      wide_support[, total_support := rowSums(.SD, na.rm = TRUE), .SDcols = paste0("support_", support_vars)]
      wide[wide_support, on = join_keys, total_support := i.total_support]
      for (cc in setdiff(names(wide_support), c(join_keys, "total_support"))) {
        wide[wide_support, on = join_keys, (cc) := get(paste0("i.", cc))]
      }
    }
  }

  diag <- sm[, .(
    spend_col,
    support_col = support_map[variable == .BY$variable, support_col][1] %||% NA_character_,
    matched_long_rows = long[variable == .BY$variable & is.finite(spend), .N],
    matched_support_rows = if ("support" %in% names(long)) long[variable == .BY$variable & is.finite(support), .N] else 0L,
    matched_wide_rows = wide[is.finite(total_spend) & total_spend > 0, .N]
  ), by = variable]
  fit_obj$long_decomp <- long[]
  fit_obj$wide_decomp <- wide[]
  fit_obj$spend_attachment_diagnostics <- diag[]
  fit_obj
}

get_aligned_raw_data_hier_mmm <- function(fit_obj, raw_data = NULL) {
  if (is.null(raw_data)) return(copy(as.data.table(fit_obj$data)))
  raw_dt <- as_hier_mmm_data_table(raw_data)
  group_col <- fit_obj$group_col %||% stop("fit_obj is missing group_col.")
  time_col <- fit_obj$time_col %||% stop("fit_obj is missing time_col.")
  if (!all(c(group_col, time_col) %in% names(raw_dt))) {
    stop("raw_data must contain group_col and time_col: ", group_col, ", ", time_col)
  }
  key_dt <- copy(as.data.table(fit_obj$data))[, .(row_id__ = .I, group__ = get(group_col), time__ = get(time_col))]
  raw_key <- copy(raw_dt)
  raw_key[, group__ := get(group_col)]
  raw_key[, time__ := get(time_col)]
  out <- raw_key[key_dt, on = .(group__, time__)]
  setorder(out, row_id__)
  out[]
}

