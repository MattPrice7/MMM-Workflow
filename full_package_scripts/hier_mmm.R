# Public wrapper. Keeps the normal call simple while preserving every advanced
# engine option through ... .
fit_hier_mmm <- function(data,
                         metadata_input,
                         dep_var_col,
                         group_col,
                         time_col,
                         entity_col,
                         baseline_config = NULL,
                         holiday_config = NULL,
                         spend_map = NULL,
                         business_priors = NULL,
                         kpi_value_per_outcome = 1,
                         business_prior_default_relative_sd = 0.50,
                         business_prior_min_coef_sd = 1e-6,
                         business_prior_max_precision = Inf,
                         calibration_input = NULL,
                         budget_optimizer_config = NULL,
                         raw_output_data = NULL,
                         create_response_curves = TRUE,
                         create_response_curve_draws = FALSE,
                         response_curve_multipliers = seq(0, 3, by = 0.05),
                         response_curve_step_pct = 0.01,
                         response_curve_scope = c("auto", "total", "group", "both"),
                         response_curve_draw_count = 200L,
                         response_curve_draw_seed = 123L,
                         output_dir = "hier_mmm_outputs",
                         output_prefix = "",
                         ...) {
  dots <- list(...)
  response_curve_scope <- match.arg(response_curve_scope)
  business_prior_update <- NULL

  if (!is.null(business_priors)) {
    business_prior_update <- apply_business_priors_to_metadata_hier_mmm(
      data = data,
      metadata_input = metadata_input,
      business_priors = business_priors,
      dep_var_col = dep_var_col,
      group_col = group_col,
      time_col = time_col,
      entity_col = entity_col,
      spend_map = spend_map,
      kpi_value_per_outcome = kpi_value_per_outcome,
      default_relative_sd = business_prior_default_relative_sd,
      min_coef_sd = business_prior_min_coef_sd,
      max_precision = business_prior_max_precision,
      prep_args = dots
    )
    metadata_input <- business_prior_update$metadata
  }

  engine_args <- modifyList(dots, list(
    data = data,
    metadata_input = metadata_input,
    dep_var_col = dep_var_col,
    group_col = group_col,
    time_col = time_col,
    entity_col = entity_col,
    holiday_config = holiday_config,
    output_dir = output_dir,
    output_prefix = output_prefix
  ))

  if (!is.null(baseline_config)) {
    bc <- baseline_config
    if (is.null(holiday_config) && !is.null(bc$holiday_config)) {
      engine_args$holiday_config <- bc$holiday_config
    }
    if (!is.null(bc$type)) engine_args$intercept_type <- as.character(bc$type)
    ucm_names <- intersect(names(bc), c("level", "season", "cycle", "season_period", "season_harmonics", "cycle_period", "cycle_harmonics"))
    if (length(ucm_names)) {
      base_type <- tolower(trimws(as.character((bc$type %||% engine_args$intercept_type %||% "fourier")[1])))
      base_spec <- engine_args$ucm_spec %||% if (identical(base_type, "ucm")) {
        list(
          level = TRUE, season = TRUE, cycle = FALSE,
          season_period = 52L, season_harmonics = 2L,
          cycle_period = 104L, cycle_harmonics = 1L
        )
      } else {
        list(
          level = FALSE, season = TRUE, cycle = FALSE,
          season_period = 52L, season_harmonics = 2L,
          cycle_period = 104L, cycle_harmonics = 1L
        )
      }
      engine_args$ucm_spec <- modifyList(base_spec, bc[ucm_names])
    }
    if (!is.null(bc$level_innovation_sd)) engine_args$sigma_level_prior_sd <- bc$level_innovation_sd
    if (!is.null(bc$initial_level_mean))  engine_args$level0_mu_prior_mean <- bc$initial_level_mean
    if (!is.null(bc$initial_level_sd))    engine_args$level0_mu_prior_sd <- bc$initial_level_sd
    if (!is.null(bc$group_level_sd))      engine_args$level0_sd_prior_sd <- bc$group_level_sd
    if (!is.null(bc$season_sd))           engine_args$season_sd_prior_sd <- bc$season_sd
    if (!is.null(bc$cycle_sd))            engine_args$cycle_sd_prior_sd <- bc$cycle_sd
    if (!is.null(bc$noise_sd))            engine_args$sigma_y_prior_sd <- bc$noise_sd
  }

  fit_obj <- do.call(fit_hier_mmm_engine, engine_args)
  fit_obj$business_prior_audit <- if (!is.null(business_prior_update)) business_prior_update$business_prior_audit else data.table()

  if (!is.null(spend_map)) {
    fit_obj <- attach_spend_to_hier_mmm_outputs(
      fit_obj,
      spend_map = spend_map,
      raw_data = raw_output_data %||% data
    )
    if (!is.null(output_dir) && nzchar(output_dir)) {
      pfx <- if (nzchar(output_prefix %||% "")) paste0(output_prefix, "_") else ""
      dir.create(output_dir, showWarnings = FALSE, recursive = TRUE)
      fwrite(fit_obj$wide_decomp, file.path(output_dir, paste0(pfx, "wide_decomp.csv")))
      fwrite(fit_obj$long_decomp, file.path(output_dir, paste0(pfx, "long_decomp.csv")))
      if (nrow(fit_obj$spend_attachment_diagnostics)) {
        fwrite(fit_obj$spend_attachment_diagnostics, file.path(output_dir, paste0(pfx, "spend_attachment_diagnostics.csv")))
      }
    }
  }

  if (isTRUE(create_response_curves)) {
    fit_obj$posterior_means <- fit_obj$posterior_means %||% extract_pm_from_fit_obj_hier_mmm(fit_obj)
    fit_obj$response_curves <- build_response_curves_hier_mmm(
      fit_obj = fit_obj,
      spend_map = spend_map,
      raw_data = raw_output_data %||% data,
      multiplier_grid = response_curve_multipliers,
      step_pct = response_curve_step_pct,
      response_curve_scope = response_curve_scope
    )
    if (!is.null(output_dir) && nzchar(output_dir) && nrow(fit_obj$response_curves)) {
      pfx <- if (nzchar(output_prefix %||% "")) paste0(output_prefix, "_") else ""
      dir.create(output_dir, showWarnings = FALSE, recursive = TRUE)
      fwrite(fit_obj$response_curves, file.path(output_dir, paste0(pfx, "response_curves.csv")))
    }
  } else {
    fit_obj$response_curves <- data.table()
  }

  if (isTRUE(create_response_curve_draws)) {
    fit_obj$response_curves_draws <- build_response_curves_draws_hier_mmm(
      fit_obj = fit_obj,
      spend_map = spend_map,
      raw_data = raw_output_data %||% data,
      multiplier_grid = response_curve_multipliers,
      step_pct = response_curve_step_pct,
      response_curve_scope = response_curve_scope,
      max_draws = response_curve_draw_count,
      seed = response_curve_draw_seed
    )
    if (!is.null(output_dir) && nzchar(output_dir) && nrow(fit_obj$response_curves_draws)) {
      pfx <- if (nzchar(output_prefix %||% "")) paste0(output_prefix, "_") else ""
      dir.create(output_dir, showWarnings = FALSE, recursive = TRUE)
      fwrite(fit_obj$response_curves_draws, file.path(output_dir, paste0(pfx, "response_curves_draws.csv")))
    }
  } else {
    fit_obj$response_curves_draws <- fit_obj$response_curves_draws %||% data.table()
  }

  if (!is.null(spend_map) || !is.null(calibration_input) || !is.null(budget_optimizer_config)) {
    fit_obj$posterior_means <- extract_pm_from_fit_obj_hier_mmm(fit_obj)
    fit_obj$decision_outputs <- run_hier_mmm_decisioning(
      fit_obj = fit_obj,
      spend_map = spend_map,
      raw_data = raw_output_data %||% data,
      calibration_input = calibration_input,
      budget_optimizer_config = budget_optimizer_config,
      output_dir = output_dir,
      output_prefix = output_prefix
    )
  } else {
    fit_obj$decision_outputs <- list()
  }

  fit_obj
}
