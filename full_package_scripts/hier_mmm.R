  if (stan_data$alpha_parameterization != 3L && stan_data$G_alpha != stan_data$G) stop("Internal error: centered/non-centered alpha mode must set G_alpha to G.")
  if (stan_data$J_curve + stan_data$J_linear != stan_data$J) stop("Internal error: J_curve + J_linear must equal J.")
  if (anyNA(stan_data$group_id) || any(stan_data$group_id < 1L | stan_data$group_id > stan_data$G)) stop("Internal error: invalid group_id mapping.")
  if (anyNA(stan_data$train_idx) || any(stan_data$train_idx < 1L | stan_data$train_idx > stan_data$N)) stop("Internal error: invalid train_idx mapping.")

  list(
    stan_data = stan_data,
    data = dt,
    metadata = md,
    curve_priors = variable_lookup[, .(
