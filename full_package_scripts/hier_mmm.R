      parallel_chains = parallel_chains, iter_warmup = iter_warmup,
      iter_sampling = iter_sampling, output_dir = file.path(output_dir, vname),
      output_prefix = vname
    ), dots)
    args <- modifyList(base_args, variant_arg_list(variants[i]))
    fit_result <- tryCatch(
      list(ok = TRUE, fit = do.call(fit_hier_mmm, args), error = NA_character_),
      error = function(e) list(ok = FALSE, fit = NULL, error = conditionMessage(e))
    )
    if (!isTRUE(fit_result$ok)) {
      if (!isTRUE(continue_on_error)) stop(fit_result$error, call. = FALSE)
      fits[[vname]] <- fit_result
      summary_rows[[i]] <- data.table(
        variant = vname,
        status = "error",
        error = fit_result$error,
        intercept_type = args$intercept_type %||% NA_character_,
        likelihood = args$likelihood %||% NA_character_,
        estimate_dvalue = isTRUE(args$estimate_dvalue),
        sample_curve_parameters = args$sample_curve_parameters %||% NA_character_,
        sample_coef_hierarchy = args$sample_coef_hierarchy %||% NA_character_,
        coef_parameterization = args$coef_parameterization %||% NA_character_,
        alpha_parameterization = args$alpha_parameterization %||% NA_character_,
        ucm_parameterization = args$ucm_parameterization %||% NA_character_,
        center_predictors_for_sampling = isTRUE(args$center_predictors_for_sampling),
        rmse = NA_real_,
        r2 = NA_real_,
        divergences_total = NA_real_,
        treedepth_hits_total = NA_real_,
        min_bfmi = NA_real_,
        max_rhat = NA_real_,
        model_readiness = "error"
      )
      next
    }
    fits[[vname]] <- fit_result$fit
    fit_i <- fits[[vname]]
    fq <- fit_i$diagnostics$fit_quality_overall
    so <- fit_i$diagnostics$sampler_overall
    flags <- fit_i$diagnostics$diagnostics_flags
    readiness <- fit_i$diagnostics$model_readiness %||% data.table()
    summary_rows[[i]] <- data.table(
      variant = vname,
      status = "ok",
      error = NA_character_,
      intercept_type = fit_i$intercept_type %||% NA_character_,
      likelihood = fit_i$likelihood %||% NA_character_,
      estimate_dvalue = isTRUE(fit_i$estimate_dvalue),
      sample_curve_parameters = fit_i$sample_curve_parameters %||% NA_character_,
      sample_coef_hierarchy = fit_i$sample_coef_hierarchy %||% NA_character_,
      coef_parameterization = fit_i$coef_parameterization %||% NA_character_,
      alpha_parameterization = fit_i$alpha_parameterization %||% NA_character_,
      ucm_parameterization = fit_i$ucm_parameterization %||% NA_character_,
      center_predictors_for_sampling = isTRUE(fit_i$center_predictors_for_sampling),
      rmse = if (nrow(fq)) fq[1, rmse] else NA_real_,
      r2 = if (nrow(fq)) fq[1, r2] else NA_real_,
      divergences_total = if (nrow(so)) so[1, divergences_total] else NA_real_,
      treedepth_hits_total = if (nrow(so)) so[1, treedepth_hits_total] else NA_real_,
      min_bfmi = if (nrow(so)) so[1, min_bfmi] else NA_real_,
      max_rhat = if (nrow(so)) so[1, max_rhat] else NA_real_,
      model_readiness = if (nrow(readiness)) readiness$model_readiness[1] else NA_character_
    )
  }
  summary <- rbindlist(summary_rows, fill = TRUE)
  dir.create(output_dir, recursive = TRUE, showWarnings = FALSE)
  fwrite(summary, file.path(output_dir, "sensitivity_summary.csv"))
  list(summary = summary, fits = fits)
}

