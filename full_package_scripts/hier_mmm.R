validate_prior_scales <- function(stan_data, verbose = TRUE) {
  all_sds <- c(
    stan_data$coef_sd_pos_log, stan_data$coef_sd_neg_log,
    stan_data$coef_sd_lower_log, stan_data$coef_sd_upper_log,
    stan_data$coef_raw_sd_bounded, stan_data$coef_sd_free,
    stan_data$rrate_raw_sd, stan_data$cvalue_raw_sd, stan_data$observed_cvalue_raw_sd,
    stan_data$dvalue_raw_sd
  )
  all_sds <- all_sds[is.finite(all_sds)]
  if (length(all_sds) && any(all_sds < 1e-4) && isTRUE(verbose)) {
    warning("One or more prior SDs are < 1e-4. Check coef_precision / curve precision values in metadata.")
  }
  if (length(all_sds) && any(all_sds > 50) && isTRUE(verbose)) {
    warning("One or more prior SDs are > 50. Priors may be effectively uninformative; check metadata.")
  }
  invisible(TRUE)
}

safe_cor <- function(x, y) {
  ok <- is.finite(x) & is.finite(y)
  if (sum(ok) < 3L) return(NA_real_)
  sx <- safe_sd(x[ok])
  sy <- safe_sd(y[ok])
  if (sx <= 1e-8 || sy <= 1e-8) return(NA_real_)
  suppressWarnings(stats::cor(x[ok], y[ok]))
}


clean_metadata <- function(meta_input,
                           bound_one_sided_coef_defaults = TRUE,
                           default_positive_coef_upper = 3,
                           default_negative_coef_lower = -3,
                           default_one_sided_coef_span = 6) {
  raw <- read_input_table(meta_input)
  if (!("variable" %in% names(raw))) stop("Metadata is missing required column: variable")
  raw <- prepare_metadata_shell(raw)

  dt <- copy(raw[!is_ucm_metadata_row(raw)])
  if (!nrow(dt)) stop("Metadata contains no model variable rows after removing UCM configuration rows.")

  if (!("source_entity" %in% names(dt))) dt[, source_entity := "GLOBAL"]
  # Accept handoff aliases from the semi-univariate prior builder. `cvalue` is
  # Stan's internal generic inverse-scale/rate parameter for either Weibull or
  # Hill. Do not map EC50/curve-location columns directly to it; those are
  # locations on the curve-input scale and need an explicit conversion.
  alias_map <- list(
    coef = c("coef_prior_final", "coef_prior"),
    coef_precision = c("coef_precision_final"),
    cvalue = c("curve_rate", "saturation_rate", "curve_inverse_scale", "saturation_inverse_scale"),
    rrate_precision = c("rrate_precision_final"),
    cvalue_precision = c("cvalue_precision_final", "curve_rate_precision", "saturation_rate_precision", "curve_inverse_scale_precision", "saturation_inverse_scale_precision"),
    dvalue_precision = c("dvalue_precision_final")
  )
  for (target_nm in names(alias_map)) {
    if (!(target_nm %in% names(dt))) {
      hit <- alias_map[[target_nm]][alias_map[[target_nm]] %in% names(dt)]
      if (length(hit)) dt[, (target_nm) := get(hit[1])]
    }
  }
  supplied_rrate__ <- "rrate" %in% names(dt)
  supplied_rrate_precision__ <- "rrate_precision" %in% names(dt)
  supplied_cvalue__ <- "cvalue" %in% names(dt)
  supplied_cvalue_precision__ <- "cvalue_precision" %in% names(dt)
  supplied_dvalue__ <- "dvalue" %in% names(dt)
  supplied_dvalue_precision__ <- "dvalue_precision" %in% names(dt)
  if (!("coef_bound" %in% names(dt))) {
    if ("bounds" %in% names(dt)) dt[, coef_bound := bounds] else dt[, coef_bound := ""]
  }
  if (!("curve_bound" %in% names(dt))) dt[, curve_bound := ""]
  if (!("curve_type" %in% names(dt))) dt[, curve_type := "weibull"]
  dt[, curve_type := normalize_curve_type_hier_mmm(curve_type)]
  if (!"cvalue_bound" %in% names(dt)) {
    hit <- intersect(c("curve_rate_bound", "saturation_rate_bound", "curve_inverse_scale_bound", "saturation_inverse_scale_bound"), names(dt))
    if (length(hit)) dt[, cvalue_bound := get(hit[1])]
  }
  for (nm in c("rrate_bound", "cvalue_bound", "dvalue_bound")) {
    if (!(nm %in% names(dt))) dt[, (nm) := ""]
  }
  anchor_aliases <- c(
    "anchor_saturation_handoff", "anchor_saturation", "median_anchor_saturation",
    "median_anchor", "saturation_at_median", "saturation_at_anchor"
  )
  if (!"anchor_saturation" %in% names(dt)) dt[, anchor_saturation := NA_real_]
  for (aa in anchor_aliases) {
    if (aa %in% names(dt)) {
      z <- suppressWarnings(as.numeric(dt[[aa]]))
      hit <- is.finite(z) & (!is.finite(suppressWarnings(as.numeric(dt$anchor_saturation))) | is.na(suppressWarnings(as.numeric(dt$anchor_saturation))))
      dt[hit, anchor_saturation := z[hit]]
    }
  }
  dt[, anchor_saturation := suppressWarnings(as.numeric(anchor_saturation))]
  dt[is.finite(anchor_saturation), anchor_saturation := clip_numeric(anchor_saturation, 0.01, 0.99)]
  supplied_anchor_row__ <- is.finite(dt$anchor_saturation)
  if (!"anchor_source" %in% names(dt)) dt[, anchor_source := NA_character_]
  if (!"cvalue_from_anchor" %in% names(dt)) dt[, cvalue_from_anchor := FALSE]
  explicit_no_curve__ <- if ("has_curve" %in% names(dt)) {
    suppressWarnings(as.integer(dt$has_curve)) == 0L
  } else {
    rep(FALSE, nrow(dt))
  }
  default_anchor_rows__ <- dt$role %in% c("media", "reach_frequency") &
    !supplied_anchor_row__ &
    !isTRUE(supplied_cvalue__) &
    !explicit_no_curve__
  if (any(default_anchor_rows__, na.rm = TRUE)) {
    dt[default_anchor_rows__, `:=`(
      anchor_saturation = 0.50,
      anchor_source = fifelse(is.na(anchor_source) | !nzchar(anchor_source), "default_median_active_50_saturation", anchor_source)
    )]
  }
  has_anchor__ <- is.finite(dt$anchor_saturation)
  if (!"rrate" %in% names(dt)) dt[, rrate := 0]
  if (!"rrate_precision" %in% names(dt)) dt[, rrate_precision := 1]
  if (!"cvalue" %in% names(dt)) dt[, cvalue := fifelse(has_anchor__, NA_real_, 0)]
  if (!"cvalue_precision" %in% names(dt)) dt[, cvalue_precision := 1]
  cvalue_numeric__ <- suppressWarnings(as.numeric(dt$cvalue))
  if (!"dvalue" %in% names(dt)) dt[, dvalue := fifelse(has_anchor__ | (is.finite(cvalue_numeric__) & cvalue_numeric__ > 0), 1, 0)]
  if (!"dvalue_precision" %in% names(dt)) dt[, dvalue_precision := 1]
  dt[, `:=`(
    rrate_defaulted = !isTRUE(supplied_rrate__),
    rrate_precision_defaulted = !isTRUE(supplied_rrate_precision__),
    cvalue_defaulted_from_anchor = has_anchor__ & !isTRUE(supplied_cvalue__),
    cvalue_precision_defaulted = !isTRUE(supplied_cvalue_precision__),
    dvalue_defaulted = !isTRUE(supplied_dvalue__),
    dvalue_precision_defaulted = !isTRUE(supplied_dvalue_precision__)
  )]

  req <- c(
    "variable", "source_entity",
    "rrate", "rrate_precision",
    "cvalue", "cvalue_precision",
    "dvalue", "dvalue_precision",
    "coef", "coef_precision",
    "coef_bound"
  )
  miss <- setdiff(req, names(dt))
  if (length(miss)) stop("Metadata is missing required model-variable columns: ", paste(miss, collapse = ", "))

  if (any(!nzchar(dt$variable))) stop("Metadata contains blank model variable names.")
  if (anyDuplicated(dt$variable)) stop("Metadata must have exactly one model row per variable. Duplicates found.")

  dt[, source_entity := normalize_source_entity(source_entity)]
  dt[, role := standardize_role(role)]
  dt[, effect_type := role]

  num_cols <- c(
    "rrate", "rrate_precision",
    "cvalue", "cvalue_precision",
    "dvalue", "dvalue_precision",
    "coef", "coef_precision"
  )
  legacy_num_cols <- intersect(
    c("rrate_lower", "rrate_upper", "cvalue_lower", "cvalue_upper", "dvalue_lower", "dvalue_upper"),
    names(dt)
  )
  for (nm in c(num_cols, legacy_num_cols)) dt[, (nm) := suppressWarnings(as.numeric(get(nm)))]
  cvalue_missing <- (!is.finite(dt$cvalue) | is.na(dt$cvalue) | dt$cvalue <= 0) & is.finite(dt$anchor_saturation)
  if (any(cvalue_missing)) {
    dt[cvalue_missing, `:=`(
      cvalue = 1,
      cvalue_from_anchor = TRUE,
      anchor_source = fifelse(is.na(anchor_source) | !nzchar(anchor_source), "direct_metadata_anchor", anchor_source)
    )]
  }
  if (anyNA(dt[, ..num_cols])) stop("Metadata contains NA/non-numeric values in required numeric model-variable columns.")
  if (!"coef_hierarchy_scale" %in% names(dt)) dt[, coef_hierarchy_scale := 1]
  dt[, coef_hierarchy_scale := suppressWarnings(as.numeric(coef_hierarchy_scale))]
  dt[!is.finite(coef_hierarchy_scale) | is.na(coef_hierarchy_scale) | coef_hierarchy_scale < 0, coef_hierarchy_scale := 1]
  dt[, coef_hierarchy_scale := clip_numeric(coef_hierarchy_scale, 0, 5)]

  dt[, has_curve := as.integer(!(rrate == 0 & cvalue == 0 & dvalue == 0))]

  cb <- parse_coef_bound_cell(dt$coef_bound, label = "coef_bound", allow_pos_neg = TRUE)
  dt[, `:=`(
    bound_type = cb$bound_type,
    coef_lower = cb$lower,
    coef_upper = cb$upper
  )]

  dt[coef_precision <= 0 | !is.finite(coef_precision), coef_precision := 1]
  dt[rrate_precision <= 0 | !is.finite(rrate_precision), rrate_precision := 1]
  dt[cvalue_precision <= 0 | !is.finite(cvalue_precision), cvalue_precision := 1]
  dt[dvalue_precision <= 0 | !is.finite(dvalue_precision), dvalue_precision := 1]

  default_positive_coef_upper <- as.numeric(default_positive_coef_upper)[1]
  default_negative_coef_lower <- as.numeric(default_negative_coef_lower)[1]
  default_one_sided_coef_span <- as.numeric(default_one_sided_coef_span)[1]
  if (!is.finite(default_positive_coef_upper) || default_positive_coef_upper <= 0) default_positive_coef_upper <- 3
  if (!is.finite(default_negative_coef_lower) || default_negative_coef_lower >= 0) default_negative_coef_lower <- -3
  if (!is.finite(default_one_sided_coef_span) || default_one_sided_coef_span <= 0) default_one_sided_coef_span <- 6
  dt[, coef_default_bound_applied := FALSE]
  if (isTRUE(bound_one_sided_coef_defaults)) {
    coef_nominal_sd__ <- sqrt(1 / pmax(dt$coef_precision, 1e-8))
    pos_hit <- dt$bound_type == "pos" & !is.finite(dt$coef_upper)
    if (any(pos_hit)) {
      upper <- pmax(default_positive_coef_upper, dt$coef[pos_hit] + 6 * coef_nominal_sd__[pos_hit], abs(dt$coef[pos_hit]) * 4)
      dt[pos_hit, `:=`(bound_type = "bounded", coef_lower = 0, coef_upper = upper, coef_default_bound_applied = TRUE)]
    }
    neg_hit <- dt$bound_type == "neg" & !is.finite(dt$coef_lower)
    if (any(neg_hit)) {
      lower <- pmin(default_negative_coef_lower, dt$coef[neg_hit] - 6 * coef_nominal_sd__[neg_hit], -abs(dt$coef[neg_hit]) * 4)
      dt[neg_hit, `:=`(bound_type = "bounded", coef_lower = lower, coef_upper = 0, coef_default_bound_applied = TRUE)]
    }
    lower_hit <- dt$bound_type == "lower" & !is.finite(dt$coef_upper)
    if (any(lower_hit)) {
      upper <- pmax(dt$coef_lower[lower_hit] + default_one_sided_coef_span, dt$coef[lower_hit] + 6 * coef_nominal_sd__[lower_hit])
      dt[lower_hit, `:=`(bound_type = "bounded", coef_upper = upper, coef_default_bound_applied = TRUE)]
    }
    upper_hit <- dt$bound_type == "upper" & !is.finite(dt$coef_lower)
    if (any(upper_hit)) {
      lower <- pmin(dt$coef_upper[upper_hit] - default_one_sided_coef_span, dt$coef[upper_hit] - 6 * coef_nominal_sd__[upper_hit])
      dt[upper_hit, `:=`(bound_type = "bounded", coef_lower = lower, coef_default_bound_applied = TRUE)]
    }
  }

  n <- nrow(dt)
  get_default <- function(lower_col, upper_col, lower_default, upper_default) {
    lo <- rep(lower_default, n)
    hi <- rep(upper_default, n)
    if (lower_col %in% names(dt)) {
      z <- suppressWarnings(as.numeric(dt[[lower_col]]))
      lo[is.finite(z)] <- z[is.finite(z)]
    }
    if (upper_col %in% names(dt)) {
      z <- suppressWarnings(as.numeric(dt[[upper_col]]))
      hi[is.finite(z)] <- z[is.finite(z)]
    }
    list(lower = lo, upper = hi)
  }

  choose_bound <- function(specific_col) {
    spec <- trimws(as.character(dt[[specific_col]]))
    gen <- trimws(as.character(dt[["curve_bound"]]))
    spec[is.na(spec)] <- ""
    gen[is.na(gen)] <- ""
    ifelse(nzchar(spec), spec, gen)
  }

  rb_def <- get_default("rrate_lower", "rrate_upper", 0.00, 0.90)
  cb_def <- get_default("cvalue_lower", "cvalue_upper", 0.05, 8.00)
  db_def <- get_default("dvalue_lower", "dvalue_upper", 0.50, 2.00)

  rb <- parse_curve_bound_cell(choose_bound("rrate_bound"), rb_def$lower, rb_def$upper, label = "rrate_bound")
  cvb <- parse_curve_bound_cell(choose_bound("cvalue_bound"), cb_def$lower, cb_def$upper, label = "cvalue_bound")
  db <- parse_curve_bound_cell(choose_bound("dvalue_bound"), db_def$lower, db_def$upper, label = "dvalue_bound")

  dt[, `:=`(
    rrate_lower = rb$lower,
    rrate_upper = rb$upper,
    cvalue_lower = cvb$lower,
    cvalue_upper = cvb$upper,
    dvalue_lower = db$lower,
    dvalue_upper = db$upper
  )]

  dt[has_curve == 0L, `:=`(
    rrate_lower = 0.00,
    rrate_upper = 1e-4,
    cvalue_lower = 0.00,
    cvalue_upper = 1e-4,
    dvalue_lower = 0.00,
    dvalue_upper = 1e-4
  )]

  if (dt[has_curve == 1L, any(rrate_lower >= rrate_upper | cvalue_lower >= cvalue_upper | dvalue_lower >= dvalue_upper)]) {
    stop("Curve bounds must resolve to lower < upper for all curved variables.")
  }

  dt[has_curve == 1L, rrate := clip_to_open_interval(rrate, rrate_lower, rrate_upper)]
  dt[has_curve == 1L, cvalue := clip_to_open_interval(cvalue, cvalue_lower, cvalue_upper)]
  dt[has_curve == 1L, dvalue := clip_to_open_interval(dvalue, dvalue_lower, dvalue_upper)]

  dt[bound_type == "bounded", coef := clip_to_open_interval(coef, coef_lower, coef_upper)]

  if (dt[bound_type == "pos", any(coef <= 0)]) {
    stop("Positive-bounded variables must have positive coefficient prior centers.")
  }
  if (dt[bound_type == "neg", any(coef >= 0)]) {
    stop("Negative-bounded variables must have negative coefficient prior centers.")
  }
  if (dt[bound_type == "lower", any(coef <= coef_lower)]) {
    stop("Lower-bounded variables must have coef > lower bound.")
  }
  if (dt[bound_type == "upper", any(coef >= coef_upper)]) {
    stop("Upper-bounded variables must have coef < upper bound.")
  }

  if ("row_type_legacy" %in% names(dt)) dt[, row_type_legacy := NULL]
  if ("variable_role" %in% names(dt)) dt[, variable_role := NULL]

  setcolorder(dt, c(
    "variable", "effect_type", "source_entity", "role", "has_curve",
    "curve_type", "rrate", "rrate_precision", "cvalue", "cvalue_precision", "dvalue", "dvalue_precision",
    "coef", "coef_precision", "coef_hierarchy_scale", "coef_bound", "bound_type", "coef_lower", "coef_upper",
    "coef_default_bound_applied",
    "curve_bound", "rrate_bound", "cvalue_bound", "dvalue_bound",
    "rrate_lower", "rrate_upper", "cvalue_lower", "cvalue_upper", "dvalue_lower", "dvalue_upper"
  ))
  dt[]
}
build_curve_priors <- function(meta, estimate_dvalue = FALSE, curve_prior_relax = 2, min_sd_frac = 0.08) {
  md <- copy(meta)
  first_num_col <- function(dt, cols, default = NA_real_) {
    out <- rep(default, nrow(dt))
    for (cc in cols) {
      if (cc %in% names(dt)) {
        z <- suppressWarnings(as.numeric(dt[[cc]]))
        hit <- is.finite(z) & z > 0 & (!is.finite(out) | out <= 0)
        out[hit] <- z[hit]
      }
    }
    out
  }
  if (!"stan_observed_cvalue_reliability" %in% names(md)) md[, stan_observed_cvalue_reliability := NA_real_]
  if (!"stan_observed_cvalue_source" %in% names(md)) md[, stan_observed_cvalue_source := NA_character_]
  md[, observed_cvalue_mu__ := first_num_col(.SD, c("stan_observed_cvalue", "pooled_ramp_cvalue", "cvalue_data_driven", "observed_slope_cvalue"))]
  md[, observed_cvalue_reliability__ := suppressWarnings(as.numeric(stan_observed_cvalue_reliability))]
  if ("pooled_ramp_reliability" %in% names(md)) {
    z <- suppressWarnings(as.numeric(md$pooled_ramp_reliability))
    md[!is.finite(observed_cvalue_reliability__) & is.finite(z), observed_cvalue_reliability__ := z]
  }
  if ("cvalue_data_improvement" %in% names(md)) {
    z <- suppressWarnings(as.numeric(md$cvalue_data_improvement))
    md[!is.finite(observed_cvalue_reliability__) & is.finite(z) & z > 0, observed_cvalue_reliability__ := pmin(0.60, pmax(0.10, z * 3))]
  }
  md[!is.finite(observed_cvalue_reliability__) | observed_cvalue_reliability__ < 0, observed_cvalue_reliability__ := 0]
  md[, observed_cvalue_reliability__ := clip_numeric(observed_cvalue_reliability__, 0, 1)]

  out <- md[, .(
    variable, has_curve,
    rrate_mu = rrate,
    cvalue_mu = cvalue,
    dvalue_mu = dvalue,
    rrate_sd = pmax(sqrt(1 / pmax(rrate_precision, 1e-8)) * curve_prior_relax,
                    min_sd_frac * (rrate_upper - rrate_lower)),
    cvalue_sd = pmax(sqrt(1 / pmax(cvalue_precision, 1e-8)) * curve_prior_relax,
                     min_sd_frac * (cvalue_upper - cvalue_lower)),
    dvalue_sd = pmax(sqrt(1 / pmax(dvalue_precision, 1e-8)) * curve_prior_relax,
                     min_sd_frac * (dvalue_upper - dvalue_lower)),
    observed_cvalue_mu = observed_cvalue_mu__,
    observed_cvalue_reliability = observed_cvalue_reliability__,
    observed_cvalue_source = as.character(stan_observed_cvalue_source),
    rrate_lower, rrate_upper,
    cvalue_lower, cvalue_upper,
    dvalue_lower = if (estimate_dvalue) dvalue_lower else dvalue,
    dvalue_upper = if (estimate_dvalue) dvalue_upper else (dvalue + 1e-4)
  )]

  out[has_curve == 1L, `:=`(
    rrate_raw_mu = inv_logit_bounded(rrate_mu, rrate_lower, rrate_upper),
    cvalue_raw_mu = inv_logit_bounded(cvalue_mu, cvalue_lower, cvalue_upper),
    dvalue_raw_mu = inv_logit_bounded(dvalue_mu, dvalue_lower, dvalue_upper),
    rrate_raw_sd = raw_sd_from_original_sd(rrate_mu, rrate_sd, rrate_lower, rrate_upper),
    cvalue_raw_sd = raw_sd_from_original_sd(cvalue_mu, cvalue_sd, cvalue_lower, cvalue_upper),
    dvalue_raw_sd = raw_sd_from_original_sd(dvalue_mu, dvalue_sd, dvalue_lower, dvalue_upper),
    use_observed_cvalue_prior = as.integer(is.finite(observed_cvalue_mu) & observed_cvalue_mu > cvalue_lower & observed_cvalue_mu < cvalue_upper & observed_cvalue_reliability >= 0.10),
    observed_cvalue_raw_mu = inv_logit_bounded(clip_to_open_interval(observed_cvalue_mu, cvalue_lower, cvalue_upper), cvalue_lower, cvalue_upper),
    observed_cvalue_raw_sd = raw_sd_from_original_sd(
      clip_to_open_interval(observed_cvalue_mu, cvalue_lower, cvalue_upper),
      pmax((0.40 - 0.25 * observed_cvalue_reliability) * pmax(observed_cvalue_mu, 1e-6), min_sd_frac * (cvalue_upper - cvalue_lower)),
      cvalue_lower,
      cvalue_upper,
      floor = 0.20,
      cap = 3.0
    )
  )]

  out[has_curve == 0L, `:=`(
    rrate_raw_mu = 0, rrate_raw_sd = 0.05,
    cvalue_raw_mu = 0, cvalue_raw_sd = 0.05,
    dvalue_raw_mu = 0, dvalue_raw_sd = 0.05,
    use_observed_cvalue_prior = 0L,
    observed_cvalue_raw_mu = 0,
    observed_cvalue_raw_sd = 1
  )]
  out[!is.finite(observed_cvalue_raw_mu), observed_cvalue_raw_mu := 0]
  out[!is.finite(observed_cvalue_raw_sd) | observed_cvalue_raw_sd <= 0, observed_cvalue_raw_sd := 1]
  out[]
}

build_coef_prior_components <- function(meta,
                                        coef_prior_relax = 1.5,
                                        min_coef_sd_abs = 0.001,
                                        min_coef_sd_frac = 0.05,
                                        hard_prior_precision_threshold = 1e6,
                                        min_tau_scale = 0.05) {
  dt <- copy(meta)
  dt[, prior_sd_nominal__ := sqrt(1 / pmax(coef_precision, 1e-8)) * coef_prior_relax]
  dt[, prior_sd_floor__ := min_coef_sd_frac * abs(coef) + min_coef_sd_abs]
  dt[, coef_sd := fifelse(
    is.finite(coef_precision) & coef_precision >= hard_prior_precision_threshold,
    pmax(prior_sd_nominal__, 1e-8),
    pmax(prior_sd_nominal__, prior_sd_floor__)
  )]

  pos <- dt[bound_type == "pos"]
  neg <- dt[bound_type == "neg"]
  lower <- dt[bound_type == "lower"]
  upper <- dt[bound_type == "upper"]
  bounded <- dt[bound_type == "bounded"]
  free <- dt[bound_type == "free"]

  pos_conv <- if (nrow(pos)) softplus_from_mean_sd(pos$coef, pos$coef_sd) else list(raw_mu = numeric(), raw_sd = numeric())
  neg_conv <- if (nrow(neg)) softplus_from_mean_sd(abs(neg$coef), neg$coef_sd) else list(raw_mu = numeric(), raw_sd = numeric())
  lower_conv <- if (nrow(lower)) softplus_from_mean_sd(lower$coef - lower$coef_lower, lower$coef_sd) else list(raw_mu = numeric(), raw_sd = numeric())
  upper_conv <- if (nrow(upper)) softplus_from_mean_sd(upper$coef_upper - upper$coef, upper$coef_sd) else list(raw_mu = numeric(), raw_sd = numeric())

  bounded_raw_mu <- if (nrow(bounded)) inv_logit_bounded(bounded$coef, bounded$coef_lower, bounded$coef_upper) else numeric()
  bounded_raw_sd <- if (nrow(bounded)) raw_sd_from_original_sd(
    bounded$coef, bounded$coef_sd, bounded$coef_lower, bounded$coef_upper,
    floor = 0.10, cap = 2.0
  ) else numeric()
  tau_scale_for <- function(base, scale) {
    scale <- as.numeric(scale)
    scale[!is.finite(scale) | is.na(scale) | scale < 0] <- 1
    pmax(base * scale, min_tau_scale * pmin(scale, 1), 1e-6)
  }

  list(
    pos = list(dt = pos, meanlog = pos_conv$raw_mu, sdlog = pos_conv$raw_sd,
               tau_scale = tau_scale_for(pos_conv$raw_sd * 0.50, pos$coef_hierarchy_scale)),
    neg = list(dt = neg, meanlog = neg_conv$raw_mu, sdlog = neg_conv$raw_sd,
               tau_scale = tau_scale_for(neg_conv$raw_sd * 0.50, neg$coef_hierarchy_scale)),
    lower = list(dt = lower, meanlog = lower_conv$raw_mu, sdlog = lower_conv$raw_sd,
                 tau_scale = tau_scale_for(lower_conv$raw_sd * 0.50, lower$coef_hierarchy_scale)),
    upper = list(dt = upper, meanlog = upper_conv$raw_mu, sdlog = upper_conv$raw_sd,
                 tau_scale = tau_scale_for(upper_conv$raw_sd * 0.50, upper$coef_hierarchy_scale)),
    bounded = list(dt = bounded, raw_mu = bounded_raw_mu, raw_sd = bounded_raw_sd,
                   tau_scale = tau_scale_for(bounded_raw_sd * 0.75, bounded$coef_hierarchy_scale)),
    free = list(dt = free, mean = free$coef, sd = free$coef_sd,
                tau_scale = tau_scale_for(free$coef_sd * 0.75, free$coef_hierarchy_scale))
  )
}

assess_group_exposure_variation <- function(dt,
                                            variables,
                                            group_col,
                                            time_col,
                                            cv_threshold = 0.02,
                                            range_threshold = 0.05,
                                            min_mean_abs = 1e-8) {
  if (!length(variables)) {
    return(data.table(
      variable = character(),
      geo_variation_week_share = numeric(),
      geo_variation_median_cv = numeric()
    ))
  }
  out <- vector("list", length(variables))
  for (i in seq_along(variables)) {
    vv <- variables[i]
    by_time <- dt[, {
      x <- suppressWarnings(as.numeric(get(vv)))
      x <- x[is.finite(x)]
      if (length(x) <= 1L) {
        .(n_group = length(x), mean_abs = mean(abs(x)), sd_x = 0, range_x = 0)
      } else {
        .(
          n_group = length(x),
          mean_abs = mean(abs(x)),
          sd_x = stats::sd(x),
          range_x = max(x) - min(x)
        )
      }
    }, by = time_col]
    by_time[, denom := pmax(mean_abs, min_mean_abs)]
    by_time[, cv := fifelse(is.finite(sd_x), sd_x / denom, 0)]
    by_time[, range_rel := fifelse(is.finite(range_x), range_x / denom, 0)]
    usable <- by_time[n_group > 1L & is.finite(denom)]
    out[[i]] <- data.table(
      variable = vv,
      geo_variation_week_share = if (nrow(usable)) mean(usable$cv >= cv_threshold | usable$range_rel >= range_threshold, na.rm = TRUE) else 0,
      geo_variation_median_cv = if (nrow(usable)) stats::median(usable$cv, na.rm = TRUE) else 0
    )
  }
  rbindlist(out, fill = TRUE)
}


clean_coef_override_input <- function(coef_override_input,
                                      group_col,
                                      metadata,
                                      group_lookup) {
  if (is.null(coef_override_input)) {
    G <- nrow(group_lookup); J <- nrow(metadata)
    return(list(
      use = 0L,
      mu = matrix(0, nrow = G, ncol = J),
      sd = matrix(0, nrow = G, ncol = J),
      long = data.table()
    ))
  }

  ov <- read_input_table(coef_override_input)
  req <- c(group_col, "variable", "coef", "coef_precision")
  miss <- setdiff(req, names(ov))
  if (length(miss)) stop("coef_override_input is missing required columns: ", paste(miss, collapse = ", "))

  ov <- copy(ov)
  ov[, variable := trimws(as.character(variable))]
  ov[, coef := suppressWarnings(as.numeric(coef))]
  ov[, coef_precision := suppressWarnings(as.numeric(coef_precision))]
  ov <- ov[is.finite(coef) & is.finite(coef_precision) & coef_precision > 0]

  if (!nrow(ov)) {
    G <- nrow(group_lookup); J <- nrow(metadata)
    return(list(
      use = 0L,
      mu = matrix(0, nrow = G, ncol = J),
      sd = matrix(0, nrow = G, ncol = J),
      long = data.table()
    ))
  }

  setnames(ov, group_col, "group_value")
  ov[, group_value := as.character(group_value)]
  ov <- unique(ov, by = c("group_value", "variable"))

  md_clip <- metadata[, .(variable, bound_type, coef_lower, coef_upper)]
  ov <- md_clip[ov, on = "variable", nomatch = 0L]
  gl <- copy(group_lookup)[, .(group_value = as.character(group_value), group_idx)]
  ov <- gl[ov, on = "group_value", nomatch = 0L]

  eps <- 1e-6
  ov[bound_type == "pos", coef := pmax(coef, eps)]
  ov[bound_type == "neg", coef := pmin(coef, -eps)]
  ov[bound_type == "lower", coef := pmax(coef, coef_lower + eps)]
  ov[bound_type == "upper", coef := pmin(coef, coef_upper - eps)]
  ov[bound_type == "bounded", coef := pmin(pmax(coef, coef_lower + eps), coef_upper - eps)]

  ov[, coef_sd := 1 / sqrt(pmax(coef_precision, 1e-8))]

  G <- nrow(group_lookup)
  J <- nrow(metadata)
  mu <- matrix(0, nrow = G, ncol = J)
  sd <- matrix(0, nrow = G, ncol = J)

  var_map <- metadata[, .(variable, variable_idx = .I)]
  ov <- var_map[ov, on = "variable", nomatch = 0L]

  for (i in seq_len(nrow(ov))) {
    g <- ov$group_idx[i]
    j <- ov$variable_idx[i]
    mu[g, j] <- ov$coef[i]
    sd[g, j] <- ov$coef_sd[i]
  }

  list(
    use = as.integer(nrow(ov) > 0),
    mu = mu,
    sd = sd,
    long = ov[, .(group_value, variable, coef, coef_precision, coef_sd)]
  )
}

