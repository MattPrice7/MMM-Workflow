      cycle_harmonics = intercept_spec$cycle_harmonics
    ),
    normalize_curve_x = normalize_curve_x,
    sample_curve_parameters = sample_curve_parameters,
    sample_coef_hierarchy = sample_coef_hierarchy,
    coef_parameterization = coef_parameterization,
    coef_hierarchy_auto_min_groups = coef_hierarchy_auto_min_groups,
    coef_hierarchy_auto_min_scale = coef_hierarchy_auto_min_scale,
    coef_hierarchy_auto_min_geo_variation_share = coef_hierarchy_auto_min_geo_variation_share,
    alpha_parameterization = alpha_parameterization,
    ucm_parameterization = ucm_parameterization,
    center_predictors_for_sampling = center_predictors_for_sampling,
    likelihood = likelihood,
    student_t_nu = student_t_nu,
    estimate_dvalue = estimate_dvalue,
    curve_prior_relax = curve_prior_relax,
    coef_prior_relax = coef_prior_relax,
    min_coef_sd_abs = min_coef_sd_abs,
