    stop("extra_control_cols cannot overlap metadata variables because that would double-count them: ",
         paste(overlap_extra, collapse = ", "))
  }

  if (dt[, anyDuplicated(data.table::data.table(group_key__ = get(group_col), time_key__ = get(time_col))) > 0L]) {
    stop("Data contains duplicate group_col + time_col rows. Each panel-week must be unique.")
  }

  if (anyNA(dt[[group_col]])) stop("group_col contains NA values.")
  if (anyNA(dt[[time_col]])) stop("time_col contains NA values.")

  keep_cols <- unique(c(group_col, time_col, entity_col, dep_var_col, md$variable, extra_control_cols, dep_scale_col, holdout_col))
  ignored_data_cols <- setdiff(names(dt), keep_cols)
