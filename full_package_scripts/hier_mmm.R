  if (!is.null(output_dir) && nzchar(output_dir)) {
    checkpoint("write output files", status = "START", detail = output_dir)
    write_outputs_hier_mmm(
      outputs = outputs,
      diagnostics = diagnostics,
      output_dir = output_dir,
      prefix = output_prefix
    )
    checkpoint("write output files", detail = output_dir)
  }

  checkpoint("fit_hier_mmm complete", detail = "returning fit object and outputs")

  list(
    fit = fit,
    stan_data = prep$stan_data,
    metadata = prep$metadata,
    curve_priors = prep$curve_priors,
    variable_lookup = prep$variable_lookup,
    group_lookup = prep$group_lookup,
    data = prep$data,
    intercept_type = prep$intercept_type,
    intercept_spec = prep$intercept_spec,
    ucm_spec = prep$ucm_spec,
    ucm_metadata = prep$ucm_metadata,
    init_strategy = init_strategy,
    pathfinder_fit = pathfinder_fit,
    intercept_use_level = prep$intercept_use_level,
