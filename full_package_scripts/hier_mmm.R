  }

  setorderv(dt, c(group_col, time_col))

  # Holdout must be marked before mean-indexing so validation rows do not leak into
  # training-scale denominators.
  dt[, is_holdout__ := FALSE]
  if (!is.null(holdout_col)) {
    hv <- holdout_value
    if (is.logical(hv) && length(hv) == 1L && isTRUE(hv)) {
      hv_raw <- tolower(trimws(as.character(dt[[holdout_col]])))
      hv_raw[is.na(hv_raw)] <- ""
      dt[, is_holdout__ := hv_raw %in% c("true", "t", "yes", "y", "1", "on", "holdout", "test")]
    } else if (is.logical(hv) && length(hv) == 1L && isFALSE(hv)) {
      hv_raw <- tolower(trimws(as.character(dt[[holdout_col]])))
