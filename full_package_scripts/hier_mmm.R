# =============================================================================
# hier_mmm.R
# FINAL BUNDLE VERSION: v19_consultant_workflow_patch_2026_05_19
#
#
# KEY CHANGES FROM v15
#   1. Holdout rows are marked before mean-indexing, so training-scale denominators
#      do not use validation/holdout values.
#   2. Stan log_lik is now length N_train only; use extract_loo_hier_mmm().
#   3. Added lightweight prior-scale warnings before Stan compile.
#   4. Tightened prior-vs-posterior near_prior flag from 0.25 to 0.10 prior SD.
#   5. Fixed duplicated corr_ucm_actual field in UCM/media stealing diagnostics.
#   6. Made posterior mean extraction use a named lookup instead of repeated table scans.
#   7. Public API cleanup: dep_var_col / dep_scale_col names, automatic Stan file lookup,
#      deployment-ready output defaults, student_t default likelihood, and fixed dvalue default.
#   8. Metadata can include coef_hierarchy_scale to shrink group coefficient variation
#      for national-only or weakly geo-resolved media.
# PURPOSE
#   Parsimonious hierarchical Bayesian MMM wrapper for weekly panel data.
#   This is the FINAL MODEL wrapper. It is not a prior factory.
#
# MODEL DESIGN
#   - No manual family column in the metadata input.
#   - Each variable is its own transform-sharing unit internally.
#   - Curves are estimated at the VARIABLE level only, never at the group level.
#   - Curve parameters are only instantiated for variables with has_curve = 1.
#   - Optional holdout rows can be excluded from the likelihood but retained for prediction diagnostics.
#   - Curve normalization is based on TRAINING rows only to avoid validation leakage.
#   - Coefficient prior floors are small and configurable so near-zero guardrail priors are not accidentally loosened.
#   - Major run checkpoints can be printed and optionally written to a log file.
#   - v16 keeps the explicit index mapping and avoids fragile data.table x.* join lookups.
#   - Coefficients vary by group through a hierarchical prior.
#   - Non-UCM variables not listed in the metadata file are excluded from the modeled
#     variable matrix by default.
#   - Data is automatically sorted by group_col, then time_col.
#   - group_col + time_col must be unique within the input data.
#
# RECOMMENDED DEFAULT INTERCEPT SETUP
#   Default baseline is Fourier seasonality without a stochastic UCM level:
#   intercept_type = "fourier"
#   ucm_spec = list(
#     season = TRUE,
#     cycle = FALSE,
#     season_period = 52L,
#     season_harmonics = 2L,
#     cycle_period = 104L,
#     cycle_harmonics = 1L
#   )
#
#   Final UCM joint-estimation run is still supported through:
#   intercept_type = "ucm"
#   init_strategy = "ucm_pathfinder"
#   or the convenience wrapper fit_hier_mmm_ucm_final().
#
# MODEL-LEVEL INTERCEPT / UCM INPUTS
#   UCM can be controlled either by wrapper inputs or by metadata rows with role = "ucm".
#   UCM rows are internal model variables/components generated by the model;
#   they are not required to exist as raw data columns.
#   Supported UCM config rows include ucm_level, ucm_season, and ucm_cycle with:
#     - enabled   : TRUE/FALSE
#     - period    : used for season/cycle rows
#     - harmonics : used for season/cycle rows
#     - prior     : prior center or scale for UCM rows
#     - precision : optional prior precision; converted to prior sd as 1 / sqrt(precision)
#
# REQUIRED WEEKLY DATA INPUT
#   Weekly group-level panel data with at least:
#     - dep_var_col     : dependent variable column
#     - group_col       : panel id (for example mod_id)
#     - time_col        : sortable weekly column
#     - entity_col    : target entity being modeled in each row / panel
#     - one column for every non-UCM variable listed in the metadata file
#   - any data columns NOT listed as non-UCM metadata variables are excluded from X
#     and are not modeled as variables unless explicitly passed in
#     extra_control_cols
#
# REQUIRED METADATA INPUT (single file / data.table / data.frame)
#   Required columns EXACTLY in this logic:
#     - variable            : raw data column for normal X variables; internal UCM name for role=ucm
#     - role                : media, distribution, price, promo, trade, comp, macro, dummy, control, other, ucm
#     - source_entity       : source entity for that variable
#                             blank / NA / 0 / GLOBAL / globale all mean "GLOBAL"
#                             otherwise this is compared to entity_col only as
#                             a halo diagnostic (not as a model family)
#     - rrate               : prior center for retention / adstock
#     - rrate_precision     : prior precision for rrate
#     - cvalue              : internal saturation inverse-scale/rate prior center
#                             retained for backward compatibility. Prefer
#                             curve_rate or anchor_saturation in new metadata.
#                             For Hill, this is not EC50; EC50 on the normalized
#                             curve-input scale is 1 / curve_rate when dvalue = 1.
#     - cvalue_precision    : prior precision for the internal curve rate
#     - dvalue              : prior center for saturation shape
#     - dvalue_precision    : prior precision for dvalue
#     - coef                : coefficient prior center at VARIABLE level
#     - coef_precision      : coefficient prior precision at VARIABLE level
#     - coef_bound          : coefficient constraint cell: pos, neg, (lower,upper), (lower,), (,upper), blank/free
#     - curve_bound         : optional shorthand curve bound; parameter-specific bounds below override it
#     - rrate_bound         : optional rrate bound cell using the same parenthetical syntax
#     - cvalue_bound        : optional internal curve-rate bound cell using the
#                             same parenthetical syntax; curve_rate_bound is also accepted
#     - dvalue_bound        : optional dvalue bound cell using the same parenthetical syntax
#
# IMPORTANT RULES
#   - If rrate, internal curve rate, and dvalue are all 0, that variable is treated as NO-CURVE.
#   - Separate precisions are required for rrate, internal curve rate, dvalue, and coef.
#   - coef_by_mod_id stays on model scale; decomposition outputs are rescaled to
#     original units when possible.
#   - source_entity is generic. It does not need to be product-specific.
#   - source_entity is normalized so blank / NA / 0 / GLOBAL / globale all become "GLOBAL".
#   - halo is diagnosed as source_entity != entity_col for non-GLOBAL variables.
#
# NOTES
#   - This wrapper converts precision to prior sd internally with optional relaxation.
#   - coef_bound applies to coefficients. rrate_bound/curve_rate_bound/dvalue_bound apply to curve parameters.
#   - Curves are bounded internally to parsimonious defaults:
#       rrate  in [0.00, 0.90]
#       internal curve rate in [0.05, 8.00]
#       dvalue in [0.50, 2.00]
#   - If estimate_dvalue = FALSE, dvalue is effectively fixed near its metadata center.
#   - Default likelihood is student_t because weekly MMM data often has outliers/heavy tails.
#   - Stan file is auto-detected from the folder containing hier_mmm.R.
#   - Outputs default to ./hier_mmm_outputs unless output_dir is changed.
#
# OUTPUTS
#   - fit              : CmdStanMCMC object
#   - stan_data        : data passed to Stan
#   - metadata         : cleaned metadata input
#   - curve_priors     : per-variable curve priors used by Stan
#   - variable_lookup  : variable order / coef_bound / halo diagnostics
#   - group_lookup     : mapping of group index back to group value
#   - data             : cleaned / ordered working data
#   - coef_by_mod_id   : wide coefficient table by group/mod_id
#   - wide_decomp      : row-level wide decomposition table
#   - long_decomp      : row-level long decomposition table
#   - separated diagnostics:
#       diagnostics_bayes_* for the joint Bayesian model / sampler / fit quality
#       diagnostics_ucm_* for UCM parameters and intercept components
#   - optional coef_override_input can provide mod_id/group-specific backup coefficient priors
#
# DEPENDENCIES
#   install.packages(c("data.table", "cmdstanr", "posterior"))
# =============================================================================

