    )],
    variable_lookup = variable_lookup,
    group_lookup = group_lookup,
    intercept_spec = intercept_spec$label,
    intercept_use_level = intercept_spec$use_level,
    intercept_use_season = intercept_spec$use_season,
    intercept_use_cycle = intercept_spec$use_cycle,
    intercept_type = tolower(trimws(as.character(intercept_type %||% "fourier"))),
    ucm_spec = list(
      level = as.logical(intercept_spec$use_level),
