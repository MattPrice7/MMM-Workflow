mean_index_by_group <- function(dt, cols, by, eps = 1e-8, train_col = NULL) {
  dt <- copy(as.data.table(dt))
  cols <- unique(cols[cols %in% names(dt)])
  if (!length(cols)) return(dt)

  if (!is.null(train_col) && train_col %in% names(dt)) {
    den <- dt[get(train_col) == TRUE | get(train_col) == 1L, lapply(.SD, function(x) {
      m <- mean(x[is.finite(x)], na.rm = TRUE)
      if (!is.finite(m) || is.na(m) || abs(m) <= eps) 1 else m
    }), by = by, .SDcols = cols]
  } else {
    den <- dt[, lapply(.SD, function(x) {
      m <- mean(x[is.finite(x)], na.rm = TRUE)
      if (!is.finite(m) || is.na(m) || abs(m) <= eps) 1 else m
    }), by = by, .SDcols = cols]
  }

  den_cols <- paste0(cols, "__mi_denom")
  setnames(den, cols, den_cols)
  dt <- den[dt, on = by]

  for (i in seq_along(cols)) {
    cc <- cols[i]
    dd <- den_cols[i]
    dt[, (cc) := fifelse(abs(get(dd)) <= eps, get(cc), get(cc) / get(dd))]
    dt[, (dd) := NULL]
  }
  dt[]
}

mean_index_global_cols <- function(dt, cols, eps = 1e-8, train_col = NULL) {
  dt <- copy(as.data.table(dt))
  cols <- unique(cols[cols %in% names(dt)])
  if (!length(cols)) return(dt)
  train_mask <- rep(TRUE, nrow(dt))
  if (!is.null(train_col) && train_col %in% names(dt)) {
    train_mask <- dt[[train_col]] == TRUE | dt[[train_col]] == 1L
    train_mask[is.na(train_mask)] <- FALSE
  }
  for (cc in cols) {
    x <- dt[[cc]]
    m <- mean(x[train_mask & is.finite(x)], na.rm = TRUE)
    if (!is.finite(m) || is.na(m) || abs(m) <= eps) m <- 1
    dt[, (cc) := get(cc) / m]
  }
  dt[]
}

build_fourier_matrix <- function(time_vec, period = 52L, harmonics = 2L) {
  n <- length(time_vec)
  period <- as.integer(period %||% 52L)
  harmonics <- as.integer(harmonics %||% 0L)
  if (n <= 0L || harmonics <= 0L || period <= 1L) return(matrix(0, n, 0))

  ord_levels <- sort(unique(time_vec))
  tt <- match(time_vec, ord_levels)
  mats <- vector("list", harmonics)
  coln <- character(2L * harmonics)

  for (k in seq_len(harmonics)) {
    ang <- 2 * pi * k * tt / period
    mats[[k]] <- cbind(sin(ang), cos(ang))
    coln[(2L * k - 1L):(2L * k)] <- c(sprintf("season_sin_%02d", k), sprintf("season_cos_%02d", k))
  }

  out <- do.call(cbind, mats)
  colnames(out) <- coln
  storage.mode(out) <- "double"
  out
}

parse_hier_mmm_dates <- function(x, label = "date") {
  if (inherits(x, "Date")) return(as.Date(x))
  if (inherits(x, "IDate")) return(as.Date(x))
  if (inherits(x, "POSIXt")) return(as.Date(x))
  if (is.numeric(x)) {
    if (all(is.na(x))) return(as.Date(rep(NA, length(x))))
    med <- stats::median(x, na.rm = TRUE)
    if (is.finite(med) && med > 30000) return(as.Date(x, origin = "1899-12-30"))
    if (is.finite(med) && med > 10000) return(as.Date(x, origin = "1970-01-01"))
  }
  out <- suppressWarnings(as.Date(x))
  if (all(is.na(out)) && length(x)) {
    stop("Could not parse ", label, " as dates for holiday control generation.", call. = FALSE)
  }
  out
}

hier_mmm_nth_weekday <- function(year, month, weekday, n) {
  d0 <- as.Date(sprintf("%04d-%02d-01", as.integer(year), as.integer(month)))
  w0 <- as.POSIXlt(d0)$wday
  d <- d0 + ((weekday - w0) %% 7L) + 7L * (as.integer(n) - 1L)
  as.Date(d)
}

hier_mmm_last_weekday <- function(year, month, weekday) {
  first_next <- if (as.integer(month) == 12L) {
    as.Date(sprintf("%04d-01-01", as.integer(year) + 1L))
  } else {
    as.Date(sprintf("%04d-%02d-01", as.integer(year), as.integer(month) + 1L))
  }
  d0 <- first_next - 1L
  w0 <- as.POSIXlt(d0)$wday
  as.Date(d0 - ((w0 - weekday) %% 7L))
}

hier_mmm_easter_date <- function(year) {
  y <- as.integer(year)
  a <- y %% 19L
  b <- y %/% 100L
  c <- y %% 100L
  d <- b %/% 4L
  e <- b %% 4L
  f <- (b + 8L) %/% 25L
  g <- (b - f + 1L) %/% 3L
  h <- (19L * a + b - d - g + 15L) %% 30L
  i <- c %/% 4L
  k <- c %% 4L
  l <- (32L + 2L * e + 2L * i - h - k) %% 7L
  m <- (a + 11L * h + 22L * l) %/% 451L
  month <- (h + l - 7L * m + 114L) %/% 31L
  day <- ((h + l - 7L * m + 114L) %% 31L) + 1L
  as.Date(sprintf("%04d-%02d-%02d", y, month, day))
}

hier_mmm_holiday_catalog <- function(calendars, years) {
  calendars <- tolower(trimws(as.character(calendars %||% "us_major")))
  calendars[is.na(calendars) | !nzchar(calendars)] <- "us_major"
  calendars[calendars %in% c("us", "usa", "united_states", "us-major")] <- "us_major"
  calendars[calendars %in% c("eu", "europe", "eu-major")] <- "eu_major"
  calendars[calendars %in% c("global", "global-major", "major")] <- "global_major"
  years <- sort(unique(as.integer(years[is.finite(years)])))
  rows <- list()
  for (cal in unique(calendars)) {
    for (yy in years) {
      if (identical(cal, "us_major")) {
        rows[[length(rows) + 1L]] <- data.table(
          calendar = cal,
          holiday_name = c(
            "new_years_day", "martin_luther_king_day", "memorial_day",
            "independence_day", "labor_day", "thanksgiving_day", "christmas_day"
          ),
          holiday_date = as.Date(c(
            sprintf("%04d-01-01", yy),
            as.character(hier_mmm_nth_weekday(yy, 1L, 1L, 3L)),
            as.character(hier_mmm_last_weekday(yy, 5L, 1L)),
            sprintf("%04d-07-04", yy),
            as.character(hier_mmm_nth_weekday(yy, 9L, 1L, 1L)),
            as.character(hier_mmm_nth_weekday(yy, 11L, 4L, 4L)),
            sprintf("%04d-12-25", yy)
          ))
        )
      } else if (identical(cal, "eu_major")) {
        easter <- hier_mmm_easter_date(yy)
        rows[[length(rows) + 1L]] <- data.table(
          calendar = cal,
          holiday_name = c("new_years_day", "easter_monday", "may_day", "christmas_day", "boxing_day"),
          holiday_date = as.Date(c(
            sprintf("%04d-01-01", yy),
            as.character(easter + 1L),
            sprintf("%04d-05-01", yy),
            sprintf("%04d-12-25", yy),
            sprintf("%04d-12-26", yy)
          ))
        )
      } else if (identical(cal, "global_major")) {
        rows[[length(rows) + 1L]] <- data.table(
          calendar = cal,
          holiday_name = c("new_years_day", "christmas_day"),
          holiday_date = as.Date(c(sprintf("%04d-01-01", yy), sprintf("%04d-12-25", yy)))
        )
      } else {
        stop("Unsupported built-in holiday calendar: ", cal,
             ". Use 'US_major', 'EU_major', 'global_major', or pass custom_holidays.", call. = FALSE)
      }
    }
  }
  if (!length(rows)) return(data.table(calendar = character(), holiday_name = character(), holiday_date = as.Date(character())))
  unique(rbindlist(rows, fill = TRUE))
}

hier_mmm_custom_holidays <- function(custom_holidays = NULL) {
  if (is.null(custom_holidays)) {
    return(data.table(calendar = character(), holiday_name = character(), holiday_date = as.Date(character())))
  }
  if (inherits(custom_holidays, "Date") || is.character(custom_holidays) || is.numeric(custom_holidays)) {
    dates <- parse_hier_mmm_dates(custom_holidays, label = "custom_holidays")
    names_in <- names(custom_holidays)
    return(data.table(
      calendar = "custom",
      holiday_name = if (!is.null(names_in) && length(names_in) == length(dates)) make.names(names_in) else paste0("custom_", seq_along(dates)),
      holiday_date = dates
    ))
  }
  ch <- as.data.table(copy(custom_holidays))
  dcol <- intersect(c("holiday_date", "date", "period", "week", "ds"), names(ch))[1]
  if (is.na(dcol)) stop("custom_holidays must include holiday_date/date/period/week/ds.", call. = FALSE)
  ncol <- intersect(c("holiday_name", "holiday", "name", "event"), names(ch))[1]
  ccol <- intersect(c("calendar", "region", "country"), names(ch))[1]
  out <- data.table(
    calendar = if (!is.na(ccol)) as.character(ch[[ccol]]) else "custom",
    holiday_name = if (!is.na(ncol)) as.character(ch[[ncol]]) else paste0("custom_", seq_len(nrow(ch))),
    holiday_date = parse_hier_mmm_dates(ch[[dcol]], label = "custom_holidays")
  )
  out[is.na(calendar) | !nzchar(calendar), calendar := "custom"]
  out[is.na(holiday_name) | !nzchar(holiday_name), holiday_name := paste0("custom_", .I)]
  unique(out[!is.na(holiday_date)])
}

normalize_holiday_config_hier_mmm <- function(holiday_config = NULL) {
  if (is.null(holiday_config) || identical(holiday_config, FALSE)) {
    return(list(enabled = FALSE))
  }
  if (identical(holiday_config, TRUE)) holiday_config <- list()
  if (!is.list(holiday_config)) stop("holiday_config must be TRUE/FALSE or a list.", call. = FALSE)
  calendars <- holiday_config$calendars %||% holiday_config$calendar %||% "US_major"
  windows <- tolower(trimws(as.character(holiday_config$windows %||% holiday_config$window %||% "week_of")))
  windows[windows %in% c("holiday", "current", "during", "week", "weekof", "same_week")] <- "week_of"
  windows[windows %in% c("before", "pre", "lead", "previous", "prior", "week-before")] <- "week_before"
  windows[windows %in% c("after", "post", "lag", "following", "next", "week-after")] <- "week_after"
  bad <- setdiff(unique(windows), c("week_before", "week_of", "week_after"))
  if (length(bad)) stop("Unsupported holiday windows: ", paste(bad, collapse = ", "),
                        ". Use week_before, week_of, and/or week_after.", call. = FALSE)
  mode <- tolower(trimws(as.character(holiday_config$mode %||% "aggregate")))[1]
  if (!mode %in% c("aggregate", "separate")) stop("holiday_config$mode must be 'aggregate' or 'separate'.", call. = FALSE)
  period_days <- suppressWarnings(as.integer(holiday_config$period_days %||% NA_integer_))[1]
  if (!is.na(period_days) && (!is.finite(period_days) || period_days <= 0L)) stop("holiday_config$period_days must be a positive integer.", call. = FALSE)
  list(
    enabled = TRUE,
    calendars = calendars,
    custom_holidays = holiday_config$custom_holidays,
    windows = unique(windows),
    mode = mode,
    prefix = as.character(holiday_config$prefix %||% "holiday")[1],
    period_days = period_days,
    include_built_in = isTRUE(holiday_config$include_built_in %||% TRUE)
  )
}

add_holiday_controls_hier_mmm <- function(data, time_col, holiday_config = NULL, existing_extra_cols = NULL) {
  cfg <- normalize_holiday_config_hier_mmm(holiday_config)
  dt <- as.data.table(copy(data))
  if (!isTRUE(cfg$enabled)) {
    return(list(data = dt, extra_control_cols = existing_extra_cols %||% character(), audit = data.table()))
  }
  if (!(time_col %in% names(dt))) stop("time_col not found for holiday controls: ", time_col, call. = FALSE)
  period_start <- parse_hier_mmm_dates(dt[[time_col]], label = time_col)
  if (anyNA(period_start)) stop("time_col contains unparseable dates for holiday controls.", call. = FALSE)
  unique_dates <- sort(unique(period_start))
  inferred_days <- if (length(unique_dates) >= 2L) {
    diffs <- as.integer(diff(unique_dates))
    diffs <- diffs[is.finite(diffs) & diffs > 0L]
    if (length(diffs)) as.integer(stats::median(diffs)) else 7L
  } else {
    7L
  }
  period_days <- cfg$period_days
  if (!is.finite(period_days) || is.na(period_days) || period_days <= 0L) period_days <- inferred_days
  period_end <- period_start + period_days - 1L
  years <- seq(
    as.integer(format(min(period_start, na.rm = TRUE) - period_days * 2L, "%Y")),
    as.integer(format(max(period_end, na.rm = TRUE) + period_days * 2L, "%Y"))
  )
  holidays <- data.table()
  if (isTRUE(cfg$include_built_in)) holidays <- hier_mmm_holiday_catalog(cfg$calendars, years)
  holidays <- rbindlist(list(holidays, hier_mmm_custom_holidays(cfg$custom_holidays)), fill = TRUE)
  holidays <- unique(holidays[!is.na(holiday_date)])
  if (!nrow(holidays)) {
    return(list(data = dt, extra_control_cols = existing_extra_cols %||% character(), audit = data.table()))
  }
  window_offsets <- c(week_before = -1L, week_of = 0L, week_after = 1L)
  make_col <- function(parts) {
    nm <- paste(parts[nzchar(parts)], collapse = "_")
    nm <- gsub("[^A-Za-z0-9_]+", "_", nm)
    nm <- gsub("_+", "_", nm)
    nm <- gsub("^_|_$", "", nm)
    if (!nzchar(nm)) nm <- "holiday_control"
    nm
  }
  add_col_safe <- function(base) {
    col <- base
    ii <- 1L
    while (col %in% names(dt)) {
      ii <- ii + 1L
      col <- paste0(base, "_", ii)
    }
    col
  }
  audit_rows <- list()
  generated_cols <- character()
  for (ww in cfg$windows) {
    offset <- window_offsets[[ww]]
    if (identical(cfg$mode, "aggregate")) {
      col <- add_col_safe(make_col(c(cfg$prefix, ww)))
      dt[, (col) := 0]
      target_dates <- holidays$holiday_date + offset * period_days
      flag <- vapply(seq_along(period_start), function(i) any(target_dates >= period_start[i] & target_dates <= period_end[i]), logical(1))
      dt[, (col) := as.numeric(flag)]
      generated_cols <- c(generated_cols, col)
      audit_rows[[length(audit_rows) + 1L]] <- data.table(
        generated_col = col,
        mode = cfg$mode,
        window = ww,
        calendar = paste(sort(unique(holidays$calendar)), collapse = "|"),
        holiday_name = "all",
        holiday_count = length(unique(holidays$holiday_date)),
        active_rows = sum(flag, na.rm = TRUE),
        period_days = period_days
      )
    } else {
      for (hh in seq_len(nrow(holidays))) {
        hname <- tolower(make.names(holidays$holiday_name[hh]))
        col <- add_col_safe(make_col(c(cfg$prefix, hname, ww)))
        target_date <- holidays$holiday_date[hh] + offset * period_days
        flag <- target_date >= period_start & target_date <= period_end
        dt[, (col) := as.numeric(flag)]
        generated_cols <- c(generated_cols, col)
        audit_rows[[length(audit_rows) + 1L]] <- data.table(
          generated_col = col,
          mode = cfg$mode,
          window = ww,
          calendar = holidays$calendar[hh],
          holiday_name = holidays$holiday_name[hh],
          holiday_count = 1L,
          active_rows = sum(flag, na.rm = TRUE),
          period_days = period_days
        )
      }
    }
  }
  audit <- rbindlist(audit_rows, fill = TRUE)
  list(
    data = dt,
    extra_control_cols = unique(c(existing_extra_cols %||% character(), generated_cols)),
    audit = audit
  )
}


parse_model_intercept <- function(intercept_type = "fourier", ucm_spec = NULL) {
  itype <- tolower(trimws(as.character(intercept_type %||% "fourier")))
  if (itype %in% c("", "na", "n/a")) itype <- "fourier"
  if (itype %in% c("seasonal", "seasonality", "fourier_season", "fourier-season", "fourier_only", "fourier-only")) itype <- "fourier"

  if (itype == "none") {
    return(list(
      label = "none",
      intercept_mode = 0L,
      use_level = 0L,
      use_season = 0L,
      use_cycle = 0L,
      season_period = NA_integer_,
      season_harmonics = 0L,
      cycle_period = NA_integer_,
      cycle_harmonics = 0L
    ))
  }

  if (itype == "flat") {
    return(list(
      label = "flat",
      intercept_mode = 1L,
      use_level = 0L,
      use_season = 0L,
      use_cycle = 0L,
      season_period = NA_integer_,
      season_harmonics = 0L,
      cycle_period = NA_integer_,
      cycle_harmonics = 0L
    ))
  }

  if (!itype %in% c("fourier", "ucm")) {
    stop("intercept_type must be one of: 'none', 'flat', 'fourier', 'ucm'.")
  }

  default_spec <- if (itype == "fourier") {
    list(
      level = FALSE,
      season = TRUE,
      cycle = FALSE,
      season_period = 52L,
      season_harmonics = 2L,
      cycle_period = 104L,
      cycle_harmonics = 1L
    )
  } else {
    list(
      level = TRUE,
      season = TRUE,
      cycle = FALSE,
      season_period = 52L,
      season_harmonics = 2L,
      cycle_period = 104L,
      cycle_harmonics = 1L
    )
  }
  spec <- modifyList(default_spec, ucm_spec %||% list())

  use_level <- isTRUE(spec$level)
  use_season <- isTRUE(spec$season)
  use_cycle <- isTRUE(spec$cycle)

  if (itype == "ucm" && !use_level) {
    stop("When intercept_type = 'ucm', ucm_spec$level must be TRUE. Use intercept_type = 'fourier' for a no-level Fourier baseline.")
  }
  if (itype == "fourier" && use_level) {
    stop("When intercept_type = 'fourier', ucm_spec$level must be FALSE. Use intercept_type = 'ucm' for a stochastic level baseline.")
  }
  if (itype == "fourier" && !use_season && !use_cycle) {
    stop("intercept_type = 'fourier' requires season = TRUE and/or cycle = TRUE.")
  }

  season_period <- as.integer(spec$season_period %||% 52L)
  season_harmonics <- as.integer(spec$season_harmonics %||% 2L)
  cycle_period <- as.integer(spec$cycle_period %||% 104L)
  cycle_harmonics <- as.integer(spec$cycle_harmonics %||% 1L)

  if (use_season) {
    if (!is.finite(season_period) || is.na(season_period) || season_period <= 1L) {
      stop("ucm_spec$season_period must be > 1 when season = TRUE.")
    }
    if (!is.finite(season_harmonics) || is.na(season_harmonics) || season_harmonics < 1L) {
      stop("ucm_spec$season_harmonics must be >= 1 when season = TRUE.")
    }
  } else {
    season_period <- NA_integer_
    season_harmonics <- 0L
  }

  if (use_cycle) {
    if (!is.finite(cycle_period) || is.na(cycle_period) || cycle_period <= 1L) {
      stop("ucm_spec$cycle_period must be > 1 when cycle = TRUE.")
    }
    if (!is.finite(cycle_harmonics) || is.na(cycle_harmonics) || cycle_harmonics < 1L) {
      stop("ucm_spec$cycle_harmonics must be >= 1 when cycle = TRUE.")
    }
  } else {
    cycle_period <- NA_integer_
    cycle_harmonics <- 0L
  }

  if (use_level) {
    label <- "level"
    if (use_season && use_cycle) {
      label <- "level+season+cycle"
    } else if (use_season) {
      label <- "level+season"
    } else if (use_cycle) {
      label <- "level+cycle"
    }
  } else {
    label <- if (use_season && use_cycle) {
      "fourier+cycle"
    } else if (use_season) {
      "fourier"
    } else {
      "cycle"
    }
  }

  list(
    label = label,
    intercept_mode = 2L,
    use_level = as.integer(use_level),
    use_season = as.integer(use_season),
    use_cycle = as.integer(use_cycle),
    season_period = season_period,
    season_harmonics = season_harmonics,
    cycle_period = cycle_period,
    cycle_harmonics = cycle_harmonics
  )
}


parse_boolish <- function(x, default = FALSE) {
  if (length(x) == 0L || is.null(x) || all(is.na(x))) return(default)
  z <- tolower(trimws(as.character(x[1])))
  if (is.na(z) || !nzchar(z)) return(default)
  if (z %in% c("true", "t", "yes", "y", "1", "on")) return(TRUE)
  if (z %in% c("false", "f", "no", "n", "0", "off")) return(FALSE)
  default
}

standardize_role <- function(x) {
  x <- tolower(trimws(as.character(x)))
  x[is.na(x) | !nzchar(x)] <- "other"
  x[x %in% c("competitor", "competition", "cmp")] <- "comp"
  x[x %in% c("dist", "distro", "distribution_acv")] <- "distribution"
  x[x %in% c("consumer_promo", "promotion")] <- "promo"
  x[x %in% c("reach_frequency", "reach_freq", "rf", "reach/frequency")] <- "reach_frequency"
  x[x %in% c("control", "fixed_effect", "fixed")] <- "control"
  x
}

standardize_row_type_legacy <- function(x) {
  x <- tolower(trimws(as.character(x)))
  x[is.na(x) | !nzchar(x)] <- "variable"
  x[x %in% c("x", "model_variable", "model var", "model-var", "var")] <- "variable"
  x[x %in% c("baseline", "intercept", "state", "ucm_component")] <- "ucm"
  x
}

prepare_metadata_shell <- function(raw) {
  raw <- copy(as.data.table(raw))
  if (!("variable" %in% names(raw))) return(raw)
  raw[, variable := trimws(as.character(variable))]

  if (!("row_type_legacy" %in% names(raw))) raw[, row_type_legacy := "variable"]
  raw[, row_type_legacy := standardize_row_type_legacy(row_type_legacy)]

  # User-facing metadata should use effect_type. Legacy role/variable_type/category are still accepted.
  if ("effect_type" %in% names(raw)) {
    raw[, role := effect_type]
  } else if (!("role" %in% names(raw))) {
    if ("variable_role" %in% names(raw)) {
      raw[, role := variable_role]
    } else if ("variable_type" %in% names(raw)) {
      raw[, role := variable_type]
    } else if ("category" %in% names(raw)) {
      raw[, role := category]
    } else {
      raw[, role := "other"]
    }
  }
  raw[, role := standardize_role(role)]
  raw[, effect_type := role]

  v <- tolower(trimws(as.character(raw$variable)))
  v[is.na(v)] <- ""
  legacy_ucm <- grepl("^_?ucm", v) | grepl("^__ucm", v)
  if ("role" %in% names(raw)) {
    legacy_role <- tolower(trimws(as.character(raw$role)))
    legacy_role[is.na(legacy_role)] <- ""
    legacy_ucm <- legacy_ucm | legacy_role == "ucm"
  }
  raw[legacy_ucm, row_type_legacy := "ucm"]
  raw[]
}

is_ucm_metadata_row <- function(dt) {
  if (!("variable" %in% names(dt))) return(rep(FALSE, nrow(dt)))
  shell <- prepare_metadata_shell(dt)
  role_is_ucm <- if ("role" %in% names(shell)) tolower(trimws(as.character(shell$role))) == "ucm" else rep(FALSE, nrow(shell))
  row_is_ucm <- if ("row_type_legacy" %in% names(shell)) shell$row_type_legacy == "ucm" else rep(FALSE, nrow(shell))
  role_is_ucm | row_is_ucm
}

parse_coef_bound_cell <- function(x, label = "bound", allow_pos_neg = TRUE) {
  x <- trimws(as.character(x %||% ""))
  x[is.na(x)] <- ""
  out <- data.table(
    raw_bound = x,
    lower = rep(-Inf, length(x)),
    upper = rep(Inf, length(x)),
    bound_type = rep("free", length(x))
  )

  lx <- tolower(x)
  pos <- allow_pos_neg & lx == "pos"
  neg <- allow_pos_neg & lx == "neg"
  free <- lx %in% c("", "free", "none", "na", "n/a")

  out[pos, `:=`(lower = 0, upper = Inf, bound_type = "pos")]
  out[neg, `:=`(lower = -Inf, upper = 0, bound_type = "neg")]
  out[free, `:=`(lower = -Inf, upper = Inf, bound_type = "free")]

  paren <- grepl("^\\(.*\\)$", x)
  if (any(paren)) {
    inner <- sub("^\\((.*)\\)$", "\\1", x[paren])
    parts <- strsplit(inner, ",", fixed = TRUE)

    lower <- vapply(parts, function(z) {
      z1 <- trimws(z[1])
      if (identical(z1, "") || is.na(z1)) -Inf else suppressWarnings(as.numeric(z1))
    }, numeric(1))

    upper <- vapply(parts, function(z) {
      z2 <- if (length(z) >= 2) trimws(z[2]) else ""
      if (identical(z2, "") || is.na(z2)) Inf else suppressWarnings(as.numeric(z2))
    }, numeric(1))

    bad <- is.na(lower) | is.na(upper)
    if (any(bad)) stop("Unable to parse one or more ", label, " cells.")

    bt <- ifelse(is.finite(lower) & is.finite(upper), "bounded",
                 ifelse(is.finite(lower) & !is.finite(upper), "lower",
                        ifelse(!is.finite(lower) & is.finite(upper), "upper", "free")))

    out[paren, `:=`(lower = lower, upper = upper, bound_type = bt)]
  }

  unknown <- !(free | pos | neg | paren)
  if (any(unknown)) {
    stop("Unsupported ", label, " value(s): ", paste(unique(x[unknown]), collapse = ", "),
         ". Use blank/free, pos, neg, (lower,upper), (lower,), or (,upper).")
  }

  bad_coef_bound <- out$lower > out$upper
  if (any(bad_coef_bound, na.rm = TRUE)) stop("Some ", label, " cells have lower > upper.")
  out[]
}

parse_curve_bound_cell <- function(x,
                                   default_lower,
                                   default_upper,
                                   label = "curve_bound") {
  b <- parse_coef_bound_cell(x, label = label, allow_pos_neg = TRUE)
  lower <- ifelse(is.finite(b$lower), b$lower, default_lower)
  upper <- ifelse(is.finite(b$upper), b$upper, default_upper)
  bad <- !is.finite(lower) | !is.finite(upper) | lower >= upper
  if (any(bad)) {
    stop(label, " must resolve to finite lower < upper for curve parameters.")
  }
  data.table(lower = lower, upper = upper, bound_type = b$bound_type, raw_bound = b$raw_bound)
}

extract_ucm_config_from_metadata <- function(meta_input,
                                             intercept_type = "fourier",
                                             ucm_spec = NULL,
                                             alpha_mu_prior_mean = 1,
                                             alpha_mu_prior_sd = 0.5,
                                             alpha_sd_prior_sd = 0.25,
                                             level0_mu_prior_mean = 1,
                                             level0_mu_prior_sd = 0.35,
                                             level0_sd_prior_sd = 0.15,
                                             sigma_level_prior_sd = 0.03,
                                             season_sd_prior_sd = 0.10,
                                             cycle_sd_prior_sd = 0.08,
                                             gamma_prior_sd = 0.25,
                                             sigma_y_prior_sd = 0.25) {
  raw <- read_input_table(meta_input)
  raw <- prepare_metadata_shell(raw)
  if (!("variable" %in% names(raw))) {
    return(list(
      intercept_type = intercept_type,
      ucm_spec = ucm_spec,
      alpha_mu_prior_mean = alpha_mu_prior_mean,
      alpha_mu_prior_sd = alpha_mu_prior_sd,
      alpha_sd_prior_sd = alpha_sd_prior_sd,
      level0_mu_prior_mean = level0_mu_prior_mean,
      level0_mu_prior_sd = level0_mu_prior_sd,
      level0_sd_prior_sd = level0_sd_prior_sd,
      sigma_level_prior_sd = sigma_level_prior_sd,
      season_sd_prior_sd = season_sd_prior_sd,
      cycle_sd_prior_sd = cycle_sd_prior_sd,
      gamma_prior_sd = gamma_prior_sd,
      sigma_y_prior_sd = sigma_y_prior_sd,
      ucm_metadata = data.table()
    ))
  }

  u <- copy(raw[is_ucm_metadata_row(raw)])
  if (!nrow(u)) {
    return(list(
      intercept_type = intercept_type,
      ucm_spec = ucm_spec,
      alpha_mu_prior_mean = alpha_mu_prior_mean,
      alpha_mu_prior_sd = alpha_mu_prior_sd,
      alpha_sd_prior_sd = alpha_sd_prior_sd,
      level0_mu_prior_mean = level0_mu_prior_mean,
      level0_mu_prior_sd = level0_mu_prior_sd,
      level0_sd_prior_sd = level0_sd_prior_sd,
      sigma_level_prior_sd = sigma_level_prior_sd,
      season_sd_prior_sd = season_sd_prior_sd,
      cycle_sd_prior_sd = cycle_sd_prior_sd,
      gamma_prior_sd = gamma_prior_sd,
      sigma_y_prior_sd = sigma_y_prior_sd,
      ucm_metadata = data.table()
    ))
  }

  u[, variable := tolower(trimws(as.character(variable)))]
  if (!("enabled" %in% names(u))) u[, enabled := NA]
  if (!("prior" %in% names(u))) u[, prior := NA_real_]
  if (!("precision" %in% names(u))) u[, precision := NA_real_]
  if (!("prior_sd" %in% names(u))) u[, prior_sd := NA_real_]
  if (!("period" %in% names(u))) u[, period := NA_integer_]
  if (!("harmonics" %in% names(u))) u[, harmonics := NA_integer_]

  u[, prior := suppressWarnings(as.numeric(prior))]
  u[, precision := suppressWarnings(as.numeric(precision))]
  u[, prior_sd := suppressWarnings(as.numeric(prior_sd))]
  u[, period := suppressWarnings(as.integer(period))]
  u[, harmonics := suppressWarnings(as.integer(harmonics))]

  get_row <- function(pattern) {
    idx <- grep(pattern, u$variable)
    if (length(idx)) u[idx[1]] else u[0]
  }
  get_bool <- function(pattern, default) {
    row <- get_row(pattern)
    if (!nrow(row)) return(default)
    candidates <- intersect(c("enabled", "use", "include", "value"), names(row))
    for (cc in candidates) {
      val <- row[[cc]][1]
      if (!is.na(val) && nzchar(trimws(as.character(val)))) return(parse_boolish(val, default))
    }
    default
  }
  get_num <- function(pattern, col, default) {
    row <- get_row(pattern)
    if (!nrow(row) || !(col %in% names(row))) return(default)
    val <- suppressWarnings(as.numeric(row[[col]][1]))
    if (is.finite(val) && !is.na(val)) val else default
  }
  get_prior_center <- function(pattern, default) {
    row <- get_row(pattern)
    if (!nrow(row)) return(default)
    val <- suppressWarnings(as.numeric(row$prior[1]))
    if (is.finite(val) && !is.na(val)) val else default
  }
  get_prior_sd <- function(pattern, default) {
    row <- get_row(pattern)
    if (!nrow(row)) return(default)
    val <- suppressWarnings(as.numeric(row$precision[1]))
    if (is.finite(val) && !is.na(val) && val > 0) return(1 / sqrt(val))
    val <- suppressWarnings(as.numeric(row$prior_sd[1]))
    if (is.finite(val) && !is.na(val) && val > 0) return(val)
    default
  }
  get_prior_scale <- function(pattern, default) {
    row <- get_row(pattern)
    if (!nrow(row)) return(default)
    val <- suppressWarnings(as.numeric(row$prior[1]))
    if (is.finite(val) && !is.na(val) && val > 0) return(val)
    val <- suppressWarnings(as.numeric(row$precision[1]))
    if (is.finite(val) && !is.na(val) && val > 0) return(1 / sqrt(val))
    val <- suppressWarnings(as.numeric(row$prior_sd[1]))
    if (is.finite(val) && !is.na(val) && val > 0) return(val)
    default
  }

  default_spec <- modifyList(list(
    level = TRUE,
    season = TRUE,
    cycle = FALSE,
    season_period = 52L,
    season_harmonics = 2L,
    cycle_period = 104L,
    cycle_harmonics = 1L
  ), ucm_spec %||% list())

  default_spec$level <- get_bool("^ucm_level$|^level$|level_innov|level_sigma|sigma_level", isTRUE(default_spec$level))
  default_spec$season <- get_bool("^ucm_season$|^season$|season", isTRUE(default_spec$season))
  default_spec$cycle <- get_bool("^ucm_cycle$|^cycle$|cycle", isTRUE(default_spec$cycle))
  default_spec$season_period <- as.integer(get_num("^ucm_season$|^season$|season", "period", default_spec$season_period %||% 52L))
  default_spec$season_harmonics <- as.integer(get_num("^ucm_season$|^season$|season", "harmonics", default_spec$season_harmonics %||% 2L))
  default_spec$cycle_period <- as.integer(get_num("^ucm_cycle$|^cycle$|cycle", "period", default_spec$cycle_period %||% 104L))
  default_spec$cycle_harmonics <- as.integer(get_num("^ucm_cycle$|^cycle$|cycle", "harmonics", default_spec$cycle_harmonics %||% 1L))

  level0_mu_prior_mean <- get_prior_center("^ucm_level0$|^level0$|initial", level0_mu_prior_mean)
  level0_mu_prior_sd <- get_prior_sd("^ucm_level0$|^level0$|initial", level0_mu_prior_sd)
  level0_sd_prior_sd <- get_prior_scale("^ucm_group_level$|^group_level$|level_group|level_sd|level0_sd", level0_sd_prior_sd)
  sigma_level_prior_sd <- get_prior_scale("^ucm_level$|^level$|level_innov|level_sigma|sigma_level", sigma_level_prior_sd)

  season_sd_prior_sd <- get_prior_scale("^ucm_season$|^season$|season", season_sd_prior_sd)
  cycle_sd_prior_sd <- get_prior_scale("^ucm_cycle$|^cycle$|cycle", cycle_sd_prior_sd)

  alpha_mu_prior_mean <- get_prior_center("^ucm_flat$|^flat$|alpha", alpha_mu_prior_mean)
  alpha_mu_prior_sd <- get_prior_sd("^ucm_flat$|^flat$|alpha", alpha_mu_prior_sd)
  alpha_sd_prior_sd <- get_prior_scale("^ucm_alpha_group$|alpha_group|flat_group", alpha_sd_prior_sd)

  sigma_y_prior_sd <- get_prior_scale("^ucm_noise$|^noise$|sigma_y|residual", sigma_y_prior_sd)
  gamma_prior_sd <- get_prior_scale("^ucm_gamma$|gamma|extra_control", gamma_prior_sd)

  list(
    intercept_type = "ucm",
    ucm_spec = default_spec,
    alpha_mu_prior_mean = alpha_mu_prior_mean,
    alpha_mu_prior_sd = alpha_mu_prior_sd,
    alpha_sd_prior_sd = alpha_sd_prior_sd,
    level0_mu_prior_mean = level0_mu_prior_mean,
    level0_mu_prior_sd = level0_mu_prior_sd,
    level0_sd_prior_sd = level0_sd_prior_sd,
    sigma_level_prior_sd = sigma_level_prior_sd,
    season_sd_prior_sd = season_sd_prior_sd,
    cycle_sd_prior_sd = cycle_sd_prior_sd,
    gamma_prior_sd = gamma_prior_sd,
    sigma_y_prior_sd = sigma_y_prior_sd,
    ucm_metadata = u
  )
}
