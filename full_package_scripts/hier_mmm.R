inv_logit_bounded <- function(x, lower, upper, eps = 1e-6) {
  p <- (x - lower) / (upper - lower)
  p <- pmin(pmax(p, eps), 1 - eps)
  qlogis(p)
}

raw_sd_from_original_sd <- function(mu, sd, lower, upper, floor = 0.15, cap = 2.5) {
  width <- pmax(upper - lower, 1e-6)
  p <- pmin(pmax((mu - lower) / width, 1e-4), 1 - 1e-4)
  deriv <- width * p * (1 - p)
  out <- sd / deriv
  out[!is.finite(out) | is.na(out)] <- floor
  pmin(pmax(out, floor), cap)
}

lognormal_from_mean_sd <- function(mean_abs, sd_abs, eps = 1e-6) {
  m <- pmax(abs(mean_abs), eps)
  s <- pmax(sd_abs, eps)
  sigma2 <- log1p((s^2) / (m^2))
  sdlog <- sqrt(sigma2)
  meanlog <- log(m) - 0.5 * sigma2
  list(meanlog = meanlog, sdlog = sdlog)
}

softplus <- function(x) ifelse(x > 30, x, log1p(exp(x)))

softplus_inv <- function(y, eps = 1e-8) {
  y <- pmax(as.numeric(y), eps)
  ifelse(y > 30, y, log(expm1(y)))
}

softplus_from_mean_sd <- function(mean_abs, sd_abs, floor = 0.05, cap = 2.5, eps = 1e-6) {
  m <- pmax(abs(mean_abs), eps)
  s <- pmax(as.numeric(sd_abs), eps)
  raw_mu <- softplus_inv(m, eps = eps)
  deriv <- plogis(raw_mu)
  raw_sd <- s / pmax(deriv, 1e-4)
  raw_sd[!is.finite(raw_sd) | is.na(raw_sd)] <- floor
  list(raw_mu = raw_mu, raw_sd = pmin(pmax(raw_sd, floor), cap))
}

clip_to_open_interval <- function(x, lower, upper, eps_frac = 1e-4) {
  width <- pmax(upper - lower, 1e-8)
  eps <- width * eps_frac
  pmin(pmax(x, lower + eps), upper - eps)
}

