  )
}


initial_beta_from_stan_data <- function(sd) {
  G <- sd$G; J <- sd$J
  beta <- matrix(0, nrow = G, ncol = J)

  if (sd$J_pos > 0L) {
    for (k in seq_len(sd$J_pos)) beta[, sd$pos_idx[k]] <- softplus(sd$coef_mu_pos_log[k])
  }
  if (sd$J_neg > 0L) {
    for (k in seq_len(sd$J_neg)) beta[, sd$neg_idx[k]] <- -softplus(sd$coef_mu_neg_log[k])
  }
  if (sd$J_lower > 0L) {
    for (k in seq_len(sd$J_lower)) beta[, sd$lower_idx[k]] <- sd$lower_only_lower[k] + softplus(sd$coef_mu_lower_log[k])
  }
  if (sd$J_upper > 0L) {
    for (k in seq_len(sd$J_upper)) beta[, sd$upper_idx[k]] <- sd$upper_only_upper[k] - softplus(sd$coef_mu_upper_log[k])
  }
  if (sd$J_bounded > 0L) {
    for (k in seq_len(sd$J_bounded)) {
      p <- plogis(sd$coef_raw_mu_bounded[k])
      beta[, sd$bounded_idx[k]] <- sd$bounded_lower[k] + (sd$bounded_upper[k] - sd$bounded_lower[k]) * p
    }
  }
  if (sd$J_free > 0L) {
    for (k in seq_len(sd$J_free)) beta[, sd$free_idx[k]] <- sd$coef_mu_free[k]
  }
  beta
}

prior_non_intercept_mu <- function(sd, beta = NULL) {
  beta <- beta %||% initial_beta_from_stan_data(sd)
  mu <- rep(0, sd$N)

  if (sd$J_linear > 0L) {
    for (n in seq_len(sd$N)) {
      g <- sd$group_id[n]
      x_val <- sd$X[n, sd$linear_idx]
      if (isTRUE(sd$center_predictors_for_sampling == 1L)) {
        x_val <- x_val - sd$X_center_mean[g, sd$linear_idx]
      }
      mu[n] <- mu[n] + sum(beta[g, sd$linear_idx] * x_val)
    }
  }

  if (sd$J_curve > 0L) {
    for (g in seq_len(sd$G)) {
      idx <- sd$start_idx[g]:sd$end_idx[g]
      for (k in seq_len(sd$J_curve)) {
        j <- sd$curve_idx[k]
        x <- pmax(sd$X[idx, j], 0)
        rr <- sd$rrate_lower[k] + (sd$rrate_upper[k] - sd$rrate_lower[k]) * plogis(sd$rrate_raw_mu[k])
        cv <- sd$cvalue_lower[k] + (sd$cvalue_upper[k] - sd$cvalue_lower[k]) * plogis(sd$cvalue_raw_mu[k])
        dv <- sd$dvalue_lower[k] + (sd$dvalue_upper[k] - sd$dvalue_lower[k]) * plogis(sd$dvalue_raw_mu[k])
        curve_type <- if (!is.null(sd$curve_type) && length(sd$curve_type) >= k && sd$curve_type[k] == 2L) "hill" else "weibull"
        train_mask <- if (!is.null(sd$is_train)) sd$is_train[idx] == 1L else rep(TRUE, length(idx))
        tr <- media_transform_hier_mmm(
          x = x,
          rrate = rr,
          cvalue = cv,
          dvalue = dv,
          curve_type = curve_type,
          train_mask = train_mask,
          normalize_curve_x = isTRUE(sd$normalize_curve_x == 1L),
          multiplier = 1
        )
        trans <- tr$transformed
        center_value <- if (isTRUE(sd$center_predictors_for_sampling == 1L)) tr$center_value else 0
        mu[idx] <- mu[idx] + beta[g, j] * (trans - center_value)
      }
    }
  }

  mu
}

build_ucm_warm_start_init <- function(prep,
                                      chains = 4L,
                                      seed = 123L,
                                      smooth_window = 7L,
                                      jitter = 0.01) {
  sd <- prep$stan_data
  had_seed <- exists(".Random.seed", envir = .GlobalEnv, inherits = FALSE)
  old_seed <- if (had_seed) get(".Random.seed", envir = .GlobalEnv, inherits = FALSE) else NULL
  on.exit({
    if (had_seed) assign(".Random.seed", old_seed, envir = .GlobalEnv)
    else if (exists(".Random.seed", envir = .GlobalEnv, inherits = FALSE)) rm(".Random.seed", envir = .GlobalEnv)
  }, add = TRUE)
  set.seed(seed)
  beta0 <- initial_beta_from_stan_data(sd)
  mu_non_intercept <- prior_non_intercept_mu(sd, beta = beta0)
  base_target <- as.numeric(sd$y - mu_non_intercept)
  base_target[!is.finite(base_target)] <- mean(base_target[is.finite(base_target)], na.rm = TRUE)

  level0_mu_init <- mean(base_target, na.rm = TRUE)
  level0_sd_init <- 0.02
  level0_raw_init <- rep(0, sd$G)
  sigma_level_init <- min(max(0.005, safe_sd(diff(base_target), floor = 0.005)), max(sd$sigma_level_prior_sd * 0.80, 0.005))
  level_innov_init <- rep(0, sd$N_state_innov)
  alpha_z_init <- rep(0, sd$G)
  alpha_sd_init <- max(level0_sd_init, 0.01)
  group_means <- NULL
  smooth_by_group <- NULL

  if (sd$intercept_mode == 1L) {
    group_means <- vapply(seq_len(sd$G), function(g) mean(base_target[sd$start_idx[g]:sd$end_idx[g]], na.rm = TRUE), numeric(1))
    level0_mu_init <- mean(group_means, na.rm = TRUE)
    alpha_sd_init <- max(safe_sd(group_means, floor = 0.01), 0.01)
    alpha_z_init <- clip_numeric((group_means - level0_mu_init) / alpha_sd_init, -2, 2)
  } else if (sd$intercept_mode == 2L && sd$intercept_use_level == 1L) {
    smooth_by_group <- vector("list", sd$G)
    start_vals <- numeric(sd$G)
    all_diffs <- numeric()
    for (g in seq_len(sd$G)) {
      idx <- sd$start_idx[g]:sd$end_idx[g]
      sm <- smooth_vector(base_target[idx], window = smooth_window)
      smooth_by_group[[g]] <- sm
      start_vals[g] <- sm[1]
      if (length(sm) > 1L) all_diffs <- c(all_diffs, diff(sm))
    }
    level0_mu_init <- mean(start_vals, na.rm = TRUE)
    level0_sd_init <- max(safe_sd(start_vals, floor = 0.02), 0.02)
    level0_raw_init <- clip_numeric((start_vals - level0_mu_init) / level0_sd_init, -2.5, 2.5)
    sigma_level_init <- min(max(safe_sd(all_diffs, floor = 0.005), 0.005), max(sd$sigma_level_prior_sd * 0.80, 0.005))

    if (sd$N_state_innov > 0L) {
      pos <- 1L
      for (g in seq_len(sd$G)) {
        sm <- smooth_by_group[[g]]
        if (length(sm) > 1L) {
          vals <- clip_numeric(diff(sm) / sigma_level_init, -2.5, 2.5)
          level_innov_init[pos:(pos + length(vals) - 1L)] <- vals
          pos <- pos + length(vals)
        }
      }
    }
  }

  level_path_init <- rep(level0_mu_init, sd$N)
  if (sd$intercept_mode == 1L && !is.null(group_means)) {
    for (g in seq_len(sd$G)) level_path_init[sd$start_idx[g]:sd$end_idx[g]] <- group_means[g]
  } else if (sd$intercept_mode == 2L && !is.null(smooth_by_group)) {
    for (g in seq_len(sd$G)) level_path_init[sd$start_idx[g]:sd$end_idx[g]] <- smooth_by_group[[g]]
  }

  season_coef_init <- matrix(0, nrow = sd$G, ncol = sd$K_season)
  cycle_coef_init <- matrix(0, nrow = sd$G, ncol = sd$K_cycle)
  if (sd$intercept_mode == 2L && (sd$K_season + sd$K_cycle) > 0L) {
    for (g in seq_len(sd$G)) {
      idx <- sd$start_idx[g]:sd$end_idx[g]
      y_res <- base_target[idx] - level_path_init[idx]
      mats <- list()
        if (sd$K_season > 0L) mats[[length(mats) + 1L]] <- sd$X_season[idx, , drop = FALSE]
        if (sd$K_cycle > 0L) mats[[length(mats) + 1L]] <- sd$X_cycle[idx, , drop = FALSE]
      A <- if (length(mats)) do.call(cbind, mats) else matrix(0, length(idx), 0)
      if (ncol(A) > 0L && nrow(A) >= ncol(A)) {
        coef_hat <- tryCatch(
          as.numeric(solve(crossprod(A) + diag(1e-4, ncol(A)), crossprod(A, y_res))),
          error = function(e) rep(0, ncol(A))
        )
        if (sd$K_season > 0L) season_coef_init[g, ] <- coef_hat[seq_len(sd$K_season)]
        if (sd$K_cycle > 0L) cycle_coef_init[g, ] <- coef_hat[(sd$K_season + 1L):(sd$K_season + sd$K_cycle)]
      }
    }
  }

  make_component_init <- function(coef_mat, prior_sd) {
    if (!ncol(coef_mat)) return(list(sd = numeric(), raw = coef_mat))
    sc <- apply(abs(coef_mat), 2, function(z) {
      z <- z[is.finite(z)]
      if (!length(z)) return(0.005)
      max(stats::median(z[z > 0], na.rm = TRUE), safe_sd(z, floor = 0.005), 0.005, na.rm = TRUE)
    })
    sc[!is.finite(sc) | is.na(sc)] <- 0.005
    sc <- pmin(pmax(sc, 0.005), max(prior_sd * 0.80, 0.005))
    raw <- sweep(coef_mat, 2, sc, "/")
    raw[!is.finite(raw) | is.na(raw)] <- 0
    raw <- clip_numeric(raw, -3, 3)
    list(sd = sc, raw = raw)
  }

  season_init <- make_component_init(season_coef_init, sd$season_sd_prior_sd)
  cycle_init <- make_component_init(cycle_coef_init, sd$cycle_sd_prior_sd)

  ucm_init_mu <- level_path_init
  if (sd$K_season > 0L) {
    for (g in seq_len(sd$G)) {
      idx <- sd$start_idx[g]:sd$end_idx[g]
      ucm_init_mu[idx] <- ucm_init_mu[idx] + as.numeric(sd$X_season[idx, , drop = FALSE] %*% season_coef_init[g, ])
    }
  }
  if (sd$K_cycle > 0L) {
    for (g in seq_len(sd$G)) {
      idx <- sd$start_idx[g]:sd$end_idx[g]
      ucm_init_mu[idx] <- ucm_init_mu[idx] + as.numeric(sd$X_cycle[idx, , drop = FALSE] %*% cycle_coef_init[g, ])
    }
  }

  residual_after_init <- base_target - ucm_init_mu
  sigma_y_init <- max(min(safe_sd(residual_after_init, floor = 0.05), sd$sigma_y_prior_sd * 2), sd$sigma_y_floor %||% 0.005)
  alpha_centered_init <- if (!is.null(group_means)) {
    as.numeric(group_means)
  } else {
    rep(level0_mu_init, sd$G)
  }
  if (isTRUE(sd$alpha_parameterization == 2L)) {
    alpha_z_init <- alpha_centered_init
  } else if (isTRUE(sd$alpha_parameterization == 3L)) {
    alpha_z_init <- numeric()
  }

  base_init <- list(
    rrate_raw = as.numeric(sd$rrate_raw_mu[sd$curve_sampled_pos]),
    cvalue_raw = as.numeric(sd$cvalue_raw_mu[sd$curve_sampled_pos]),
    dvalue_raw = as.numeric(sd$dvalue_raw_mu[sd$curve_sampled_pos]),

    mu_log_pos = as.numeric(sd$coef_mu_pos_log),
    tau_pos = pmax(as.numeric(sd$tau_scale_pos[sd$pos_hier_pos]) * 0.25, 0.01),
    z_pos = matrix(0, nrow = sd$G, ncol = sd$J_pos_hier),

    mu_log_neg = as.numeric(sd$coef_mu_neg_log),
    tau_neg = pmax(as.numeric(sd$tau_scale_neg[sd$neg_hier_pos]) * 0.25, 0.01),
    z_neg = matrix(0, nrow = sd$G, ncol = sd$J_neg_hier),

    mu_log_lower = as.numeric(sd$coef_mu_lower_log),
    tau_lower = pmax(as.numeric(sd$tau_scale_lower[sd$lower_hier_pos]) * 0.25, 0.01),
    z_lower = matrix(0, nrow = sd$G, ncol = sd$J_lower_hier),

    mu_log_upper = as.numeric(sd$coef_mu_upper_log),
    tau_upper = pmax(as.numeric(sd$tau_scale_upper[sd$upper_hier_pos]) * 0.25, 0.01),
    z_upper = matrix(0, nrow = sd$G, ncol = sd$J_upper_hier),

    mu_raw_bounded = as.numeric(sd$coef_raw_mu_bounded),
    tau_bounded = pmax(as.numeric(sd$tau_scale_bounded[sd$bounded_hier_pos]) * 0.25, 0.01),
    z_bounded = matrix(0, nrow = sd$G, ncol = sd$J_bounded_hier),

    mu_free = as.numeric(sd$coef_mu_free),
    tau_free = pmax(as.numeric(sd$tau_scale_free[sd$free_hier_pos]) * 0.25, 0.01),
    z_free = matrix(0, nrow = sd$G, ncol = sd$J_free_hier),

    alpha_mu = level0_mu_init,
    alpha_sd = alpha_sd_init,
    alpha_z = alpha_z_init,

    level0_mu = level0_mu_init,
    level0_sd = level0_sd_init,
    level0_raw = level0_raw_init,
    sigma_level = sigma_level_init,
    level_innov_raw = level_innov_init,

    season_sd = season_init$sd,
    season_raw = season_init$raw,
    cycle_sd = cycle_init$sd,
    cycle_raw = cycle_init$raw,

    gamma = rep(0, sd$K_extra),
    sigma_y = sigma_y_init
  )

  if (isTRUE(sd$ucm_parameterization == 2L)) {
    if (sd$intercept_mode == 2L && sd$intercept_use_level == 1L && !is.null(smooth_by_group)) {
      base_init$level0_raw <- vapply(smooth_by_group, function(z) z[1], numeric(1))
      if (sd$N_state_innov > 0L) {
        pos <- 1L
        for (g in seq_len(sd$G)) {
          sm <- smooth_by_group[[g]]
          if (length(sm) > 1L) {
            vals <- clip_numeric(diff(sm), -2.5, 2.5)
            base_init$level_innov_raw[pos:(pos + length(vals) - 1L)] <- vals
            pos <- pos + length(vals)
          }
        }
      }
    }
    if (sd$K_season > 0L) base_init$season_raw <- season_coef_init
    if (sd$K_cycle > 0L) base_init$cycle_raw <- cycle_coef_init
  }

  if (sd$J_pos_hier > 0L && length(sd$coef_centered_pos)) {
    for (h in seq_len(sd$J_pos_hier)) {
      k <- sd$pos_hier_pos[h]
      if (isTRUE(sd$coef_centered_pos[k] == 1L)) {
        base_init$z_pos[, h] <- base_init$mu_log_pos[k]
      }
    }
  }
  if (sd$J_neg_hier > 0L && length(sd$coef_centered_neg)) {
    for (h in seq_len(sd$J_neg_hier)) {
      k <- sd$neg_hier_pos[h]
      if (isTRUE(sd$coef_centered_neg[k] == 1L)) {
        base_init$z_neg[, h] <- base_init$mu_log_neg[k]
      }
    }
  }
  if (sd$J_lower_hier > 0L && length(sd$coef_centered_lower)) {
    for (h in seq_len(sd$J_lower_hier)) {
      k <- sd$lower_hier_pos[h]
      if (isTRUE(sd$coef_centered_lower[k] == 1L)) {
        base_init$z_lower[, h] <- base_init$mu_log_lower[k]
      }
    }
  }
  if (sd$J_upper_hier > 0L && length(sd$coef_centered_upper)) {
    for (h in seq_len(sd$J_upper_hier)) {
      k <- sd$upper_hier_pos[h]
      if (isTRUE(sd$coef_centered_upper[k] == 1L)) {
        base_init$z_upper[, h] <- base_init$mu_log_upper[k]
      }
    }
  }
  if (sd$J_bounded_hier > 0L && length(sd$coef_centered_bounded)) {
    for (h in seq_len(sd$J_bounded_hier)) {
      k <- sd$bounded_hier_pos[h]
      if (isTRUE(sd$coef_centered_bounded[k] == 1L)) {
        base_init$z_bounded[, h] <- base_init$mu_raw_bounded[k]
      }
    }
  }
  if (sd$J_free_hier > 0L && length(sd$coef_centered_free)) {
    for (h in seq_len(sd$J_free_hier)) {
      k <- sd$free_hier_pos[h]
      if (isTRUE(sd$coef_centered_free[k] == 1L)) {
        base_init$z_free[, h] <- base_init$mu_free[k]
      }
    }
  }

  make_chain <- function() {
    z <- base_init
    scalar_names <- c("alpha_mu", "alpha_sd", "level0_mu", "level0_sd", "sigma_level", "sigma_y")
    for (nm in scalar_names) z[[nm]] <- pmax(z[[nm]] * (1 + stats::rnorm(1, 0, jitter)), 1e-6)
    z$alpha_mu <- base_init$alpha_mu + stats::rnorm(1, 0, jitter)
    z$level0_mu <- base_init$level0_mu + stats::rnorm(1, 0, jitter)
    if (length(z$alpha_z)) {
      z$alpha_z <- base_init$alpha_z + stats::rnorm(length(base_init$alpha_z), 0, jitter)
    }
    z[vapply(z, function(v) length(v) > 0L, logical(1))]
  }

  replicate(as.integer(chains), make_chain(), simplify = FALSE)
}


extract_posterior_means_hier_mmm <- function(fit, prep) {
  require_hier_mmm_pkg("posterior", "extracting Stan posterior means")

  sm <- summarize_fit_vars(
    fit,
    variables = c(
      "beta", "rrate", "cvalue", "dvalue", "alpha_flat", "gamma",
      "level_component", "cycle_component", "season_component", "level_state", "sigma_y"
    )
  )
  if (!all(c("variable", "mean") %in% names(sm))) {
    stop("CmdStan summary did not return expected columns 'variable' and 'mean'.")
  }

  sm_lookup <- setNames(sm$mean, sm$variable)
  get_mean <- function(name, default = 0) {
    v <- sm_lookup[[name]]
    if (!is.null(v) && is.finite(v)) as.numeric(v) else default
  }

  J <- prep$stan_data$J
  G <- prep$stan_data$G
  K_extra <- prep$stan_data$K_extra

  beta <- matrix(0, nrow = G, ncol = J)
  for (g in seq_len(G)) {
    for (j in seq_len(J)) {
      beta[g, j] <- get_mean(sprintf("beta[%d,%d]", g, j), 0)
    }
  }

  rrate <- rep(0, J)
  cvalue <- rep(0, J)
  dvalue <- rep(1, J)
  if (prep$stan_data$J_curve > 0L) {
    for (k in seq_len(prep$stan_data$J_curve)) {
      j <- prep$stan_data$curve_idx[k]
      rrate[j] <- get_mean(sprintf("rrate[%d]", k), 0)
      cvalue[j] <- get_mean(sprintf("cvalue[%d]", k), 0)
      dvalue[j] <- get_mean(sprintf("dvalue[%d]", k), 1)
    }
  }

  alpha_flat <- vapply(seq_len(G), function(g) get_mean(sprintf("alpha_flat[%d]", g), 0), numeric(1))
  gamma <- if (K_extra > 0) vapply(seq_len(K_extra), function(k) get_mean(sprintf("gamma[%d]", k), 0), numeric(1)) else numeric()
  level_component <- vapply(seq_len(prep$stan_data$N), function(i) get_mean(sprintf("level_component[%d]", i), 0), numeric(1))
  cycle_component <- vapply(seq_len(prep$stan_data$N), function(i) get_mean(sprintf("cycle_component[%d]", i), 0), numeric(1))
  season_component <- vapply(seq_len(prep$stan_data$N), function(i) get_mean(sprintf("season_component[%d]", i), 0), numeric(1))
  level_state <- vapply(seq_len(prep$stan_data$N), function(i) get_mean(sprintf("level_state[%d]", i), 0), numeric(1))
  sigma_y <- get_mean("sigma_y", NA_real_)

  list(
    beta = beta,
    rrate = rrate,
    cvalue = cvalue,
    dvalue = dvalue,
    alpha_flat = alpha_flat,
    gamma = gamma,
    level_component = level_component,
    cycle_component = cycle_component,
    season_component = season_component,
    level_state = level_state,
    sigma_y = sigma_y
  )
}


build_outputs_hier_mmm <- function(fit, prep, extra_control_cols = NULL) {
  pm <- extract_posterior_means_hier_mmm(fit, prep)
  dt <- copy(prep$data)
  vl <- copy(prep$variable_lookup)[order(variable_idx)]
  if (!"curve_type" %in% names(vl)) vl[, curve_type := "weibull"]
  gl <- copy(prep$group_lookup)[order(group_idx)]
  var_names <- vl$variable
  curve_vars <- vl[has_curve == 1L, variable]
  linear_vars <- vl[has_curve == 0L, variable]

  dt[, row_id := .I]
  dt[, sample := fifelse(is_holdout__, "holdout", "train")]

  # coefficient outputs are kept on MODEL SCALE (before decomp rescaling)
  coef_long <- as.data.table(expand.grid(group_idx = gl$group_idx, variable_idx = vl$variable_idx, KEEP.OUT.ATTRS = FALSE))
  coef_long[, coef := as.numeric(pm$beta[cbind(group_idx, variable_idx)])]
  coef_long[gl, on = "group_idx", `:=`(
    group_value = i.group_value,
    model_id = i.group_value,
    mod_id = i.group_value,
    target_entity = i.target_entity
  )]
  coef_long[vl, on = "variable_idx", `:=`(variable = i.variable, source_entity = i.source_entity, role = i.role, has_curve = i.has_curve)]
  setcolorder(coef_long, c("group_value", "model_id", "mod_id", "target_entity", "variable", "source_entity", "role", "has_curve", "coef"))
  coef_by_group <- dcast(coef_long, group_value + model_id + target_entity ~ variable, value.var = "coef")
  coef_by_mod_id <- dcast(coef_long, mod_id + target_entity ~ variable, value.var = "coef")

  contrib_cols <- character()
  center_predictors <- isTRUE(prep$center_predictors_for_sampling) &&
    !is.null(prep$stan_data$X_center_mean) &&
    nrow(prep$stan_data$X_center_mean) == prep$stan_data$G
  center_offset_model <- numeric(nrow(dt))

  # intercept contribution on model scale
  if (prep$intercept_spec == "flat") {
    dt[, intercept := pm$alpha_flat[group_idx]]
    contrib_cols <- c(contrib_cols, "intercept")
  } else if (isTRUE(prep$intercept_use_level == 1L) || isTRUE(prep$intercept_use_cycle == 1L) || isTRUE(prep$intercept_use_season == 1L)) {
    dt[, intercept_level := pm$level_component]
    if (isTRUE(prep$intercept_use_level == 1L)) {
      dt[, intercept_flat := 0]
    } else {
      dt[, intercept_flat := pm$alpha_flat[group_idx]]
    }
    dt[, intercept_cycle := pm$cycle_component]
    dt[, intercept_seasonality := pm$season_component]
    dt[, intercept_total := intercept_flat + intercept_level + intercept_cycle + intercept_seasonality]
    if (isTRUE(prep$intercept_use_level == 1L)) contrib_cols <- c(contrib_cols, "intercept_level") else contrib_cols <- c(contrib_cols, "intercept_flat")
    if (isTRUE(prep$intercept_use_cycle == 1L)) contrib_cols <- c(contrib_cols, "intercept_cycle")
    if (isTRUE(prep$intercept_use_season == 1L)) contrib_cols <- c(contrib_cols, "intercept_seasonality")
  } else {
    dt[, intercept := 0]
    contrib_cols <- c(contrib_cols, "intercept")
  }

  # extra controls on model scale
  if (length(extra_control_cols)) {
    used_extra <- extra_control_cols[extra_control_cols %in% names(dt)]
    for (k in seq_along(used_extra)) {
      nm <- used_extra[k]
      dt[, (nm) := get(nm) * pm$gamma[k]]
    }
    contrib_cols <- c(contrib_cols, used_extra)
  }

  # linear variables on model scale
  if (length(linear_vars)) {
    for (v in linear_vars) {
      j <- vl[variable == v, variable_idx]
      beta_vec <- pm$beta[cbind(dt$group_idx, j)]
      if (isTRUE(center_predictors)) {
        center_offset_model <- center_offset_model + beta_vec * prep$stan_data$X_center_mean[dt$group_idx, j]
      }
      dt[, (v) := get(v) * beta_vec]
    }
    contrib_cols <- c(contrib_cols, linear_vars)
  }

  # curved variables on model scale
  if (length(curve_vars)) {
    for (v in curve_vars) {
      j <- vl[variable == v, variable_idx]
      rr <- pm$rrate[j]
      cv <- pm$cvalue[j]
      dv <- pm$dvalue[j]
      curve_type <- vl[variable == v, curve_type][1] %||% "weibull"
      tmp <- numeric(nrow(dt))
      for (g in gl$group_idx) {
        idx <- which(dt$group_idx == g)
        tr <- media_transform_hier_mmm(
          x = dt[[v]][idx],
          rrate = rr,
          cvalue = cv,
          dvalue = dv,
          curve_type = curve_type,
          train_mask = !dt$is_holdout__[idx],
          normalize_curve_x = prep$normalize_curve_x,
          multiplier = 1
        )
        trans_vec <- tr$transformed
        center_value <- tr$center_value
        if (isTRUE(center_predictors)) {
          center_offset_model[idx] <- center_offset_model[idx] + pm$beta[g, j] * center_value
        }
        tmp[idx] <- pm$beta[g, j] * trans_vec
      }
      dt[, (v) := tmp]
    }
    contrib_cols <- c(contrib_cols, curve_vars)
  }

  contrib_cols <- unique(contrib_cols[contrib_cols %in% names(dt)])
  reporting_cols <- intersect("intercept_total", names(dt))

  if (isTRUE(center_predictors) && any(abs(center_offset_model) > 1e-12, na.rm = TRUE)) {
    if ("intercept" %in% contrib_cols) {
      dt[, intercept := intercept - center_offset_model]
    } else if ("intercept_flat" %in% contrib_cols) {
      dt[, intercept_flat := intercept_flat - center_offset_model]
    } else if ("intercept_level" %in% contrib_cols) {
      dt[, intercept_level := intercept_level - center_offset_model]
    }
    if ("intercept_total" %in% names(dt)) {
      dt[, intercept_total := 0]
      if ("intercept_flat" %in% names(dt)) dt[, intercept_total := intercept_total + intercept_flat]
      if ("intercept_level" %in% names(dt)) dt[, intercept_total := intercept_total + intercept_level]
      if ("intercept_cycle" %in% names(dt)) dt[, intercept_total := intercept_total + intercept_cycle]
      if ("intercept_seasonality" %in% names(dt)) dt[, intercept_total := intercept_total + intercept_seasonality]
    }
  }

  dt[, pred_model_scale := rowSums(.SD), .SDcols = contrib_cols]
  dt[, y_model_scale := y_model__]
  dt[, residual_model_scale := y_model_scale - pred_model_scale]

  # rescale decomposition back to original units when possible
  scale_cols <- unique(c(contrib_cols, reporting_cols, "pred_model_scale", "residual_model_scale"))
  for (nm in scale_cols) {
    dt[, (paste0(nm, "__orig")) := get(nm) * rescale_factor__]
  }
  dt[, y_actual := fifelse(is.finite(y_original__), y_original__, y_model_scale * rescale_factor__)]
  dt[, pred := pred_model_scale__orig]
  dt[, residual := residual_model_scale__orig]
  dt[, rescale_factor := rescale_factor__]

  # move original-scale contribution/reporting columns back onto the original names for export
  for (nm in unique(c(contrib_cols, reporting_cols))) {
    dt[, (nm) := get(paste0(nm, "__orig"))]
  }

  # Posterior prediction intervals for fitted mean y_hat, rescaled to original units.
  yhat_sm <- tryCatch(summarize_fit_vars(fit, variables = "y_hat"), error = function(e) data.table())
  if (nrow(yhat_sm) && "variable" %in% names(yhat_sm)) {
    q05_col <- pick_first_existing_col(yhat_sm, c("q5", "q05", "5%", "5.0%", "q_5"))
    q50_col <- pick_first_existing_col(yhat_sm, c("median", "q50", "50%", "50.0%", "q_50"))
    q95_col <- pick_first_existing_col(yhat_sm, c("q95", "95%", "95.0%", "q_95"))
    yhat_idx <- suppressWarnings(as.integer(sub("^y_hat\\[([0-9]+)\\]$", "\\1", yhat_sm$variable)))
    ok <- is.finite(yhat_idx) & yhat_idx >= 1L & yhat_idx <= nrow(dt)
    if (any(ok)) {
      yhat_sm <- yhat_sm[ok]
      yhat_sm[, row_id := yhat_idx[ok]]
      yhat_tmp <- yhat_sm[, .(
        row_id,
        pred_q05_model__ = if (!is.na(q05_col)) get(q05_col) else NA_real_,
        pred_q50_model__ = if (!is.na(q50_col)) get(q50_col) else NA_real_,
        pred_q95_model__ = if (!is.na(q95_col)) get(q95_col) else NA_real_
      )]
      dt[yhat_tmp, `:=`(
        pred_q05 = i.pred_q05_model__ * rescale_factor__,
        pred_q50 = i.pred_q50_model__ * rescale_factor__,
        pred_q95 = i.pred_q95_model__ * rescale_factor__
      ), on = "row_id"]
    }
  }

  id_cols <- c(
    "row_id",
    prep$group_col,
    prep$time_col,
    prep$entity_col,
    "sample",
    "y_actual",
    "pred",
    "pred_q05",
    "pred_q50",
    "pred_q95",
    "residual",
    "rescale_factor",
    "y_model_scale",
    "pred_model_scale",
    "residual_model_scale"
  )
  id_cols <- unique(id_cols[id_cols %in% names(dt)])

  wide_keep <- unique(c(id_cols, reporting_cols, contrib_cols))
  wide_decomp <- dt[, ..wide_keep]

  long_decomp <- melt(
    wide_decomp,
    id.vars = setdiff(wide_keep, contrib_cols),
    measure.vars = contrib_cols,
    variable.name = "variable",
    value.name = "contribution"
  )

  idc <- setdiff(names(wide_decomp), contrib_cols)
  residual_long <- copy(wide_decomp[, idc, with = FALSE])
  residual_long[, variable := "residual"]
  residual_long[, contribution := residual]

  long_decomp <- rbindlist(list(long_decomp, residual_long), use.names = TRUE, fill = TRUE)[order(row_id, variable)]

  list(
    coef_long = coef_long,
    coef_by_group = coef_by_group,
    coef_by_mod_id = coef_by_mod_id,
    wide_decomp = wide_decomp,
    long_decomp = long_decomp
  )
}


build_fit_quality_diagnostics <- function(wide_decomp, group_col, time_col) {
  dt <- copy(as.data.table(wide_decomp))
  req <- c("y_actual", "pred", "residual")
  if (!all(req %in% names(dt))) return(list(overall = data.table(), by_group = data.table()))

  calc <- function(x) {
    y <- x$y_actual
    p <- x$pred
    r <- x$residual
    denom <- sum((y - mean(y, na.rm = TRUE))^2, na.rm = TRUE)
    data.table(
      n = sum(is.finite(y) & is.finite(p)),
      actual_mean = mean(y, na.rm = TRUE),
      pred_mean = mean(p, na.rm = TRUE),
      residual_mean = mean(r, na.rm = TRUE),
      rmse = sqrt(mean(r^2, na.rm = TRUE)),
      mae = mean(abs(r), na.rm = TRUE),
      mape = mean(abs(r / fifelse(abs(y) <= 1e-8, NA_real_, y)), na.rm = TRUE),
      smape = mean(2 * abs(r) / fifelse(abs(y) + abs(p) <= 1e-8, NA_real_, abs(y) + abs(p)), na.rm = TRUE),
      r2 = if (is.finite(denom) && denom > 0) 1 - sum(r^2, na.rm = TRUE) / denom else NA_real_,
      residual_acf1 = {
        rr <- r[is.finite(r)]
        if (length(rr) > 2L) suppressWarnings(stats::acf(rr, plot = FALSE, lag.max = 1)$acf[2]) else NA_real_
      }
    )
  }

  overall <- calc(dt)
  if ("sample" %in% names(dt)) overall[, sample := "all"]

  by_sample <- data.table()
  if ("sample" %in% names(dt)) {
    by_sample <- dt[order(get(time_col)), calc(.SD), by = sample]
  }

  by_group <- data.table()
  if (group_col %in% names(dt)) {
    by_group <- dt[order(get(time_col)), calc(.SD), by = group_col]
    if ("sample" %in% names(dt)) {
      by_group_sample <- dt[order(get(time_col)), calc(.SD), by = c(group_col, "sample")]
      by_group <- rbindlist(list(by_group[, sample := "all"], by_group_sample), use.names = TRUE, fill = TRUE)
    }
  }
  list(overall = rbindlist(list(overall, by_sample), use.names = TRUE, fill = TRUE), by_group = by_group)
}


build_diagnostics_hier_mmm <- function(fit, max_treedepth = NA_integer_) {
  parameter_summary <- summarize_fit_vars(fit)

  sampler_chain <- data.table()
  sampler_overall <- data.table()

  sdiag <- tryCatch(fit$sampler_diagnostics(), error = function(e) NULL)
  if (!is.null(sdiag)) {
    sdiag_arr <- as.array(sdiag)
    dn <- dimnames(sdiag_arr)
    diag_names <- dn[[3]]
    n_iter <- dim(sdiag_arr)[1]
    n_chain <- dim(sdiag_arr)[2]

    get_diag <- function(name) {
      if (!(name %in% diag_names)) return(NULL)
      out <- sdiag_arr[, , match(name, diag_names), drop = TRUE]
      if (is.null(dim(out))) out <- matrix(out, ncol = 1L)
      if (length(dim(out)) == 1L) out <- matrix(out, ncol = 1L)
      out
    }

    div <- get_diag("divergent__")
    td <- get_diag("treedepth__")
    energy <- get_diag("energy__")
    accept <- get_diag("accept_stat__")
    leapfrog <- get_diag("n_leapfrog__")

    chain_list <- vector("list", n_chain)
    for (ch in seq_len(n_chain)) {
      div_n <- if (!is.null(div)) sum(div[, ch], na.rm = TRUE) else NA_real_
      td_n <- if (!is.null(td) && is.finite(max_treedepth)) sum(td[, ch] >= max_treedepth, na.rm = TRUE) else NA_real_
      bfmi <- NA_real_
      if (!is.null(energy)) {
        e <- as.numeric(energy[, ch])
        if (length(e) > 2 && stats::var(e) > 0) {
          bfmi <- mean(diff(e)^2) / stats::var(e)
        }
      }
      chain_list[[ch]] <- data.table(
        chain = ch,
        iterations = n_iter,
        divergences = div_n,
        treedepth_hits = td_n,
        mean_accept_stat = if (!is.null(accept)) mean(accept[, ch], na.rm = TRUE) else NA_real_,
        mean_n_leapfrog = if (!is.null(leapfrog)) mean(leapfrog[, ch], na.rm = TRUE) else NA_real_,
        min_energy_bfmi = bfmi
      )
    }
    sampler_chain <- rbindlist(chain_list, fill = TRUE)
    sampler_overall <- sampler_chain[, .(
      chains = .N,
      iterations_total = sum(iterations, na.rm = TRUE),
      divergences_total = sum(divergences, na.rm = TRUE),
      treedepth_hits_total = sum(treedepth_hits, na.rm = TRUE),
      mean_accept_stat = mean(mean_accept_stat, na.rm = TRUE),
      mean_n_leapfrog = mean(mean_n_leapfrog, na.rm = TRUE),
      min_bfmi = finite_min_or_na(min_energy_bfmi),
      max_rhat = if (n_chain >= 2L && "rhat" %in% names(parameter_summary)) finite_max_or_na(parameter_summary$rhat) else NA_real_,
      min_ess_bulk = if ("ess_bulk" %in% names(parameter_summary)) finite_min_or_na(parameter_summary$ess_bulk) else NA_real_,
      min_ess_tail = if ("ess_tail" %in% names(parameter_summary)) finite_min_or_na(parameter_summary$ess_tail) else NA_real_
    )]
  }

  diagnostics_flags <- data.table()
  if (nrow(sampler_overall)) {
    so <- sampler_overall[1]
    diagnostics_flags <- data.table(
      any_divergences = isTRUE(so$divergences_total > 0),
      any_treedepth_hits = isTRUE(so$treedepth_hits_total > 0),
      max_rhat_over_1_01 = isTRUE(is.finite(so$max_rhat) && so$max_rhat > 1.01),
      min_ess_bulk_under_400 = isTRUE(is.finite(so$min_ess_bulk) && so$min_ess_bulk < 400),
      min_ess_tail_under_400 = isTRUE(is.finite(so$min_ess_tail) && so$min_ess_tail < 400),
      min_bfmi_under_0_3 = isTRUE(is.finite(so$min_bfmi) && so$min_bfmi < 0.3)
    )
  }

  ucm_pattern <- "^(alpha|alpha_flat|level|level0|sigma_level|season|cycle|intercept_)|^(level_component|season_component|cycle_component|level_state)\\["
  if ("variable" %in% names(parameter_summary)) {
    parameter_summary[, diagnostic_block := fifelse(grepl(ucm_pattern, variable), "ucm", "bayesian_model")]
    parameter_summary_ucm <- parameter_summary[diagnostic_block == "ucm"]
    parameter_summary_bayes <- parameter_summary[diagnostic_block == "bayesian_model"]
  } else {
    parameter_summary_ucm <- data.table()
    parameter_summary_bayes <- parameter_summary
  }

  list(
    parameter_summary = parameter_summary,
    parameter_summary_bayes = parameter_summary_bayes,
    parameter_summary_ucm = parameter_summary_ucm,
    sampler_chain = sampler_chain,
    sampler_overall = sampler_overall,
    diagnostics_flags = diagnostics_flags
  )
}

recommend_hier_mmm_sampler_settings <- function(fit_or_diagnostics,
                                                current_max_treedepth = NA_integer_,
                                                iter_warmup = NA_integer_,
                                                iter_sampling = NA_integer_) {
  diagnostics <- if (is.list(fit_or_diagnostics) && !is.null(fit_or_diagnostics[["diagnostics"]])) {
    fit_or_diagnostics[["diagnostics"]]
  } else {
    fit_or_diagnostics
  }
  so <- diagnostics$sampler_overall %||% data.table()
  flags <- diagnostics$diagnostics_flags %||% data.table()
  recs <- list()
  add_rec <- function(priority, issue, recommendation, detail = "") {
    recs[[length(recs) + 1L]] <<- data.table(
      priority = priority,
      issue = issue,
      recommendation = recommendation,
      detail = detail
    )
  }
  if (nrow(flags) && isTRUE(flags$any_divergences[1])) {
    add_rec(
      "high",
      "divergences",
      "Increase adapt_delta, inspect tight coefficient/curve priors, and review collinear media before trusting posterior intervals.",
      if (nrow(so)) paste0("divergences_total=", so$divergences_total[1]) else ""
    )
  }
  if (nrow(flags) && isTRUE(flags$any_treedepth_hits[1])) {
    short_run <- is.finite(iter_warmup) && !is.na(iter_warmup) && iter_warmup < 300L
    add_rec(
      "medium",
      "max_treedepth_hits",
      if (short_run) {
        "This looks like a smoke-test geometry warning first: rerun with at least 300 warmup draws before changing the model. If hits persist under production warmup, compare normal likelihood, centered predictors, tighter external priors, and aggregation or simplification of weakly identified media/baseline terms."
      } else {
        "Max-treedepth pressure is posterior geometry, not a data-plumbing failure. For production runs, use longer warmup/sampling and max_treedepth 12-14; if hits persist, compare normal likelihood, centered predictors, tighter external priors, and aggregation or simplification of weakly identified media/baseline terms."
      },
      if (nrow(so)) paste0("treedepth_hits_total=", so$treedepth_hits_total[1],
                           "; current_max_treedepth=", current_max_treedepth,
                           "; iter_warmup=", iter_warmup,
                           "; iter_sampling=", iter_sampling) else ""
    )
  }
  if (nrow(flags) && (isTRUE(flags$max_rhat_over_1_01[1]) || isTRUE(flags$min_ess_bulk_under_400[1]) || isTRUE(flags$min_ess_tail_under_400[1]))) {
    add_rec(
      "medium",
      "low_effective_sample_or_rhat",
      "Use at least 4 chains, longer warmup/sampling, and review prior/posterior movement for curve and coefficient parameters.",
      if (nrow(so)) paste0("max_rhat=", signif(so$max_rhat[1], 4), "; min_ess_bulk=", signif(so$min_ess_bulk[1], 4), "; min_ess_tail=", signif(so$min_ess_tail[1], 4)) else ""
    )
  }
  if (nrow(flags) && isTRUE(flags$min_bfmi_under_0_3[1])) {
    add_rec(
      "medium",
      "low_bfmi",
      "Try stronger scale priors, better initialization, or simpler baseline structure before interpreting tail behavior.",
      if (nrow(so)) paste0("min_bfmi=", signif(so$min_bfmi[1], 4)) else ""
    )
  }
  if (!length(recs)) {
    add_rec("ok", "no_sampler_red_flags", "No immediate sampler setting change suggested from available diagnostics.", "")
  }
  rbindlist(recs, use.names = TRUE, fill = TRUE)
}

build_hier_mmm_model_readiness <- function(diagnostics,
                                           iter_warmup = NA_integer_,
                                           iter_sampling = NA_integer_,
                                           max_treedepth = NA_integer_,
                                           production_warmup_floor = 300L) {
  issues <- list()
  add_issue <- function(severity, area, issue, detail = "", recommendation = "") {
    issues[[length(issues) + 1L]] <<- data.table(
      severity = severity,
      area = area,
      issue = issue,
      detail = as.character(detail %||% ""),
      recommendation = as.character(recommendation %||% "")
    )
  }

  iter_warmup <- as.integer(iter_warmup %||% NA_integer_)
  iter_sampling <- as.integer(iter_sampling %||% NA_integer_)
  max_treedepth <- as.integer(max_treedepth %||% NA_integer_)
  production_warmup_floor <- as.integer(production_warmup_floor %||% 300L)
  short_run <- is.finite(iter_warmup) && !is.na(iter_warmup) && iter_warmup < production_warmup_floor

  so <- diagnostics$sampler_overall %||% data.table()
  fq <- diagnostics$fit_quality_overall %||% data.table()
  col <- diagnostics$collinearity_overall %||% data.table()
  coef_pp <- diagnostics$prior_posterior_coef %||% data.table()
  curve_pp <- diagnostics$prior_posterior_curve %||% data.table()

  if (!nrow(so)) {
    add_issue("warning", "sampler", "missing_sampler_diagnostics",
              "Sampler diagnostics were not available in the fit object.",
              "Do not treat the fit as production-reviewed until sampler diagnostics are available.")
  } else {
    so1 <- so[1]
    if (isTRUE(is.finite(so1$chains) && so1$chains < 2)) {
      add_issue("review", "sampler", "single_chain_run",
                paste0("chains=", so1$chains),
                "Use at least 2 chains for convergence screening and 4 chains for production posterior review.")
    } else if (isTRUE(is.finite(so1$chains) && so1$chains < 4)) {
      add_issue("review", "sampler", "fewer_than_four_chains",
                paste0("chains=", so1$chains),
                "Use 4 chains for final production posterior review.")
    }
    if (isTRUE(so1$divergences_total > 0)) {
      add_issue("blocker", "sampler", "divergences",
                paste0("divergences_total=", so1$divergences_total),
                "Do not use posterior intervals or optimizer outputs until divergences are resolved.")
    }
    if (isTRUE(is.finite(so1$min_bfmi) && so1$min_bfmi < 0.3)) {
      add_issue("blocker", "sampler", "low_bfmi",
                paste0("min_bfmi=", signif(so1$min_bfmi, 4)),
                "Review scale priors, initialization, and weakly identified latent structure.")
    }
    if (isTRUE(so1$treedepth_hits_total > 0)) {
      add_issue(
        if (short_run) "review" else "warning",
        "sampler",
        "max_treedepth_hits",
        paste0("treedepth_hits_total=", so1$treedepth_hits_total,
               "; max_treedepth=", max_treedepth,
               "; iter_warmup=", iter_warmup),
        if (short_run) {
          "Rerun with production warmup before treating this as a model failure."
        } else {
          "Compare geometry settings, simplify weakly identified terms, or strengthen credible external priors."
        }
      )
    }
    rhat_valid <- isTRUE(is.finite(so1$chains) && so1$chains >= 2)
    if (rhat_valid && isTRUE(is.finite(so1$max_rhat) && so1$max_rhat > 1.05)) {
      add_issue("blocker", "sampler", "rhat_over_1_05",
                paste0("max_rhat=", signif(so1$max_rhat, 4)),
                "Run longer chains or simplify the model before using fit outputs.")
    } else if (rhat_valid && isTRUE(is.finite(so1$max_rhat) && so1$max_rhat > 1.01)) {
      add_issue("warning", "sampler", "rhat_over_1_01",
                paste0("max_rhat=", signif(so1$max_rhat, 4)),
                "Use longer chains and review affected parameters.")
    }
    if (isTRUE(is.finite(so1$min_ess_bulk) && so1$min_ess_bulk < 400)) {
      add_issue("review", "sampler", "low_bulk_ess",
                paste0("min_ess_bulk=", signif(so1$min_ess_bulk, 4)),
                "Use longer sampling for production intervals.")
    }
    if (isTRUE(is.finite(so1$min_ess_tail) && so1$min_ess_tail < 400)) {
      add_issue("review", "sampler", "low_tail_ess",
                paste0("min_ess_tail=", signif(so1$min_ess_tail, 4)),
                "Use longer sampling before relying on tail intervals.")
    }
  }

  if (short_run) {
    add_issue("info", "run_length", "short_warmup_smoke_run",
              paste0("iter_warmup=", iter_warmup, "; production_floor=", production_warmup_floor),
              "Use this run for plumbing and geometry screening, not final posterior claims.")
  }

  if (nrow(fq) && "sample" %in% names(fq)) {
    train <- fq[sample == "train"]
    holdout <- fq[sample == "holdout"]
    if (nrow(train) && is.finite(train$r2[1]) && train$r2[1] < 0.4) {
      add_issue("warning", "fit", "weak_train_fit",
                paste0("train_r2=", signif(train$r2[1], 4)),
                "Review missing controls, lag structure, transformations, and prior strength.")
    }
    if (nrow(holdout) && is.finite(holdout$r2[1]) && holdout$r2[1] < 0) {
      add_issue("review", "fit", "weak_holdout_fit",
                paste0("holdout_r2=", signif(holdout$r2[1], 4)),
                "Treat decisioning outputs cautiously and inspect holdout periods.")
    }
  }

  if (nrow(col)) {
    top_col <- col[order(-abs_correlation)][1]
    add_issue("review", "identification", "high_collinearity",
              paste0("top_pair=", top_col$variable_1, "|", top_col$variable_2,
                     "; abs_correlation=", signif(top_col$abs_correlation, 4),
                     "; flagged_pairs=", nrow(col)),
              "Expect coefficient/curve tradeoffs; use external, quasi-geo, or business-prior evidence where credible.")
  }

  if (nrow(coef_pp) && all(c("posterior_mean", "near_prior_flag", "far_from_prior_flag") %in% names(coef_pp))) {
    pp_ok <- coef_pp[is.finite(posterior_mean)]
    if (nrow(pp_ok)) {
      near_share <- pp_ok[, mean(near_prior_flag %in% TRUE, na.rm = TRUE)]
      far_share <- pp_ok[, mean(far_from_prior_flag %in% TRUE, na.rm = TRUE)]
      if (is.finite(near_share) && near_share >= 0.75) {
        add_issue("review", "prior_posterior", "coef_posterior_close_to_prior",
                  paste0("near_prior_share=", signif(near_share, 4)),
                  "Check that tight priors are intentionally calibration-grade rather than compensating for weak identification.")
      }
      if (is.finite(far_share) && far_share >= 0.25) {
        add_issue("review", "prior_posterior", "coef_posterior_far_from_prior",
                  paste0("far_from_prior_share=", signif(far_share, 4)),
                  "Review whether prior centers are stale, mismatched to KPI scale, or contradicted by the data.")
      }
    }
  }

  if (nrow(curve_pp) && all(c("posterior_mean", "far_from_prior_flag") %in% names(curve_pp))) {
    curve_ok <- curve_pp[is.finite(posterior_mean)]
    if (nrow(curve_ok)) {
      far_curve_share <- curve_ok[, mean(far_from_prior_flag %in% TRUE, na.rm = TRUE)]
      if (is.finite(far_curve_share) && far_curve_share >= 0.25) {
        add_issue("review", "prior_posterior", "curve_posterior_far_from_prior",
                  paste0("far_from_prior_share=", signif(far_curve_share, 4)),
                  "Review curve prior construction and do not over-interpret saturation without independent support.")
      }
    }
  }

  issue_dt <- if (length(issues)) rbindlist(issues, use.names = TRUE, fill = TRUE) else data.table(
    severity = "ok",
    area = "overall",
    issue = "no_major_readiness_flags",
    detail = "",
    recommendation = "Proceed with standard MMM review: prior predictive checks, posterior diagnostics, fit quality, and business sanity checks."
  )
  rank_lookup <- c(ok = 0L, info = 1L, review = 2L, warning = 3L, blocker = 4L)
  issue_dt[, severity_rank := as.integer(rank_lookup[severity])]
  issue_dt[is.na(severity_rank), severity_rank := 0L]
  setorder(issue_dt, -severity_rank, area, issue)
  top_rank <- issue_dt[1, severity_rank]
  status <- if (top_rank >= 4L) {
    "not_ready"
  } else if (top_rank >= 3L) {
    "review_before_use"
  } else if (top_rank >= 2L) {
    "usable_with_review"
  } else {
    "usable_after_standard_review"
  }

  list(
    summary = data.table(
      model_readiness = status,
      highest_severity = issue_dt[1, severity],
      issue_count = issue_dt[issue != "no_major_readiness_flags", .N],
      blocker_count = issue_dt[severity == "blocker", .N],
      warning_count = issue_dt[severity == "warning", .N],
      review_count = issue_dt[severity == "review", .N],
      info_count = issue_dt[severity == "info", .N],
      short_warmup_smoke_run = short_run,
      iter_warmup = iter_warmup,
      iter_sampling = iter_sampling,
      max_treedepth = max_treedepth,
      top_issue = issue_dt[1, issue],
      top_recommendation = issue_dt[1, recommendation]
    ),
    issues = issue_dt[]
  )
}

build_ucm_component_diagnostics <- function(wide_decomp, group_col, time_col) {
  dt <- copy(as.data.table(wide_decomp))
  component_cols <- intersect(c("intercept_flat", "intercept_level", "intercept_seasonality", "intercept_cycle", "intercept_total"), names(dt))
  if (!length(component_cols)) return(list(overall = data.table(), by_group = data.table()))

  calc <- function(x) {
    rbindlist(lapply(component_cols, function(cc) {
      z <- x[[cc]]
      data.table(
        component = cc,
        n = sum(is.finite(z)),
        mean = mean(z, na.rm = TRUE),
        sd = safe_sd(z),
        min = if (any(is.finite(z))) suppressWarnings(min(z, na.rm = TRUE)) else NA_real_,
        max = if (any(is.finite(z))) suppressWarnings(max(z, na.rm = TRUE)) else NA_real_,
        contribution_share_of_actual = if ("y_actual" %in% names(x) && abs(sum(x$y_actual, na.rm = TRUE)) > 1e-8) sum(z, na.rm = TRUE) / sum(x$y_actual, na.rm = TRUE) else NA_real_,
        acf1 = {
          zz <- z[is.finite(z)]
          if (length(zz) > 2L) suppressWarnings(stats::acf(zz, plot = FALSE, lag.max = 1)$acf[2]) else NA_real_
        },
        corr_with_residual = if ("residual" %in% names(x)) suppressWarnings(stats::cor(z, x$residual, use = "complete.obs")) else NA_real_,
        corr_with_actual = if ("y_actual" %in% names(x)) suppressWarnings(stats::cor(z, x$y_actual, use = "complete.obs")) else NA_real_
      )
    }), fill = TRUE)
  }

  overall <- calc(dt)
  by_group <- data.table()
  if (group_col %in% names(dt)) {
    by_group <- dt[order(get(time_col)), calc(.SD), by = group_col]
  }
  list(overall = overall, by_group = by_group)
}

build_prior_posterior_diagnostics <- function(fit, prep, outputs) {
  sm <- summarize_fit_vars(fit)
  if (!("variable" %in% names(sm))) {
    return(list(coef = data.table(), curve = data.table()))
  }

  sd_col <- pick_first_existing_col(sm, c("sd", "stddev", "std_dev"))
  q05_col <- pick_first_existing_col(sm, c("q5", "q05", "5%", "5.0%", "q_5"))
  q50_col <- pick_first_existing_col(sm, c("median", "q50", "50%", "50.0%", "q_50"))
  q95_col <- pick_first_existing_col(sm, c("q95", "95%", "95.0%", "q_95"))

  param_stats <- function(param_names) {
    out <- data.table(parameter = param_names)
    stat <- sm[, .(
      parameter = variable,
      posterior_mean = mean,
      posterior_sd = if (!is.na(sd_col)) get(sd_col) else NA_real_,
      posterior_q05 = if (!is.na(q05_col)) get(q05_col) else NA_real_,
      posterior_q50 = if (!is.na(q50_col)) get(q50_col) else NA_real_,
      posterior_q95 = if (!is.na(q95_col)) get(q95_col) else NA_real_
    )]
    out[stat, on = "parameter", `:=`(
      posterior_mean = i.posterior_mean,
      posterior_sd = i.posterior_sd,
      posterior_q05 = i.posterior_q05,
      posterior_q50 = i.posterior_q50,
      posterior_q95 = i.posterior_q95
    )]
    out[]
  }

  coef_diag <- copy(outputs$coef_long)
  if (!all(c("group_idx", "variable_idx") %in% names(coef_diag))) {
    coef_diag[, group_idx := match(mod_id, prep$group_lookup$group_value)]
    coef_diag[, variable_idx := match(variable, prep$variable_lookup$variable)]
  }
  coef_diag[, parameter := sprintf("beta[%d,%d]", group_idx, variable_idx)]
  coef_stats <- param_stats(coef_diag$parameter)
  coef_diag[coef_stats, on = "parameter", `:=`(
    posterior_mean = i.posterior_mean,
    posterior_sd = i.posterior_sd,
    posterior_q05 = i.posterior_q05,
    posterior_q50 = i.posterior_q50,
    posterior_q95 = i.posterior_q95
  )]

  md_coef <- copy(prep$metadata)[, .(
    variable,
    prior_center = coef,
    coef_precision,
    coef_bound,
    bound_type,
    coef_lower,
    coef_upper
  )]
  md_coef[, prior_sd_nominal := 1 / sqrt(pmax(coef_precision, 1e-8))]
  md_coef[, prior_sd_nominal_relaxed := prior_sd_nominal * (prep$coef_prior_relax %||% 1.5)]
  md_coef[, prior_sd_floor := (prep$min_coef_sd_frac %||% 0.05) * abs(prior_center) + (prep$min_coef_sd_abs %||% 0.001)]
  md_coef[, prior_sd_used := fifelse(
    is.finite(coef_precision) & coef_precision >= (prep$hard_prior_precision_threshold %||% 1e6),
    pmax(prior_sd_nominal_relaxed, 1e-8),
    pmax(prior_sd_nominal_relaxed, prior_sd_floor)
  )]

  coef_diag[md_coef, on = "variable", `:=`(
    prior_center = i.prior_center,
    prior_sd_nominal = i.prior_sd_nominal,
    prior_sd_used = i.prior_sd_used,
    coef_precision = i.coef_precision,
    coef_bound = i.coef_bound,
    bound_type = i.bound_type,
    coef_lower = i.coef_lower,
    coef_upper = i.coef_upper
  )]
  coef_diag[, `:=`(
    posterior_minus_prior = posterior_mean - prior_center,
    posterior_z_vs_prior = (posterior_mean - prior_center) / pmax(prior_sd_used, 1e-8),
    posterior_sd_to_prior_sd = posterior_sd / pmax(prior_sd_used, 1e-8),
    near_prior_flag = is.finite(posterior_mean) & abs(posterior_mean - prior_center) <= 0.10 * pmax(prior_sd_used, 1e-8),
    far_from_prior_flag = is.finite(posterior_mean) & abs(posterior_mean - prior_center) >= 2.00 * pmax(prior_sd_used, 1e-8)
  )]

  curve_md <- copy(prep$metadata)[has_curve == 1L]
  curve_diag <- data.table()
  if (nrow(curve_md)) {
    curve_long <- rbindlist(list(
      curve_md[, .(
        variable,
        curve_parameter = "rrate",
        internal_stan_parameter = "rrate",
        prior_center = rrate,
        prior_precision = rrate_precision,
        lower = rrate_lower,
        upper = rrate_upper
      )],
      curve_md[, .(
        variable,
        curve_parameter = "curve_rate",
        internal_stan_parameter = "cvalue",
        prior_center = cvalue,
        prior_precision = cvalue_precision,
        lower = cvalue_lower,
        upper = cvalue_upper
      )],
      curve_md[, .(
        variable,
        curve_parameter = "dvalue",
        internal_stan_parameter = "dvalue",
        prior_center = dvalue,
        prior_precision = dvalue_precision,
        lower = dvalue_lower,
        upper = dvalue_upper
      )]
    ), fill = TRUE)
    curve_long[prep$variable_lookup[, .(variable, variable_idx, curve_param_idx)], on = "variable", `:=`(
      variable_idx = i.variable_idx,
      curve_param_idx = i.curve_param_idx
    )]
    curve_long[, parameter := sprintf("%s[%d]", internal_stan_parameter, curve_param_idx)]
    curve_long[, prior_sd_nominal := 1 / sqrt(pmax(prior_precision, 1e-8))]
    curve_long[, prior_sd_used := pmax(
      prior_sd_nominal * (prep$curve_prior_relax %||% 2),
      0.08 * pmax(upper - lower, 1e-8)
    )]
    curve_long[curve_parameter == "dvalue" & isFALSE(prep$estimate_dvalue), prior_sd_used := pmin(prior_sd_used, 1e-4)]

    curve_stats <- param_stats(curve_long$parameter)
    curve_diag <- copy(curve_long)
    curve_diag[curve_stats, on = "parameter", `:=`(
      posterior_mean = i.posterior_mean,
      posterior_sd = i.posterior_sd,
      posterior_q05 = i.posterior_q05,
      posterior_q50 = i.posterior_q50,
      posterior_q95 = i.posterior_q95
    )]
    curve_diag[, `:=`(
      posterior_minus_prior = posterior_mean - prior_center,
      posterior_z_vs_prior = (posterior_mean - prior_center) / pmax(prior_sd_used, 1e-8),
      posterior_sd_to_prior_sd = posterior_sd / pmax(prior_sd_used, 1e-8),
      near_prior_flag = is.finite(posterior_mean) & abs(posterior_mean - prior_center) <= 0.10 * pmax(prior_sd_used, 1e-8),
      far_from_prior_flag = is.finite(posterior_mean) & abs(posterior_mean - prior_center) >= 2.00 * pmax(prior_sd_used, 1e-8)
    )]
  }

  list(coef = coef_diag[], curve = curve_diag[])
}


build_collinearity_diagnostics <- function(prep, threshold = 0.85) {
  dt <- copy(prep$data)
  vars <- prep$variable_lookup$variable
  vars <- vars[vars %in% names(dt)]
  if (length(vars) < 2L) return(list(overall = data.table(), by_group = data.table()))

  pair_cor <- function(x, scope = "overall", group_value = NA_character_) {
    x <- as.data.table(x)
    mat <- as.matrix(x[, ..vars])
    storage.mode(mat) <- "double"
    cm <- suppressWarnings(stats::cor(mat, use = "pairwise.complete.obs"))
    idx <- which(upper.tri(cm), arr.ind = TRUE)
    if (!nrow(idx)) return(data.table())
    out <- data.table(
      scope = scope,
      group_value = group_value,
      variable_1 = colnames(cm)[idx[, 1]],
      variable_2 = colnames(cm)[idx[, 2]],
      correlation = cm[idx],
      abs_correlation = abs(cm[idx])
    )
    out <- out[is.finite(abs_correlation) & abs_correlation >= threshold]
    out[order(-abs_correlation)]
  }

  overall <- pair_cor(dt, scope = "overall", group_value = NA_character_)
  by_group <- data.table()
  if (prep$group_col %in% names(dt)) {
    by_group <- rbindlist(lapply(split(dt, dt[[prep$group_col]]), function(z) {
      pair_cor(z, scope = "group", group_value = as.character(z[[prep$group_col]][1]))
    }), fill = TRUE)
  }

  role_map <- prep$variable_lookup[, .(
    variable,
    role,
    source_entity,
    has_curve
  )]
  add_roles <- function(x) {
    if (!nrow(x)) return(x)
    x[role_map, on = .(variable_1 = variable), `:=`(
      role_1 = i.role,
      source_entity_1 = i.source_entity,
      has_curve_1 = i.has_curve
    )]
    x[role_map, on = .(variable_2 = variable), `:=`(
      role_2 = i.role,
      source_entity_2 = i.source_entity,
      has_curve_2 = i.has_curve
    )]
    x[]
  }

  list(overall = add_roles(overall), by_group = add_roles(by_group))
}


build_ucm_media_stealing_diagnostics <- function(wide_decomp,
                                                 variable_lookup,
                                                 group_col,
                                                 time_col) {
  dt <- copy(as.data.table(wide_decomp))
  media_cols <- variable_lookup[role %in% c("media", "reach_frequency"), variable]
  media_cols <- media_cols[media_cols %in% names(dt)]
  ucm_col <- if ("intercept_total" %in% names(dt)) "intercept_total" else if ("intercept" %in% names(dt)) "intercept" else NA_character_
  if (!length(media_cols) || is.na(ucm_col)) return(list(overall = data.table(), by_group = data.table()))

  dt[, media_total__ := rowSums(.SD, na.rm = TRUE), .SDcols = media_cols]
  dt[, ucm_total__ := get(ucm_col)]

  calc <- function(x) {
    y_sum <- if ("y_actual" %in% names(x)) sum(x$y_actual, na.rm = TRUE) else NA_real_
    media_sum <- sum(x$media_total__, na.rm = TRUE)
    ucm_sum <- sum(x$ucm_total__, na.rm = TRUE)
    data.table(
      n = nrow(x),
      ucm_column = ucm_col,
      media_columns = paste(media_cols, collapse = "|"),
      ucm_sum = ucm_sum,
      media_sum = media_sum,
      ucm_share_of_actual = if (is.finite(y_sum) && abs(y_sum) > 1e-8) ucm_sum / y_sum else NA_real_,
      media_share_of_actual = if (is.finite(y_sum) && abs(y_sum) > 1e-8) media_sum / y_sum else NA_real_,
      corr_ucm_media = safe_cor(x$ucm_total__, x$media_total__),
      corr_ucm_actual = if ("y_actual" %in% names(x)) safe_cor(x$ucm_total__, x$y_actual) else NA_real_,
      corr_ucm_residual = if ("residual" %in% names(x)) safe_cor(x$ucm_total__, x$residual) else NA_real_,
      ucm_acf1 = {
        zz <- x$ucm_total__[is.finite(x$ucm_total__)]
        if (length(zz) > 2L) suppressWarnings(stats::acf(zz, plot = FALSE, lag.max = 1)$acf[2]) else NA_real_
      },
      media_acf1 = {
        zz <- x$media_total__[is.finite(x$media_total__)]
        if (length(zz) > 2L) suppressWarnings(stats::acf(zz, plot = FALSE, lag.max = 1)$acf[2]) else NA_real_
      },
      high_ucm_media_corr_flag = is.finite(safe_cor(x$ucm_total__, x$media_total__)) && abs(safe_cor(x$ucm_total__, x$media_total__)) >= 0.70,
      high_ucm_share_flag = is.finite(y_sum) && abs(y_sum) > 1e-8 && abs(ucm_sum / y_sum) >= 0.80
    )
  }

  overall <- calc(dt)
  by_group <- data.table()
  if (group_col %in% names(dt)) {
    by_group <- dt[order(get(time_col)), calc(.SD), by = group_col]
  }
  list(overall = overall, by_group = by_group)
}



