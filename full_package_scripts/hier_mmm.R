    gamma_prior_sd <- ucm_cfg$gamma_prior_sd
    sigma_y_prior_sd <- ucm_cfg$sigma_y_prior_sd
  }
  md <- clean_metadata(
    raw_metadata,
    bound_one_sided_coef_defaults = bound_one_sided_coef_defaults,
    default_positive_coef_upper = default_positive_coef_upper,
    default_negative_coef_lower = default_negative_coef_lower,
    default_one_sided_coef_span = default_one_sided_coef_span
  )

  req_data <- c(dep_var_col, group_col, time_col, entity_col)
  if (!all(req_data %in% names(dt))) stop("Data is missing one of: ", paste(req_data, collapse = ", "))
  if (!all(md$variable %in% names(dt))) {
    stop("These metadata variables are missing from data: ", paste(setdiff(md$variable, names(dt)), collapse = ", "))
  }
  if (!is.null(dep_scale_col) && !(dep_scale_col %in% names(dt))) {
    stop("dep_scale_col not found in data: ", dep_scale_col)
  }
  if (!is.null(holdout_col) && !(holdout_col %in% names(dt))) {
    stop("holdout_col not found in data: ", holdout_col)
  }
  holiday_controls <- add_holiday_controls_hier_mmm(
    data = dt,
    time_col = time_col,
    holiday_config = holiday_config,
    existing_extra_cols = extra_control_cols
  )
  dt <- holiday_controls$data
  extra_control_cols <- holiday_controls$extra_control_cols
